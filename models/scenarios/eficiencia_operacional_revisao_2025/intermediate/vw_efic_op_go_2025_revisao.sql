SELECT
	T.*,
	CASE
		WHEN (T.FLAG_RET + T.FLAG_PRAZO + T.FLAG_IMPRODUTIVA + T.FLAG_IMPROCEDENCIA + T.FLAG_REINCIDENCIA) >= 1 THEN 'INEF.'
		ELSE 'EFIC.'
	END FLAG_TOTAL,
	CASE
		WHEN T.FLAG_REINCIDENCIA = 1 THEN 'REINCIDENCIA'
		WHEN T.FLAG_IMPROCEDENCIA = 1 THEN 'IMPROCEDENCIA'
		WHEN T.FLAG_RET = 1 THEN 'RETRABALHO'
		WHEN T.FLAG_PRAZO = 1 THEN 'PRAZO'
		WHEN T.FLAG_IMPRODUTIVA = 1 THEN 'IMPRODUTIVIDADE_UPS'
		ELSE ''
	END CATEGORIA
FROM
	(
	--RS
	SELECT
		DISTINCT
        ups.empresa,
		ups.p_Regional regional_prx,
		ups.p_Polo polo_prx,
		ups.p_pROCESSO processo,
		ups.serv_campo,
		ups.p_fornecedor fonecedor,
		ups.equipe,
		to_char(ups.os_oper) OS_OPER,
		ups.os,
		ups.ostipo_id,
		ups.ossubtipo_id,
		ups.tipo_conclusao,
		CASE
			WHEN UPS.ostipo_id <> 'IS' AND UPS.Ch_Ups IN ('CORTE IMPRODUTIVO', 'SERVICO INVALIDO', 'SERVICO IMPRODUTIVO', 'MT IMPRODUTIVA', 'EMERGENCIA IMPRODUTIVA', 'RELIGACAO IMPRODUTIVA') THEN 'IMPRODUTIVO'
			ELSE 'PRODUTIVO'
		END tipo,
		ups.ch_ups,
		ups.UPS valor_ups,
		ups.competencia,
		ups.data_conclusao,
		UPS.DIAS,
		ups.turnos,
		to_number(RET.OS_OPER_ORIGEM) as OS_OPER_ORIGEM,
		RET.PREFIXO_ORIGEM,
		RET.TIPO_ORIGEM,
		RET.TIPO_CONCLUSAO_ORIGEM,
		'' codigo_medida,
		'' STATUS_PRAZO,
		--PROCEDENCIA,
		INC.REINCIDENTE_90_DIAS,
		INC.N_REINC,
		CASE
			WHEN RET.OS_OPER_ORIGEM IS NOT NULL THEN 1
			ELSE 0
		END FLAG_RET,
		0 FLAG_PRAZO,
		CASE
			WHEN UPS.ostipo_id <> 'IS' AND UPS.Ch_Ups IN ('CORTE IMPRODUTIVO', 'SERVICO INVALIDO', 'SERVICO IMPRODUTIVO', 'MT IMPRODUTIVA', 'EMERGENCIA IMPRODUTIVA', 'RELIGACAO IMPRODUTIVA') THEN 1
			ELSE 0
		END FLAG_IMPRODUTIVA,
		CASE
			WHEN ups.ostipo_id = 'NR'
			AND ups.tipo_conclusao IN ('CASA FECHADA', 'ENDERECO NAO LOCALIZADO', 'NÃO LOCALIZADO',
        'INTERRUPCAO INDIVIDUAL POR DEFEITO INTERNO', 'LIGACAO CORTADA', 'CONSUMIDOR CORTADO',
        'DISJUNTOR DESLIGADO', 'NORMAL', 'DEFEITO INTERNO', 'OUTROS ESPECIFICAR (IMPROCEDENTE)',
        'SERVICO PREVENTIVO NAO PROGRAMADO', 'ENCONTRADO NORMAL') THEN 1
			ELSE 0
		END FLAG_IMPROCEDENCIA,
		CASE
			WHEN INC.REINCIDENTE_90_DIAS = 'S' THEN 1
			ELSE 0
		END FLAG_REINCIDENCIA,
		ups.data_dados
	FROM
		{{ ref('serv_exec_detalhado2_2025_revisao') }} ups
	LEFT JOIN (
		SELECT
			*
			FROM SB_PERFORMANCE.EQTL_GO.RETRABALHO
		WHERE
			empresa = 'EQTL GO'
			AND os_oper_origem IS NOT NULL) ret ON
		ups.os_oper = reT.os_oper
		AND to_char(reT.oco_data_conclusao_origem, 'YYYYMM') = UPS.COMPETENCIA
	LEFT JOIN  {{ ref('det_reinc_trafo') }} INC ON
		UPS.OS = INC.OCO_NUMERO
		AND UPS.COMPETENCIA = TO_CHAR(INC.DATA_CONCLUSAO, 'YYYYMM')
		--LEFT JOIN CEEE.PRAZO_SAP_STC PP ON PP.NOTA = UPS.OS
	WHERE
		1 = 1
		AND ups.ch_ups NOT IN ('SERVICO INVALIDO')
		AND ups.empresa = 'EQTL_GO'
        
) T