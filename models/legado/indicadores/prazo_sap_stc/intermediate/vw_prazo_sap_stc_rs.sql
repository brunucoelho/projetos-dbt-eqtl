SELECT
'EQTL_RS 'EMPRESA,
cad.regional REGIONAL,
cad.distrital DISTRITAL,
CASE
    WHEN cad.municipio IN ('ACEGUA', 'AMARAL FERRADOR', 'ARAMBARE', 'ARROIO DO PADRE', 'BARAO DO TRIUNFO', 'BARRA DO RIBEIRO', 'CAPAO DO LEAO', 'CAPIVARI DO SUL', 'CARAA', 'CERRO GRANDE DO SUL', 'CHUI', 'CHUVISCA', 'CRISTAL', 'DOM PEDRO DE ALCANTARA', 'HULHA NEGRA', 'LAGOA DOS PATOS', 'LAGOA MIRIM', 'MAMPITUBA', 'MARIANA PIMENTEL', 'MINAS DO LEAO', 'MORRINHOS DO SUL', 'MORRO REDONDO', 'PEDRAS ALTAS', 'PEDRO OSORIO', 'SENTINELA DO SUL', 'SERTAO SANTANA', 'TRES CACHOEIRAS', 'TURUCU', 'XANGRI-LA') THEN 'PORTO ALEGRE'
    WHEN cad.municipio IN ('ITATI','MAQUINE', 'TRES FORQUILHAS') THEN 'TERRA DE AREIA'
    WHEN cad.municipio IN ('IMBE') THEN 'TRAMANDAI'
    ELSE cad.municipio -- Modificação aqui
END AS BASE,
cad.seccional SECCIONAL,
cad.municipio MUNICIPIO,
cad.localidade LOCALIDADE,
CASE
    WHEN cad.perimetro = 'U' then 'URBANO'
    WHEN cad.perimetro = 'R' then 'RURAL'
    END AS AREA_ESTRUT_REGIONAL,
A.COD_UN_CONS_COS CONTA_CONTRATO,
A.COD_UN_CONS_COS INSTALACAO,
A.NUM_SEQ_OPER_COS NOTA,
A.COD_SIMPL_OS_COS LOS,
A.NUM_SEQ_OPER_COS OS,
A.NUM_SEQ_GER_COS SEQ,
'' EQUIPE_EXECUCAO,
A.COD_TIPO_OS_COS TIPO_NOTA,
B.DES_TIPO_OS_TOS TEXTO_BREVE,
A.COD_SUB_TIPO_OS_COS GRUPO_CODES,
A.COD_SUB_TIPO_OS_COS CODIFICACAO,
' 'GRUPO_MEDIDA,
A.COD_TIPO_CONCL_COS CODIGO_MEDIDA,
'MEDE' STATUS_MEDIDA,
D.DES_TIPO_CONCL_OS_CNC DESCRICAO,
'NA' MOTIVO,
TO_CHAR(A.DTA_CONCL_SERV_COS,'YYYYMM') MES_COMPETENCIA,
A.DTA_INC_COS DATA_NOTA,
A.DTA_INC_COS DATA_CRIACAO,
A.DTA_INC_COS DATA_CRIACAO_MEDIDA,
A.DTA_PROGM_COS INICIO_DESEJADO,
A.DTA_LIM_COS FIM_DESEJADO,
A.DTA_PROGM_COS FIM_PROGRAMADO_MEDIDA,
A.DTA_CONCL_SERV_COS DATA_CONCLUSAO_MEDIDA,
/*CASE WHEN A.DTA_CONCL_SERV_COS >= A.DTA_LIM_COS THEN 'ATENDIDO NO PRAZO'
ELSE 'ATENDIDO FORA DO PRAZO'
END STATUS_PRAZO,*/
A.COD_SITU_COS STATUS_CCS,
' 'FINANCIAMENTO_PADRAO,
--WF.DES_AREA_WRKFLW_CAW AREA,
--A.NOM_USU_SITU_COS USUARIO,
--A.NOM_USU_PROGM_COS,
CURRENT_DATE() AS ATUALIZACAO

FROM EQTLINFO_PRD.EQTL_RS.CAD_ORD_SERV A
LEFT JOIN EQTLINFO_PRD.EQTL_RS.TAB_CADASTRO cad on A.COD_UN_CONS_COS = cad.instalacao-- or A.COD_UN_CONS_COS = cad.conta_contrato
--LEFT JOIN EQTLINFO_HML.EQTL_RS_EQZ.MEDIDAS_NOTAS_SERVICOS aa on aa.nota = substr( (A.num_seq_oper_cos::BIGINT)::VARCHAR, 4)
--LEFT JOIN EQTLINFO_PRD.EQTL_RS_EQZ.VISITAS_NOTAS vv ON (vv.NOTA = (A.num_seq_oper_cos::BIGINT)::VARCHAR) and (vv.data_final_servico = aa.data_conclusao_medida)
LEFT JOIN EQTLINFO_PRD.EQTL_RS.CAD_TIPO_OS B                ON B.COD_TIPO_OS_TOS = A.COD_TIPO_OS_COS
LEFT JOIN EQTLINFO_RAW.CS_RS.CAD_AREA_WF C               ON A.NUM_AREA_WRKFLW_COS = C.NUM_AREA_WRKFLW_CAW
LEFT JOIN EQTLINFO_PRD.EQTL_RS.TAB_TIPO_CONCL_ORD_SERV D    ON A.COD_TIPO_OS_COS = D.COD_TIPO_OS_CNC 
                                              AND A.COD_SUB_TIPO_OS_COS = D.COD_SUB_TIPO_OS_CNC 
                                              AND A.COD_TIPO_CONCL_COS = D.COD_TIPO_CONCL_OS_CNC
LEFT JOIN EQTLINFO_PRD.EQTL_RS.CAD_SUB_TIPO_OS E            ON A.COD_TIPO_OS_COS = E.COD_TIPO_OS_STO and A.COD_SUB_TIPO_OS_COS = E.COD_SUB_TIPO_OS_STO
LEFT JOIN EQTLINFO_RAW.CS_RS.TAB_LOCALIDADE L             ON L.COD_LOC_LOC = A.COD_LOC_COS
LEFT JOIN EQTLINFO_RAW.CS_RS.TAB_MUNICIPIO M              ON L.COD_MUN_LOC = M.COD_MUN_MUN
LEFT JOIN EQTLINFO_PRD.EQTL_RS.REL_FILTRO RLF               ON rlf.cod_loc_rlf = A.COD_LOC_COS
LEFT JOIN EQTLINFO_RAW.CS_RS.CAD_AREA_WF WF               ON A.NUM_AREA_WRKFLW_COS = WF.NUM_AREA_WRKFLW_CAW

WHERE A.COD_TIPO_OS_COS IN ('MT','DS','LN','MG','RL','MM','TR','TP','RI','DR','IS','FP','MQ')
AND
to_char(A.DTA_CONCL_SERV_COS, 'YYYYMM') = {{ get_month_ref() }}
AND
A.cod_tipo_os_cos||'-'||A.cod_sub_tipo_os_cos in ('DS-DUC','DS-DUT','GE-VIS','IS-EQP','LG-VIS',
                                                    'MS-025''MS-093','MS-032','RI-RLC','RI-RLU','RT-REQ','TR-VIS')

AND A.DTA_CONCL_SERV_COS >= TO_DATE('01/01/2023','DD/MM/YYYY')
AND DTA_INC_COS IS NOT NULL
AND A.NUM_AREA_WRKFLW_COS <> '9200000000'