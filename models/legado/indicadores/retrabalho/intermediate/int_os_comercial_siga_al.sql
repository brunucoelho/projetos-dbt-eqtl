SELECT
'EQTL AL' AS EMPRESA,
CURRENT_TIMESTAMP   AS   data_carga,
COALESCE(
  TRY_TO_TIMESTAMP_NTZ(DATA_HORA_ACAO_CONCLUSAO, 'YYYY-MM-DD HH24:MI:SS'),
  TRY_TO_TIMESTAMP_NTZ(DATA_HORA_ACAO_CONCLUSAO, 'YYYY-MM-DD HH24:MI'),
  TRY_TO_TIMESTAMP_NTZ(DATA_HORA_ACAO_CONCLUSAO, 'YYYY-MM-DD'),               -- só data → 00:00:00
  TRY_TO_TIMESTAMP_NTZ(REPLACE(REPLACE(DATA_HORA_ACAO_CONCLUSAO,'T',' '),'Z','')) -- ISO 8601
) AS OCO_DATA_CONCLUSAO,
CAST(TOA.NB_LATITUDE AS VARCHAR) AS LATITUDE,
CAST(TOA.NB_LONGITUDE AS VARCHAR) AS LONGITUDE,
'' AS BAIRRO,
'COMERCIAL' AS abrangencia,
TOA.NB_ID os_oper,
TOA.TX_OS OS,
TOA.TX_EQ_TIPONOTA OSTIPO,
TOA.TX_EQ_GRUPOCODE OSSUBTIPO,
TOA.TX_CITY MUNICIPIO,
TOA.TX_WORKZONE LOCALIDADE,
TRUNC(BR.instalacao,0) uc,
REGEXP_REPLACE(TOA.TX_RESOURCEEXTERNALID, '^AL_', '') AS PREFIXO,
TOA.TX_EQ_OBSERVACAO REGISTRO_EXEC,
'SEM COMPONENTE DANIFICADO' AS componete_danificado,
TRY_TO_NUMBER(TOA.TX_BOX_EQ_TIPODEDEFEITO) AS tb_defeito_id,
CAST(NULL AS NUMBER) AS COD_CONCLUSAO,
cm.DESCRICAO TIPO_CONCLUSAO,
CAST(NULL AS NUMBER) AS COD_SUBCAUSA,
TOA.TX_TEXT_EQ_SUBTIPOCAUSATEXT subcausa,
CASE 
	WHEN TX_EQ_TIPONOTA IN ('TR', 'LN', 'MQ', 'IS', 'RI', 'MM', 'LG', 'DR', 'RL', 'M1')AND TOA.TX_EQ_TIPOCONCLUSAO IN ('EX')  THEN 'P'
    WHEN TX_EQ_TIPONOTA IN ('MT') AND cm.DESCRICAO in ('EXECUTADO') THEN 'P'
ELSE 'I'
END TIPO,
'' AS REGIONAL,
CASE 
    WHEN TOA.TX_EQ_DATAABERTURA IS NOT NULL AND REGEXP_LIKE(TOA.TX_EQ_DATAABERTURA, '^\d{2}/\d{2}/\d{4} \d{2}:\d{2}:\d{2}$') 
    THEN TO_TIMESTAMP(TOA.TX_EQ_DATAABERTURA, 'DD/MM/YYYY HH24:MI:SS')
    ELSE NULL
END AS DATA_ORIGEM
FROM
	EQTLINFO_RAW.SIGA.TB_OFSC_ACTIVITIES_RT toa
	INNER JOIN EQTLINFO_RAW.SIGA.tb_ofsc_resources_rt res on toa.nb_resourceid = res.nb_id
	LEFT JOIN EQTLINFO_PRD.EQTL_AL.TAB_CADASTRO BR ON lpad(BR.CONTA_CONTRATO,12,'0') = lpad(TOA.TX_EQ_CONTACONTRATO,12,'0')
LEFT JOIN EQTLINFO_RAW.OPER_AL.CODE_MEDIDA cm ON
	cm.CODE_MEDIDA_ID = SUBSTR(nvl(toa.TX_EQ_TIPOCONCLUSAOEXEC, toa.TX_EQ_TIPOCONCLUSAONAOEXEC), -4)
	AND cm.GRUPO_CODE_MEDIDA_ID = toa.TX_EQ_GRUPOCODEMEDIDA
	AND cm.OSSUBTIPO_ID = toa.TX_EQ_GRUPOCODE
WHERE --TRUNC(TOA.DATA_HORA_ACAO_CONCLUSÃO,'MM') BETWEEN  add_months(trunc(sysdate,'MM'),-3) AND trunc(sysdate,'MM')
	TRUNC(toa.dt_activity, 'MM') >= TO_DATE('2024-09-01', 'YYYY-MM-DD')
	AND TOA.TX_EQ_TIPONOTA IN ('MT', 'TR', 'LN', 'MQ', 'IS', 'RI', 'MM', 'LG', 'DR', 'RL', 'M1')
	AND toa.TX_EQ_GRUPOCODE IN ('PENDCONX', 'SURAMA', 'MEDIAVAR', 'SUBSMED', 'SUBSRAM', 'EXSVGDIS', 'ALMODTAR',
'REATIVAC', 'VIS', 'LDS', 'MDQUALAM', 'RELIGABD', 'LIGNOVBT', 'LIGEATBT', 'RELIGAAT', 'INSPTECN', 'LIGREATI', 'DESLORAM', 'VDS', 'RLN',
'RLJ', 'RELIGABT', 'MUDMEDLC', 'LIGPROVI', 'LIGANOVA', 'LGJ', 'RELIGASI', 'LUC', 'VLJ', 'TROCPABT', 'RLA', 'TROCPAOT', 'RLA', 'RLA', 'RCI')
--and cm.DESCRICAO not in ('NÃO EXECUTADO','NAO EXECUTADO')
	AND TOA.TX_STATUS IN ('completed','notdone','complete')
	AND res.TX_TYPE not IN ('Balde','Manutenção: Balde','Grupo')
	AND SUBSTR(TOA.TX_RESOURCEEXTERNALID,0,2) = 'AL'