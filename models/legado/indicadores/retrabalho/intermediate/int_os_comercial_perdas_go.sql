SELECT
    CAST('EQTL GO' AS VARCHAR(7)) AS EMPRESA,
    CAST(CURRENT_TIMESTAMP() AS TIMESTAMP_NTZ(9)) AS DATA_CARGA,
    CAST(DT_CONCLUSAO AS TIMESTAMP_NTZ(0)) AS OCO_DATA_CONCLUSAO,
    CAST(NULL AS VARCHAR(50)) AS BAIRRO,
    CAST('COMERCIAL' AS VARCHAR(13)) AS ABRANGENCIA,
    CAST(NR_OS AS FLOAT) AS OS_OPER,
    CAST(NR_OS || '-' || SUBSTR(DT_SOLICITACAO, 0, 7) AS VARCHAR(16777216)) AS OS,
    CAST('FS' AS VARCHAR(2)) AS OSTIPO,
    CAST(CODIGO_IRREGULARIDADE_CAMPO AS VARCHAR(10)) AS OSSUBTIPO,
    CAST(NULL AS VARCHAR(50)) AS MUNICIPIO,
    CAST(NULL AS VARCHAR(50)) AS LOCALIDADE,
    CAST(NR_UC AS FLOAT) AS UC,
    CAST(PRX_DESCRICAO AS VARCHAR(15)) AS PREFIXO,
    SUBSTR(OBSERVACAO_NORM,250)  AS REGISTRO_EXEC,
    CAST(NULL AS VARCHAR(50)) AS COMPONENTE_DANIFICADO,
    CAST(NULL AS FLOAT) AS TB_DEFEITO_ID,
    CAST(NULL AS FLOAT) AS COD_CONCLUSAO,
    CAST(NULL AS VARCHAR(50)) AS TIPO_CONCLUSAO,
    CAST(NULL AS FLOAT) AS COD_SUBCAUSA,
    CAST(NULL AS VARCHAR(1)) AS SUBCAUSA,
    CAST('P' AS VARCHAR(1)) AS TIPO,
    CAST(NULL AS VARCHAR(50)) AS REGIONAL,
    CAST(NULL AS TIMESTAMP_NTZ(0)) AS DATA_ORIGEM
FROM EQTLINFO_PRD.EQTL_GO.ODS_GSPERDAS og
WHERE CD_TIPO_IRREG_ENCONTRADA IS NOT NULL
