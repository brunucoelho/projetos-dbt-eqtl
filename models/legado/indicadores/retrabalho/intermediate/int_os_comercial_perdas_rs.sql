SELECT
    CAST('EQTL RS' AS VARCHAR(7)) AS EMPRESA,
    CAST(CURRENT_TIMESTAMP AS TIMESTAMP_NTZ(9)) AS DATA_CARGA,
    CAST(DATA_BAIXA AS TIMESTAMP_NTZ(0)) AS OCO_DATA_CONCLUSAO,
    CAST(NULL AS FLOAT) AS LATITUDE,
    CAST(NULL AS FLOAT) AS LONGITUDE,
    CAST(NULL AS VARCHAR(50)) AS BAIRRO,
    CAST('OS_COMERCIAL' AS VARCHAR(12)) AS ABRANGENCIA,
    CAST(NULL AS FLOAT) AS OS_OPER,
    CAST(TO_CHAR(DATA_NOTA, 'MM') || '/' || TO_CHAR(DATA_NOTA, 'YYYY') || '-' || NOTA AS VARCHAR(16777216)) AS OS,
    CAST(TIPO_NOTA AS VARCHAR(2)) AS OSTIPO,
    CAST(IRREGULARIDADE AS VARCHAR(8)) AS OSSUBTIPO,
    CAST(MUNICIPIO AS VARCHAR(50)) AS MUNICIPIO,
    CAST(LOCALIDADE AS VARCHAR(50)) AS LOCALIDADE,
    CAST(INSTALACAO AS FLOAT) AS UC,
    CAST(EXECUTOR AS VARCHAR(15)) AS PREFIXO,
    CAST(NULL AS VARCHAR(500)) AS REGISTRO_EXEC,
    CAST(NULL AS VARCHAR(25)) AS COMPONENTE_DANIFICADO,
    CAST(NULL AS FLOAT) AS TB_DEFEITO_ID,
    CAST(NULL AS FLOAT) AS COD_CONCLUSAO,
    CAST(NULL AS VARCHAR(100)) AS TIPO_CONCLUSAO,
    CAST(NULL AS FLOAT) AS COD_SUBCAUSA,
    CAST(NULL AS VARCHAR(9)) AS SUBCAUSA,
    CAST('P' AS VARCHAR(1)) AS TIPO,
    CAST(REGIONAL AS VARCHAR(50)) AS REGIONAL,
    CAST('GIFC_V_PLANO_BASE' AS VARCHAR(50)) AS BASE,
    CAST(NULL AS TIMESTAMP_NTZ(0)) AS DATA_ORIGEM
FROM
    EQTLINFO_PRD.GESTINFO_CORP.GIFC_V_PLANO_BASE OS
WHERE
    DATA_BAIXA >= TO_DATE('01/01/2025 00:00:00', 'DD/MM/YYYY HH24:MI:SS')
    AND NORMALIZACAO = 1
    AND UF = 'RS'


