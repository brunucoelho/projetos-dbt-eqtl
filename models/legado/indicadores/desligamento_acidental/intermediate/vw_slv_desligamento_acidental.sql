WITH date_range AS (
    SELECT TO_DATE('2024-08-01', 'YYYY-MM-DD') AS start_date,
           DATE_TRUNC('MONTH', CURRENT_DATE) AS end_date
),
months AS (
    SELECT start_date AS month_start
    FROM date_range
    UNION ALL
    SELECT ADD_MONTHS(month_start, 1)
    FROM months, date_range
    WHERE ADD_MONTHS(month_start, 1) <= end_date
),
desligamentos AS (
    SELECT 
        CURRENT_TIMESTAMP AS DATA_ETL,
        ge.SIGLA_EMPRESA AS EMPRESA,
        'VAR_1_DESLIGAMENTOS' AS FORMULA,
        NVL(sec.CONTRATO,'AUSENTE') AS CONTRATO,
        sc.FORNECEDOR_SAP AS FORNECEDOR,
        des.ANO,
        des.MES,
        COUNT(DISTINCT OCORRENCIA_ID) AS VAR_1_DESLIGAMENTOS,
        COUNT(DISTINCT OCORRENCIA_ID) AS VALOR,
        'UN' AS UNIDADE,
        sc.COD_TIPO_CONTRATO
    FROM SB_PERFORMANCE.EQTL_CORP.desligamento_acidental des
    LEFT JOIN {{ ref('slv_equipe_contrato') }} sec 
        ON sec.COD_EMPRESA = des.COD_EMPRESA 
       AND sec.ANO = des.ANO 
       AND sec.MES = des.MES 
       AND sec.PREFIXO = des.EQUIPE
    LEFT JOIN SB_PERFORMANCE.SILVER.GLOSSARIO_EMPRESAS ge 
        ON ge.COD_EMPRESA = des.COD_EMPRESA
    LEFT JOIN SB_PERFORMANCE.EQTL_CORP.SLV_CONTRATOS sc 
        ON sc.CONTRATO_SAP = sec.CONTRATO 
       AND sc.COD_EMPRESA = sec.COD_EMPRESA 
    WHERE des.DATA >= DATE'2024-08-01'
    GROUP BY
        ge.SIGLA_EMPRESA,
        NVL(sec.CONTRATO,'AUSENTE'),
        sc.FORNECEDOR_SAP,
        des.ANO,
        des.MES,
        sc.COD_TIPO_CONTRATO
)
SELECT 
    CURRENT_TIMESTAMP AS DATA_ETL,
    ge.SIGLA_EMPRESA AS EMPRESA,
    sc.CONTRATO_SAP AS CONTRATO,
    sc.FORNECEDOR_SAP AS FORNECEDOR,
    EXTRACT(YEAR FROM m.month_start) AS ANO,
    EXTRACT(MONTH FROM m.month_start) AS MES,
    'VAR_1_DESLIGAMENTOS' AS FORMULA,
    NVL(d.VAR_1_DESLIGAMENTOS,0) AS VAR_1_DESLIGAMENTOS,
    NVL(d.VALOR,0) AS VALOR,
    'UN' AS UNIDADE
FROM SB_PERFORMANCE.EQTL_CORP.SLV_CONTRATOS sc
LEFT JOIN SB_PERFORMANCE.SILVER.GLOSSARIO_EMPRESAS ge 
    ON ge.COD_EMPRESA = sc.COD_EMPRESA
CROSS JOIN months m
LEFT JOIN desligamentos d 
    ON d.ano = EXTRACT(YEAR FROM m.month_start) 
   AND d.mes = EXTRACT(MONTH FROM m.month_start) 
   AND d.EMPRESA = ge.SIGLA_EMPRESA
   AND d.CONTRATO = sc.CONTRATO_SAP
WHERE sc.COD_TIPO_CONTRATO IN (1,2,4)

UNION ALL 

SELECT 
    DATA_ETL,
    EMPRESA,
    CONTRATO,
    FORNECEDOR,
    ANO,
    MES,
    FORMULA,
    VAR_1_DESLIGAMENTOS,
    VALOR,
    UNIDADE
FROM desligamentos
WHERE COD_TIPO_CONTRATO = 0