SELECT 
CURRENT_TIMESTAMP DH_ETL, ge.SIGLA_EMPRESA, ge.EMPRESA,  gr.REGIONAL, gp.POLO, CONTA_CONTRATO, NOTA, 'RELIGA' TIPO, DH_ABERTURA, DH_FINALIZADO, DH_EXECUCAO_CAMPO, EQUIPE_CONCLUSAO, sec.CONTRATO, sc.FORNECEDOR_SAP
FROM {{ ref('nota_religa') }} nr
LEFT JOIN SB_PERFORMANCE.SILVER.GLOSSARIO_EMPRESAS ge ON ge.COD_EMPRESA = nr.COD_EMPRESA 
LEFT JOIN SB_PERFORMANCE.SILVER.GLOSSARIO_POLOS gp ON gp.COD_POLO = nr.COD_POLO 
LEFT JOIN SB_PERFORMANCE.SILVER.GLOSSARIO_REGIONAIS gr ON gr.COD_REGIONAL = gp.COD_REGIONAL
LEFT JOIN {{ ref('slv_equipe_contrato') }} sec 
	ON sec.ANO = EXTRACT (YEAR FROM DH_EXECUCAO_CAMPO) 
	AND sec.MES = EXTRACT (MONTH FROM DH_EXECUCAO_CAMPO) 
	AND sec.COD_EMPRESA = nr.COD_EMPRESA 
	AND sec.PREFIXO = nr.EQUIPE_CONCLUSAO
LEFT JOIN SB_PERFORMANCE.EQTL_CORP.SLV_CONTRATOS sc ON sc.COD_EMPRESA = sec.COD_EMPRESA AND sc.CONTRATO_SAP = sec.CONTRATO
WHERE STATUS = 'FINALIZADO'
AND PRODUTIVA = 'S'
AND TO_VARCHAR(DH_FINALIZADO, 'YYYYMM') = {{ get_month_ref() }}

UNION ALL

SELECT 
CURRENT_TIMESTAMP DH_ETL, ge.SIGLA_EMPRESA, ge.EMPRESA,  gr.REGIONAL, gp.POLO, CONTA_CONTRATO, NOTA, 'CORTE' TIPO, DH_ABERTURA, DH_FINALIZADO, DH_EXECUCAO_CAMPO, EQUIPE_CONCLUSAO, sec.CONTRATO, sc.FORNECEDOR_SAP
FROM {{ ref('nota_corte') }} nr
LEFT JOIN SB_PERFORMANCE.SILVER.GLOSSARIO_EMPRESAS ge ON ge.COD_EMPRESA = nr.COD_EMPRESA 
LEFT JOIN SB_PERFORMANCE.SILVER.GLOSSARIO_POLOS gp ON gp.COD_POLO = nr.COD_POLO 
LEFT JOIN SB_PERFORMANCE.SILVER.GLOSSARIO_REGIONAIS gr ON gr.COD_REGIONAL = gp.COD_REGIONAL
LEFT JOIN {{ ref('slv_equipe_contrato') }} sec 
	ON sec.ANO = EXTRACT (YEAR FROM DH_EXECUCAO_CAMPO) 
	AND sec.MES = EXTRACT (MONTH FROM DH_EXECUCAO_CAMPO) 
	AND sec.COD_EMPRESA = nr.COD_EMPRESA 
	AND sec.PREFIXO = nr.EQUIPE_CONCLUSAO
LEFT JOIN SB_PERFORMANCE.EQTL_CORP.SLV_CONTRATOS sc ON sc.COD_EMPRESA = sec.COD_EMPRESA AND sc.CONTRATO_SAP = sec.CONTRATO
WHERE STATUS = 'FINALIZADO'
AND PRODUTIVA = 'S'
AND TO_VARCHAR(DH_FINALIZADO, 'YYYYMM') = {{ get_month_ref() }}