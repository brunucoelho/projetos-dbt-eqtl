SELECT 
 CURRENT_TIMESTAMP AS DH_ETL,
 6 COD_EMPRESA,
 TO_NUMBER(d.GV3) COD_POLO, -- CORRIGIR
 LTRIM(moc.CONTA_CONTRATO::VARCHAR,'0') CONTA_CONTRATO,
 LTRIM(MOC.NR_OS::VARCHAR,'0') NOTA,
 moc.DT_SOLICITACAO DH_ABERTURA,
 COC.DT_CONCLUSAO DH_FINALIZADO,
-- RSR.DT_LIMITE DH_LIMITE,
 TOC.HOST_VI_DT_FIM_SERVICO DH_EXECUCAO_CAMPO,
 NVL(PT.PRX_NOME_INTERNO, PT.PRX_DESCRICAO) EQUIPE_CONCLUSAO,
 CASE
  WHEN moc.CD_STATUS_OS IN (1,2,3,4,5) THEN 'ANDAMENTO'
  WHEN cdm.TIPO_CONCLUSAO IN ('N','R') THEN 'FINALIZADO'
  ELSE 'CANCELADO'
 END STATUS,
 DECODE(CDM.TIPO_CONCLUSAO,'N','S','R','N','I','N') PRODUTIVA,
 moc.VLR_TOTAL_FATURAS_UC VALOR_DIVIDA,
 CASE 
	 WHEN REGEXP_LIKE( ADR.COMPLEMENTO_ENDERECO, '.*APART.*|.*APT.*|.*APTO.*|.*AP .*|.*AP-.*|.*CASA.*|.*COND.*|.*CND.*|.*ED .*|.*BLOCO.*|.*BLC.*|.*LOJA.*|.*LJ.*|.*SALA.*|.*SL.*|.*AGRU.*|.*EDIF.*') THEN 'S'
	 WHEN ADR.TIPO IN ('APARTAMENTO','CONDOMÍNIO','CONDOMINIO','SALA') THEN 'S' 
	 WHEN UPPER(coc.DS_REGISTRO_ATENDIMENTO||'-'||moc.DS_OBS_ATENDENTE) LIKE '%AGRUPA%' THEN 'S'
	 ELSE 'N' 
END UC_AGRUPADA,
 CASE
  WHEN sdo.DESCRICAO_STPOS LIKE '%MEDIDOR%' THEN 'MEDIDOR'
  WHEN sdo.DESCRICAO_STPOS LIKE '%DISJUNTOR%' THEN 'DISJUNTOR'
  WHEN sdo.DESCRICAO_STPOS LIKE '%POSTE%' THEN 'POSTE'
  WHEN sdo.DESCRICAO_STPOS LIKE '%GRUPO A%' THEN 'GRUPO_A'
  WHEN sdo.DESCRICAO_STPOS LIKE '%RAMAL%' THEN 'RAMAL'
  WHEN sdo.DESCRICAO_STPOS IS NULL THEN NULL
  ELSE 'DESCONHECIDO'
 END LOCAL_CORTE_PREVISTO,
 CASE
  WHEN coc.cd_local_religacao IS NULL THEN NULL
  WHEN coc.cd_local_religacao IN ('1','01') THEN 'MEDIDOR'
  WHEN coc.cd_local_religacao IN ('2','02') THEN 'MUFLA'
  WHEN coc.cd_local_religacao IN ('3','03') THEN 'POSTE'
  WHEN coc.cd_local_religacao IN ('4','04') THEN 'DISJUNTOR'
  WHEN coc.cd_local_religacao IN ('5','05') THEN 'PODAO'
  WHEN coc.cd_local_religacao IN ('6','06') THEN 'RAMAL'
  ELSE 'DESCONHECIDO'
 END LOCAL_CORTE_EXECUTADO,
 SDO.DESCRICAO_STPOS
 FROM EQTLINFO_RAW.OPER_GO.MOVTO_OS_COMERCIAL moc 
 LEFT JOIN EQTLINFO_RAW.OPER_GO.SUBTIPO_DE_OS sdo ON sdo.ossubtipo_id = moc.ossubtipo_id
 LEFT JOIN EQTLINFO_RAW.OPER_GO.CONCLUI_OS_COMERCIAL coc ON coc.CD_MOVTO_OS_COMERCIAL = moc.CD_MOVTO_OS_COMERCIAL
 LEFT JOIN EQTLINFO_RAW.OPER_GO.TURMA_OS_COMERCIAL toc ON toc.HIST_OS_TURMA_ID = coc.HIST_OS_TURMA_ID
 LEFT JOIN EQTLINFO_RAW.OPER_GO.HISTORICO_TURMA_PLANTAO htp ON htp.HIST_TURMA_PLANTAO_ID = toc.TURMA_PLANTAO_ID
 LEFT JOIN EQTLINFO_RAW.OPER_GO.PREFIXO_TURMA pt ON pt.PREFIXO_TURMA_ID = htp.PREFIXO_TURMA_ID
 LEFT JOIN EQTLINFO_RAW.OPER_GO.BASE b ON b.BASE_ID = moc.BASE_ID
 LEFT JOIN EQTLINFO_RAW.OPER_GO.PONTO_APOIO pa ON pa.CODIGO_PA = b.CODIGO_PA
 LEFT JOIN EQTLINFO_PRD.EQTL_GO.TAB_CADASTRO CAD ON TO_NUMBER(CAD.INSTALACAO) = moc.NR_UC
 LEFT JOIN SB_PERFORMANCE.EQTL_CORP.dicionario d ON d.DICIONARIO = 'OPER_PONTO_APOIO_CODIGO_PA' AND d.SV2 = pa.CODIGO_PA
 -- INNER JOIN EQTLINFO_RAW.CBILL_GO.RMA_SERVICE_REQUEST rsr ON rsr.id = moc.NR_OS
 -- INNER JOIN EQTLINFO_RAW.CBILL_GO.RMA_SREQUEST_STATUS rss ON rss.ID = rsr.FK_SRSTS_ID
 LEFT JOIN EQTLINFO_RAW.OPER_GO.CODE_MEDIDA CDM ON moc.GRUPO_CODE_MEDIDA_ID = CDM.GRUPO_CODE_MEDIDA_ID
  AND CDM.OSSUBTIPO_ID = moc.OSSUBTIPO_ID  
  AND CDM.CODE_MEDIDA_ID = COC.CD_TP_CONCLUSAO_OS  
 LEFT JOIN SB_PERFORMANCE.EQTL_CORP.CELG_GRUPO_CODE_MEDIDA GCM ON GCM.GRUPO_CODE_MEDIDA_ID = moc.GRUPO_CODE_MEDIDA_ID 
  AND GCM.OSSUBTIPO_ID = moc.OSSUBTIPO_ID
 LEFT JOIN (
	SELECT
		CNTUC.ID UC_ID,
		CNTUC.IDENTIFIER UC,
		TP.TIPO,
	    LISTAGG(
	        COALESCE(ADCTP.DESCRIPTION, '') || ' - ' || 
	        COALESCE(ADCTP.CODE, '') || ' - ' || 
	        COALESCE(ADCMP.VALUE, '') || ' - ' || 
	        COALESCE(ADR.COMPLEMENTS, '') || ' - ' || 
	        COALESCE(ADR.ADDRESS_NUMBER, ''),
	        ' | '
	    ) WITHIN GROUP (ORDER BY ADCTP.DESCRIPTION) AS COMPLEMENTO_ENDERECO
	FROM
		EQTLINFO_RAW.CBILL_GO.CNT_CONSUMPTION_UNIT CNTUC
	LEFT JOIN 
	    EQTLINFO_RAW.CBILL_GO.CNT_UC_ADDRESS CNTAD ON
		CNTAD.FK_CNTUC_ID = CNTUC.ID
	LEFT JOIN 
	    EQTLINFO_RAW.CBILL_GO.ADR_ADDRESS ADR ON
		ADR.ID = CNTAD.FK_ADDRS_ID_ADDRESS
	LEFT JOIN
	    SB_PERFORMANCE.BRONZE.ADR_COMPLEMENT ADCMP ON
		ADCMP.FK_ADADD_ID = CNTAD.FK_ADDRS_ID_ADDRESS
	LEFT JOIN 
	    SB_PERFORMANCE.BRONZE.ADR_COMPLEMENT_TYPE ADCTP ON
		ADCTP.ID = ADCMP.FK_ADCPT_ID
	LEFT JOIN (
		SELECT
			TRVIUC.FK_CNTUC_ID,
			TRV.DESCRIPTION TIPO
		FROM
			EQTLINFO_RAW.CBILL_GO.CNT_REF_VALUE_IN_UC TRVIUC
		INNER JOIN EQTLINFO_RAW.CBILL_GO.CNT_REFERENCE_VALUE TRV ON
			TRV.ID = TRVIUC.FK_CNTRV_ID
		WHERE
			TRVIUC.PARAM_TYPE = 43
	) TP ON TP.FK_CNTUC_ID = CNTUC.ID
	GROUP BY 
		CNTUC.ID,
		CNTUC.IDENTIFIER,
		TP.TIPO
 ) ADR ON ADR.UC = moc.CONTA_CONTRATO
 WHERE TO_VARCHAR(trunc(moc.DT_SOLICITACAO,'MM'),'YYYYMM') = {{ get_month_ref() }}
 AND moc.ostipo_id = 'CT' -- NOTA DE CORTE