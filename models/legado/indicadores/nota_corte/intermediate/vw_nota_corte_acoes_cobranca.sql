SELECT 
CURRENT_TIMESTAMP DH_ETL,
1 COD_EMPRESA,
NULL COD_POLO,
CONTA_CONTRATO,
NOTA_SERVICO NOTA,
TRY_TO_DATE(DATA_GERACAO,'YYYYMMDD')::TIMESTAMP DH_ABERTURA,
TRY_TO_DATE(DATA_FIM,'YYYYMMDD')::TIMESTAMP DH_FINALIZADO,
TRY_TO_DATE(DATA_EXECUCAO,'YYYYMMDD')::TIMESTAMP DH_EXECUCAO_CAMPO,
COD_VIATURA EQUIPE_CONCLUSAO,
DECODE(STATUS_EXECUCAO, 'X','FINALIZADO','ANDAMENTO') STATUS,
NULL PRODUTIVA,
VALOR_DEBITO_TOTAL VALOR_DIVIDA,
NULL UC_AGRUPADA,
NULL LOCAL_CORTE_PREVISTO,
NULL LOCAL_CORTE_EXECUTADO,
NULL DESCRICAO_STPOS
FROM (
SELECT DISTINCT *
FROM EQTLINFO_PRD.EQTL_AL.ACOES_COBRANCA_NOTAS
WHERE TO_VARCHAR(TRY_TO_DATE(DATA_OCORRENCIA,'YYYYMMDD'),'YYYYMM') = {{ get_month_ref() }}
AND ETAPA_COBRANCA = 'CORT'
AND (STATUS_EXECUCAO IS NULL OR STATUS_EXECUCAO = 'X')
)

