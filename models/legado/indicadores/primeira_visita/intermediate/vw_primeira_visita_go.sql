SELECT b.*, 
CASE 
	WHEN ORDEM_DE_VISITA = 1 AND VISITAS_NA_OS > 1 THEN 1
	ELSE 0
END DEMANDOU_APOIO
FROM(
SELECT 
a.*,
MAX(ORDEM_DE_VISITA) OVER (PARTITION BY OS) VISITAS_NA_OS
FROM(
	SELECT 
	CURRENT_TIMESTAMP DATA_ETL,
	'EQTL_GO' EMPRESA,
	(TO_VARCHAR(O.OCO_NUMERO::INT) || '-' || TO_VARCHAR(O.OCO_MES::INT) || '-' || TO_VARCHAR(O.OCO_ANO::INT)) OS,
	HOT_VI_DATA_CONCLUSAO DATA, 
	'NR' TIPO_NOTA,
	NVL(pt.PRX_NOME_INTERNO, pt.PRX_DESCRICAO) PREFIXO,
	hot.HOT_VI_DATA_CHEGADA DT_CHEGADA,
	ROW_NUMBER() OVER (PARTITION BY o.OCORRENCIA_ID ORDER BY hot.HOT_VI_DATA_CHEGADA ASC) ORDEM_DE_VISITA
	FROM EQTLINFO_RAW.OPER_GO.HISTORICO_OCORRENCIA_TURMA hot 
	LEFT JOIN EQTLINFO_RAW.OPER_GO.OCORRENCIA o ON o.OCORRENCIA_ID = hot.OCORRENCIA_ID 
	LEFT JOIN EQTLINFO_RAW.OPER_GO.HISTORICO_TURMA_PLANTAO htp ON htp.HIST_TURMA_PLANTAO_ID = hot.HIST_TURMA_PLANTAO_ID 
	LEFT JOIN EQTLINFO_RAW.OPER_GO.PREFIXO_TURMA pt ON pt.PREFIXO_TURMA_ID = htp.PREFIXO_TURMA_ID 
	WHERE TO_CHAR(o.OCO_DATA_NR,'YYYYMM') = {{ get_month_ref() }}
	AND hot.HOT_VI_STATUS = 4
	AND o.OCO_STATUS = 'F'
	) A
) B