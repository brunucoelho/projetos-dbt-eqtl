SELECT b.*, 
CASE 
	WHEN ORDEM_DE_VISITA = 1 AND VISITAS_NA_OS > 1 THEN 1
	ELSE 0
END DEMANDOU_APOIO
FROM(
SELECT 
a.*,
MAX(ORDEM_DE_VISITA) OVER (PARTITION BY OS) VISITAS_NA_OS
FROM (
	SELECT 
	CURRENT_TIMESTAMP DATA_ETL,
	DECODE(TX_STATE,'AL','EQTL_AL','AP','EQTL_AP','MA','EQTL_MA','PA','EQTL_PA','PI','EQTL_PI','GO','EQTL_GO','RS','EQTL_RS') EMPRESA,
	TX_OS OS, 
	DT_ACTIVITY DATA, 
	TX_EQ_TIPONOTA TIPO_NOTA, 
	SUBSTR(TX_RESOURCEEXTERNALID,4) PREFIXO, 
	DT_ESTIMATEDTIMEARRIVAL DT_CHEGADA,
	ROW_NUMBER() OVER (PARTITION BY TX_STATE, TX_OS ORDER BY DT_ESTIMATEDTIMEARRIVAL ASC) ORDEM_DE_VISITA
	FROM EQTLINFO_RAW.SIGA.TB_OFSC_ACTIVITIES_RT toar 
	WHERE TO_CHAR(DT_ACTIVITY,'YYYYMM') = {{ get_month_ref() }}
	AND TX_OS IS NOT NULL
	AND TX_EQ_TIPONOTA = 'NR'
	AND tx_status = 'completed'
	AND TX_STATE IN ('AL','AP','MA','PA','PI','RS','GO')
) a
) b