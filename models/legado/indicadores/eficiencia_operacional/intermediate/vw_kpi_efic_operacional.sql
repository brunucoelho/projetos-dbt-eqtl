TT.REGIONAL_PRX REGIONAL,
TT.POLO_PRX POLO,
TT.FONECEDOR FORNECEDOR,
''CONTRATO,
TT.COMPETENCIA,
SUM(TT.QTD_UPS) TOTAL_UPS,
'D-R-P-F' NIVEL,
('EFICIENCIA OPERACIONAL'||'-'||TT.EMPRESA||'-'||TT.REGIONAL_PRX||'-'||TT.POLO_PRX||'-'||TT.FONECEDOR)DESCRICAO,
'0,88' META,
'MAIOR MELHOR' POLARIDADE

FROM {{ ref('vw_efic_op_resumo') }} TT            
WHERE TT.REGIONAL_PRX IS NOT NULL 
AND TT.PROCESSO IN ('ATENDIMENTO EMERGENCIAL','CORTE LEVE','CORTE MOTO','LN 4X4','LN LEVE','SEED MONEY')
GROUP BY 
TT.EMPRESA,
TT.REGIONAL_PRX,
TT.POLO_PRX,
TT.FONECEDOR,
TT.COMPETENCIA)  T 

LEFT JOIN (SELECT DISTINCT
           'EFICIENCIA OPERACIONAL' INDICADOR,
            TT.EMPRESA DISTRIBUIDORA,
            TT.REGIONAL_PRX REGIONAL,
            TT.POLO_PRX POLO,
            TT.FONECEDOR FORNECEDOR,
            ''CONTRATO,
            TT.COMPETENCIA,
            SUM(TT.QTD_UPS) TOTAL_EFIC,
            'D-R-P-F' NIVEL,
            ('EFICIENCIA OPERACIONAL'||'-'||TT.EMPRESA||'-'||TT.REGIONAL_PRX||'-'||TT.POLO_PRX||'-'||TT.FONECEDOR)DESCRICAO,
            '0,88' META,
            'MAIOR MELHOR' POLARIDADE
            
             FROM  {{ ref('vw_efic_op_resumo') }} TT
             
            WHERE TT.REGIONAL_PRX IS NOT NULL 
            AND TT.PROCESSO IN ('ATENDIMENTO EMERGENCIAL','CORTE LEVE','CORTE MOTO','LN 4X4','LN LEVE','SEED MONEY')
            AND TT.FLAG_TOTAL = 'EFIC.'
            GROUP BY 
            TT.EMPRESA,
            TT.REGIONAL_PRX,
            TT.POLO_PRX,
            TT.FONECEDOR,
            TT.COMPETENCIA
            ) EF ON  EF.DISTRIBUIDORA = T.DISTRIBUIDORA 
                 AND EF.REGIONAL = T.REGIONAL 
                 AND EF.POLO = T.POLO
                 AND EF.FORNECEDOR = T.FORNECEDOR
                 AND EF.COMPETENCIA = T.COMPETENCIA


UNION ALL

