SELECT 
U.*, 
C.COD_EMPRESA,
C.PREFIXO,
C.ANO,
C.MES,
C.ORIGEM,
C.VIATURA,
C.CONTRATO,
C.FORNECEDOR_SAP,
E.EXPURGAR
FROM {{ ref('ups_mensal') }} U
LEFT JOIN SB_PERFORMANCE.EQTL_CORP.GSTC_EXPURGO E ON E.COMPETENCIA = U.COMPETENCIA AND U.EQUIPE = E.EQUIPE AND U.EMPRESA = E.EMPRESA
LEFT JOIN (
	SELECT 
	    G.SIGLA_EMPRESA,
	    G.COD_EMPRESA,
	      CASE 
	        WHEN A.COD_EMPRESA = '6' AND (B.PRX_DESCRICAO IS NULL OR TRIM(B.PRX_DESCRICAO) = '') 
	            THEN A.PREFIXO
	        WHEN A.COD_EMPRESA = '6'  
	            THEN B.PRX_DESCRICAO
	        ELSE A.PREFIXO 
	    END AS PREFIXO,
	    A.ANO,
	    A.MES,
	    A.ORIGEM,
	    A.VIATURA,
	    A.CONTRATO,
	    C.FORNECEDOR_SAP
	FROM {{ ref('slv_equipe_contrato') }} A 
	LEFT JOIN EQTLINFO_RAW.OPER_GO.PREFIXO_TURMA B  ON B.PRX_NOME_INTERNO = A.PREFIXO
	LEFT JOIN SB_PERFORMANCE.EQTL_CORP.SLV_CONTRATOS C  ON C.CONTRATO_SAP = A.CONTRATO
	LEFT JOIN SB_PERFORMANCE.SILVER.GLOSSARIO_EMPRESAS G ON G.COD_EMPRESA = A.COD_EMPRESA
) C ON C.PREFIXO = U.EQUIPE AND C.MES = U.MES_CONCL AND C.ANO = U.ANO_CONCL AND C.SIGLA_EMPRESA = U.EMPRESA
WHERE TO_DATE(U.COMPETENCIA,'YYYYMM') = TO_DATE({{ get_month_ref() }}, 'YYYYMM')