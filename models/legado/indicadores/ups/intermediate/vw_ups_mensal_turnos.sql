WITH ESCALA AS (
	SELECT 
	ORIGEM, 
	PREFIXO_UNICO EQUIPE, 
	TO_CHAR(TRUNC(DATA,'MM'),'YYYYMM') MES, 
	COUNT(DISTINCT DATA) TURNOS
	FROM {{ ref('aderencia_escala') }}
	WHERE TO_CHAR(DATA,'YYYYMM') = {{ get_month_ref() }}
    AND HTP_INICIO_TURNO IS NOT NULL
	GROUP BY ALL
)
SELECT ups.* EXCLUDE(META_MENSAL, DIAS_UTEIS, TURNOS, MEDIA_UPS_MES, ADERENCIA_META, CLASSE_MEPE_UPS, DATA_DADOS),
esc.TURNOS*42 META_MENSAL,
NULL DIAS_UTEIS,
esc.TURNOS TURNOS,
UPS.REALIZADO_MENSAL/ESC.TURNOS MEDIA_UPS_MES,
UPS.REALIZADO_MENSAL/(ESC.TURNOS*42) ADERENCIA_META,
CASE 
	WHEN ((UPS.REALIZADO_MENSAL/(ESC.TURNOS*42))*100) >  90 THEN 'A'
	WHEN ((UPS.REALIZADO_MENSAL/(ESC.TURNOS*42))*100) >  75 THEN 'B'
	WHEN ((UPS.REALIZADO_MENSAL/(ESC.TURNOS*42))*100) >  40 THEN 'C'
	WHEN ((UPS.REALIZADO_MENSAL/(ESC.TURNOS*42))*100) <= 40 THEN 'D'
ELSE '' END  CLASSE_MEPE_UPS,
CURRENT_TIMESTAMP DATA_DADOS
FROM {{ ref('ups_mensal') }} ups
LEFT JOIN ESCALA esc ON esc.ORIGEM = ups.EMPRESA 
	AND esc.EQUIPE = REPLACE(REGEXP_REPLACE(UPS.EQUIPE, '^(AP-|AL-|MA-|PA-|PI-|RS-|GO-|-)',''),'-','')
	AND esc.MES = ups.COMPETENCIA
WHERE esc.TURNOS IS NOT NULL
AND ups.COMPETENCIA = {{ get_month_ref() }}