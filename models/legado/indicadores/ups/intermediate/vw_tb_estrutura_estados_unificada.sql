SELECT 'EQTL_RS', B.BASE_ID, B.BAS_NOME, PA.CODIGO_PA PA_BASE, 'RS' BASE_ESTADO, PA.CODIGO_PA, PA.PAP_NOME, 'RS' PA_ESTADO, UT.UTE_NOME, UT.CODIGO_UT, 'RS' UT_ESTADO, M.MNC_NOME, 'RS' MNC_SIGLA_UF,  M.MNC_ID
, CURRENT_TIMESTAMP DATA_DADOS
FROM EQTLINFO_RAW.OPER_RS.MUNICIPIO M
LEFT JOIN EQTLINFO_RAW.OPER_RS.BASE B ON B.BASE_ID = M.BASE_ID
LEFT JOIN EQTLINFO_RAW.OPER_RS.PONTO_APOIO PA ON B.CODIGO_PA = PA.CODIGO_PA
LEFT JOIN EQTLINFO_RAW.OPER_RS.REGIAO_ELETRICA RE ON PA.REG_ELETRICA_ID = RE.REG_ELETRICA_ID
LEFT JOIN EQTLINFO_RAW.OPER_RS.UNIDADE_TERRITORIAL UT ON UT.REG_ELETRICA_ID = RE.REG_ELETRICA_ID