WITH base AS (
    SELECT A.*
    FROM {{ ref('resumo_ups') }} A
    LEFT JOIN {{ ref('vw_gp_gstc_equipes_sem_duplicatas') }} C
        ON C.DISTRIBUIDORA = A.EMPRESA
       AND C.PREFIXO = A.EQUIPE
    LEFT JOIN SB_PERFORMANCE.EQTL_CORP.GSTC_EXPURGO B
        ON B.COMPETENCIA = A.COMPETENCIA
       AND B.EMPRESA = A.EMPRESA
       AND B.EQUIPE = A.EQUIPE
    WHERE A.PROCESSO_PRX IN ('ATENDIMENTO EMERGENCIAL', 'CORTE LEVE', 'CORTE MOTO','LN LEVE','LN 4X4','SEED_MONEY')
      AND A.TIPO_EQP NOT IN ('FISCAL','QUALIDADE','GRUPO A','LINHA MORTA','MANUTENCAO','NAO_LIDOS','OBRAS','PI_DE_RAMAL','PODA','PROPRIO','RECUPERACAO_ENERGIA','SUSTENTACAO_SMC_PERDAS')
      AND A.REGIONAL_PRX IS NOT NULL
      AND A.CLASSE_MEPE_UPS IS NOT NULL
      AND B.EXPURGAR IS NULL
      AND TO_DATE(A.COMPETENCIA,'YYYYMM') = TO_DATE({{ get_month_ref() }},'YYYYMM')
)

-- D-R-P-F level
SELECT 
    'DESENVOLVIMENTO DE EQUIPES UPS' AS INDICADOR,
    base.EMPRESA AS DISTRIBUIDORA,
    base.REGIONAL_PRX AS REGIONAL,
    base.POLO_PRX AS POLO,
    base.FORNECEDOR,
    '' AS CONTRATO,
    base.COMPETENCIA,
    SUM(CASE WHEN base.CLASSE_MEPE_UPS = 'A' THEN 1 ELSE 0 END) AS QTD_A,
    SUM(CASE WHEN base.CLASSE_MEPE_UPS = 'B' THEN 1 ELSE 0 END) AS QTD_B,
    SUM(CASE WHEN base.CLASSE_MEPE_UPS = 'C' THEN 1 ELSE 0 END) AS QTD_C,
    SUM(CASE WHEN base.CLASSE_MEPE_UPS = 'D' THEN 1 ELSE 0 END) AS QTD_D,
    COUNT(base.EQUIPE) AS QTD_TOTAL,
    SUM(CASE WHEN base.CLASSE_MEPE_UPS IN ('A','B') THEN 1 ELSE 0 END) AS A_B,
    TRUNC(
        (SUM(CASE WHEN base.CLASSE_MEPE_UPS = 'A' THEN 1 ELSE 0 END)/NULLIF(COUNT(base.EQUIPE),0)
       + SUM(CASE WHEN base.CLASSE_MEPE_UPS = 'B' THEN 1 ELSE 0 END)/NULLIF(COUNT(base.EQUIPE),0)),4
    ) AS RESULTADO,
    'D-R-P-F' AS NIVEL,
    'DESENVOLVIMENTO DE EQUIPES UPS' || '-' || base.EMPRESA || '-' || base.REGIONAL_PRX || '-' || base.POLO_PRX || '-' || base.FORNECEDOR AS DESCRICAO,
    '0,88' AS META,
    'MAIOR MELHOR' AS POLARIDADE,
    CURRENT_TIMESTAMP AS DATA_DADOS
FROM base
GROUP BY base.EMPRESA, base.REGIONAL_PRX, base.POLO_PRX, base.FORNECEDOR, base.COMPETENCIA

UNION ALL

