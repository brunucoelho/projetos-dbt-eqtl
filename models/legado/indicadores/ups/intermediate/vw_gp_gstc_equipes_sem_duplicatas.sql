SELECT
CHAVE, EMPRESA, DISTRIBUIDORA, REGIONAL, POLO, MUNICIPIO, FONECEDOR, PREFIXO, TIPO_EQP, SUBTIPO_EQP, PROCESSO, SERV_CAMPO, SERV_CONTRATO, TIPO_EQP_CONTRATO, TIPO, QTD_ELETRICISTAS, ANO_REFERENCIA, STATUS, INICIO_TURNO1, FIM_TURNO1, INICIO_TURNO2, FIM_TURNO2, ATUALIZACAO, OBS, VIGENCIA_INICIAL_EQP, VIGENCIA_FINAL_EQP, LATITUDE, LONGITUDE
FROM (
SELECT G.*, ROW_NUMBER() OVER (PARTITION BY DISTRIBUIDORA, PREFIXO ORDER BY VIGENCIA_FINAL_EQP DESC ) RN
FROM (
	SELECT DISTINCT *
	FROM {{ ref('gp_gstc_equipes2021') }}
	) G
	)
WHERE RN = 1