WITH base AS (
    SELECT 
        A.EMPRESA,
        A.REGIONAL_PRX,
        A.POLO_PRX,
        A.FORNECEDOR,
        A.ANO_CONCL,
        A.COMPETENCIA,
        A.MEDIA_UPS_MES
    FROM {{ ref('resumo_ups') }} A
    LEFT JOIN {{ ref('vw_gp_gstc_equipes_sem_duplicatas') }} C
        ON C.DISTRIBUIDORA = A.EMPRESA AND A.EQUIPE = C.PREFIXO
    LEFT JOIN SB_PERFORMANCE.EQTL_CORP.GSTC_EXPURGO B
        ON B.COMPETENCIA = A.COMPETENCIA AND B.EMPRESA = A.EMPRESA AND B.EQUIPE = A.EQUIPE
    WHERE A.PROCESSO_PRX IN ('ATENDIMENTO EMERGENCIAL','CORTE LEVE','CORTE MOTO','LN LEVE','LN 4X4')
      AND A.TIPO_EQP NOT IN ('NAO_LIDOS','PERDAS BT','RECUPERACAO_ENERGIA','SUSTENTACAO_SMC_PERDAS','MANUTENCAO')
      AND B.expurgar IS NULL
      AND A.REGIONAL_PRX IS NOT NULL
      AND A.CLASSE_MEPE_UPS IS NOT NULL
      AND TO_DATE(A.COMPETENCIA,'YYYYMM') = TO_DATE({{ get_month_ref() }},'YYYYMM')
)

-- D-R-P-F
SELECT
    'PRODUTIVIDADE EM UPS' AS INDICADOR,
    EMPRESA,
    REGIONAL_PRX,
    POLO_PRX,
    FORNECEDOR,
    'N/A' AS CONTRATO,
    ANO_CONCL,
    COMPETENCIA,
    AVG(MEDIA_UPS_MES) AS RESULTADO,
    'D-R-P-F' AS NIVEL,
    'PRODUTIVIDADE EM UPS' || '-' || EMPRESA || '-' || REGIONAL_PRX || '-' || POLO_PRX || '-' || FORNECEDOR AS DESCRICAO,
    '42' AS META,
    'MAIOR MELHOR' AS POLARIDADE
FROM base
GROUP BY EMPRESA, REGIONAL_PRX, POLO_PRX, FORNECEDOR, ANO_CONCL, COMPETENCIA

UNION ALL

