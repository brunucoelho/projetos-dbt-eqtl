SELECT 
DECODE(A.TX_STATE,'AL','EQTL_AL','PA','EQTL_PA','PI','EQTL_PI','MA','EQTL_MA','AP','EQTL_AP','RS','EQTL_RS') EMPRESA,
E.UTE_NOME REGIONAL,
E.PAP_NOME PONTO_APOIO,
E.BAS_NOME BASE,
NULL BAIRRO,
NULL LOCALIDADE,
E.MNC_NOME MUNICIPIO,
NULL U_R,
A.TX_EQ_INSTALACAO::VARCHAR UC,
--to_number(replace(nvl(A.TX_OS,'0'),'INC ','')) OS_OPER,
a.TX_OS::VARCHAR OS_OPER,
A.NB_ID::VARCHAR OS,
SUBSTR(A.TX_RESOURCEEXTERNALID,4) EQUIPE,

CASE 
	WHEN A.TX_EQ_TIPONOTA is null THEN A.Tx_Worktype 
	ELSE A.TX_EQ_TIPONOTA 
END OSTIPO_ID,
A.TX_EQ_CODE SSTIPO_ID,
A.TX_EQ_GRUPOCODE OSSUBTIPO_ID,
A.TX_EQ_GRUPOCODEMEDIDA GRUPO_CODE_MEDIDA_ID,
CASE 
	WHEN A.TX_EQ_TIPONOTA = 'CT' THEN TX_EQ_TIPOVIATURACORTE 
	ELSE NULL 
END MODALIDADE,

NVL(A.TX_TEXT_EQ_TIPOABRANGENCIA,'CR') ABRANGENCIA,
CASE
	WHEN A.LOCAL_RELIGACAO IN ('PA01','RS01','PI01','AL01','1','01') THEN '01'
	WHEN A.LOCAL_RELIGACAO IN ('PA02','RS02','PI02','AL02','2','02') THEN '02'
	WHEN A.LOCAL_RELIGACAO IN ('AP12','AP2','AP16','AP15','RS03','PA03','PI03','AL03','AL1','3','03','AP02','AP016','AP015','AP012') THEN '03'
	WHEN A.LOCAL_RELIGACAO IN ('AP18','RS04','PI18','PI04','PA04','AL2','AL04','4','04','AP018') THEN '04'
	WHEN A.LOCAL_RELIGACAO IN ('AP17','PI05','RS05','PA05','AP01','AL3','AL05','5','05','AP1','AP017') THEN '05'
	WHEN A.LOCAL_RELIGACAO IN ('AP9','AP8','AP7','AP6','AP5','AP4','AP3','AP14','AP13','AP11','RS06','PI06','PA06','AP010','AL4','AL06','AP011','6','06','AP03','AP10','AP09','AP08','AP07','AP06','AP05','AP04','AP014','AP013'
) THEN '06'
	ELSE NULL
END  COD_LOCAL_RELIGACAO,

CASE
	WHEN A.LOCAL_RELIGACAO IN ('PA01','RS01','PI01','AL01','1','01') THEN 'BEIRAL'
	WHEN A.LOCAL_RELIGACAO IN ('PA02','RS02','PI02','AL02','2','02') THEN 'BARRAMENTO/CPREDE'
	WHEN A.LOCAL_RELIGACAO IN ('AP12','AP2','AP16','AP15','RS03','PA03','PI03','AL03','AL1','3','AP02','AP016','AP015','AP012','03') THEN 'MEDIDOR'
	WHEN A.LOCAL_RELIGACAO IN ('AP18','RS04','PI18','PI04','PA04','AL2','AL04','4','AP018','04') THEN 'DISJUNTOR'
	WHEN A.LOCAL_RELIGACAO IN ('AP17','PI05','RS05','PA05','AP01','AL3','AL05','5','AP1','AP017','05') THEN 'POSTE/CPREDE'
	WHEN A.LOCAL_RELIGACAO IN ('AP9','AP8','AP7','AP6','AP5','AP4','AP3','AP14','AP13','AP11','RS06','PI06','PA06','AP010','AL4','AL06','AP011','6','06','AP03','AP10','AP09','AP08','AP07','AP06','AP05','AP04','AP014','AP013'
	) THEN 'RAMAL RETIRADO'
	ELSE ''
END LOCAL_RELIGACAO,
TO_CHAR(TO_NUMBER(SUBSTR(NULLIF(C.TX_CHAVE, ''), -4))) COD_CONCLUSAO,
CASE 
	WHEN A.Tx_Worktype = 'PO' AND A.TX_STATUS <> 'completed' THEN 'PODA NAO EXECUTADO'
	WHEN A.Tx_Worktype = 'PO' AND A.TX_STATUS = 'completed' THEN 'PODA EXECUTADO'
	WHEN A.Tx_Worktype = 'PREINSP' AND A.TX_STATUS = 'notdone' THEN 'NAO EXECUTADO'
	WHEN A.Tx_Worktype = 'PREINSP' AND A.TX_STATUS = 'completed' THEN 'EXECUTADO'
	WHEN A.Tx_Worktype = 'AUX_NR' AND A.TX_STATUS = 'completed' THEN 'ASSISTENCIA NR'
	WHEN A.Tx_Worktype = 'AUX_NR' AND A.TX_STATUS <> 'completed' THEN 'ASSISTENCIA NR NAO EXECUTADO'
	WHEN A.TX_EQ_TIPONOTA = 'NR' THEN upper(TRANSLATE(A.TX_TEXT_EQ_TIPODECAUSATEXT,'脕脌脗脙脛脡脠脢脣脥脤脦脧脫脪脭脮脰脷脵脹脺脟脩谩脿芒茫盲茅猫锚毛铆矛卯茂贸貌么玫枚煤霉没眉莽帽','AAAAAEEEEIIIIOOOOOUUUUCNaaaaaeeeeiiiiooooouuuucn'))
	ELSE upper(TRANSLATE(C.TX_VALOR,'脕脌脗脙脛脡脠脢脣脥脤脦脧脫脪脭脮脰脷脵脹脺脟脩谩脿芒茫盲茅猫锚毛铆矛卯茂贸貌么玫枚煤霉没眉莽帽','AAAAAEEEEIIIIOOOOOUUUUCNaaaaaeeeeiiiiooooouuuucn'))
END TIPO_CONCLUSAO,
--A.TX_TEXT_EQ_TIPODENATUREZATEXT NATUREZA,
--A.TX_TEXT_EQ_TIPODECAUSATEXT TIPO_CAUSA,
--A.TX_TEXT_EQ_SUBTIPOCAUSATEXT SUBTIPO_CAUSA,
--A.TX_TEXT_EQ_PONTODEFEITO PONTO_DEFEITO,
case 
	when A.TX_STATUS = 'notdone' then 'IMPROCEDENTE'
	when A.TX_STATUS IN ('complete','completed') then 'PROCEDENTE'
	ELSE 'N/A'
END PROCEDENCIA,
A.TX_EQ_TIPOCONCLUSAONAOEXEC MOTIVO_REJEICAO,
TRY_TO_TIMESTAMP(A.TX_EQ_DATAABERTURA, 'DD/MM/YYYY HH24:MI:SS') DATA_ORIGEM,
A.DT_TIMEASSIGNMENT DATA_ATRIBUICAO,
TRY_TO_TIMESTAMP(A.DATA_HORA_ACAO_DESLOCAMENTO, 'YYYY-MM-DD HH24:MI') INICIO_DESLOCAMENTO,
DATEADD(
    minute, 
    NB_TRAVELINGTIME, 
    TRY_TO_TIMESTAMP(A.DATA_HORA_ACAO_DESLOCAMENTO, 'YYYY-MM-DD HH24:MI')
) FIM_DESLOCAMENTO,
TRY_TO_TIMESTAMP(A.DATA_HORA_ACAO_INICIO, 'YYYY-MM-DD HH24:MI') DATA_INICIO_SERVICO,
--A.DATA_HORA_ACAO_CONCLUS O DATA_FIM_SERVICO,
--A.DT_ACTIVITY DATA_CONCLUSAO,
TRY_TO_TIMESTAMP(A.DATA_HORA_ACAO_CONCLUSAO,'YYYY-MM-DD HH24:MI') DATA_CONCLUSAO,
/*A.NB_TRAVELINGTIME,
A.NB_DURATION,*/
TO_CHAR(A.DT_ACTIVITY,'YYYYMM') COMPETENCIA,
{{ parse_timestamp('A.TX_EQ_DATACONCLUSAOLIMITE') }} DATA_LIMITE,
'N/A' STATUS_PRAZO,
TO_CHAR(A.DT_ACTIVITY,'DD') DIA,
'SEM_' ||
LPAD(TO_VARCHAR(DATE_PART(weekofyear, A.DT_ACTIVITY)- DATE_PART(weekofyear, DATE_TRUNC('month', A.DT_ACTIVITY)) + 1), 2,'0') SEMANA_MES_CONC,
UPPER(TO_CHAR(A.DT_ACTIVITY, 'DY')) SEMANA_MES_ABREV,
TO_CHAR(A.DT_ACTIVITY,'MM') MES_CONCL,
UPPER(TO_CHAR(A.DT_ACTIVITY,'MON')) MES_CONCL_ABREV,
TO_CHAR(A.DT_ACTIVITY,'YYYY') ANO_CONCL,
TO_CHAR(A.DT_ACTIVITY,'MM/YYYY') MES_ANO,
TO_CHAR(TRY_TO_TIMESTAMP(A.DATA_HORA_ACAO_CONCLUSAO,'YYYY-MM-DD HH24:MI'), 'HH24:MI:SS') HORA_CONCL,

CASE 
 WHEN  TO_CHAR(TRY_TO_TIMESTAMP(A.DATA_HORA_ACAO_CONCLUSAO,'YYYY-MM-DD HH24:MI'), 'HH24:MI:SS') >= '01:00:00' AND TO_CHAR(TRY_TO_TIMESTAMP(A.DATA_HORA_ACAO_CONCLUSAO,'YYYY-MM-DD HH24:MI'), 'HH24:MI:SS') < '08:00:00' THEN 'MADRUGADA'
 WHEN  TO_CHAR(TRY_TO_TIMESTAMP(A.DATA_HORA_ACAO_CONCLUSAO,'YYYY-MM-DD HH24:MI'), 'HH24:MI:SS') >= '08:00:00' AND TO_CHAR(TRY_TO_TIMESTAMP(A.DATA_HORA_ACAO_CONCLUSAO,'YYYY-MM-DD HH24:MI'), 'HH24:MI:SS') < '13:00:00' THEN 'MANHA'
 WHEN  TO_CHAR(TRY_TO_TIMESTAMP(A.DATA_HORA_ACAO_CONCLUSAO,'YYYY-MM-DD HH24:MI'), 'HH24:MI:SS') >= '13:00:00' AND TO_CHAR(TRY_TO_TIMESTAMP(A.DATA_HORA_ACAO_CONCLUSAO,'YYYY-MM-DD HH24:MI'), 'HH24:MI:SS') < '18:00:00' THEN 'TARDE'
 WHEN  TO_CHAR(TRY_TO_TIMESTAMP(A.DATA_HORA_ACAO_CONCLUSAO,'YYYY-MM-DD HH24:MI'), 'HH24:MI:SS') >= '18:00:00' AND TO_CHAR(TRY_TO_TIMESTAMP(A.DATA_HORA_ACAO_CONCLUSAO,'YYYY-MM-DD HH24:MI'), 'HH24:MI:SS') <= '23:59:59' THEN 'NOITE'
 WHEN  TO_CHAR(TRY_TO_TIMESTAMP(A.DATA_HORA_ACAO_CONCLUSAO,'YYYY-MM-DD HH24:MI'), 'HH24:MI:SS') >= '00:00:00' AND TO_CHAR(TRY_TO_TIMESTAMP(A.DATA_HORA_ACAO_CONCLUSAO,'YYYY-MM-DD HH24:MI'), 'HH24:MI:SS') <= '01:00:00' THEN 'NOITE'
 ELSE 'ERRO'
END TURNO,

CURRENT_TIMESTAMP DATA_DADOS

FROM  EQTLINFO_RAW.SIGA.TB_OFSC_ACTIVITIES_RT A
LEFT JOIN EQTLINFO_RAW.SIGA.tb_ofsc_resources_rt B ON B.NB_ID=A.NB_RESOURCEID
LEFT JOIN SB_PERFORMANCE.EQTL_CORP.TB_ESTRUTURA_ESTADOS_UNIFICADA e on a.nb_eq_codigomunicipio = e.mnc_id 
	and e.empresa = DECODE(A.TX_STATE,'AL','EQTL_AL','PA','EQTL_PA','PI','EQTL_PI','MA','EQTL_MA','AP','EQTL_AP','RS','EQTL_RS')
LEFT JOIN EQTLINFO_RAW.SIGA.tb_dm_propriedades C ON C.TX_CHAVE = A.TX_EQ_TIPOCONCLUSAOEXEC OR C.TX_CHAVE = A.TX_EQ_TIPOCONCLUSAONAOEXEC
--LEFT JOIN SB_PERFORMANCE.EQTL_CORP.VW_GP_GSTC_EQUIPES_SEM_DUPLICATAS EQ ON EQ.DISTRIBUIDORA = 'EQTL_MA' AND EQ.PREFIXO=replace(A.TX_RESOURCEEXTERNALID,'MA_','')
 
 WHERE TX_STATE IN ('AL','AP','MA','PA','PI','RS','GO')
 --AND A.TX_WORKTYPE = 'AUX_NR'
 AND A.TX_STATUS IN ('complete','completed','notdone')
 AND TRY_TO_TIMESTAMP(A.DATA_HORA_ACAO_CONCLUSAO,'YYYY-MM-DD HH24:MI') IS NOT NULL
 AND TRUNC(A.DT_ACTIVITY,'MM') = TO_DATE({{ get_month_ref() }},'YYYYMM')
 AND A.Tx_Worktype not in ('Indisponibilidade','INTERVALO','OFFLINE','TrocaTurno')
 AND A.TX_EQ_TIPONOTA IN ('NR','CT','DR','DS','IS','LG','M1','MM','MQ','MT','NT','PA','RI','TR','LN','RL')
 
