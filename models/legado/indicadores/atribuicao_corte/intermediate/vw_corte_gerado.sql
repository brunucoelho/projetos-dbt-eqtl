SELECT 
CURRENT_TIMESTAMP DH_ETL,
1 COD_EMPRESA,
LTRIM(NOTA_SERVICO,'0') NOTA,
TRY_TO_DATE(DATA_GERACAO,'YYYYMMDD')::TIMESTAMP DH_GERACAO,
TRY_TO_DATE(DATA_FIM,'YYYYMMDD')::TIMESTAMP DH_FINALIZADO,
TRY_TO_DATE(DATA_EXECUCAO,'YYYYMMDD')::TIMESTAMP DH_EXECUCAO_CAMPO,
REPLACE(COD_VIATURA, 'AL_AL-','AL-') EQUIPE,
DECODE(STATUS_EXECUCAO, 'X','FINALIZADO','GERADO') STATUS,
FROM (
SELECT DISTINCT *
FROM EQTLINFO_PRD.EQTL_AL.ACOES_COBRANCA_NOTAS
WHERE TO_VARCHAR(TRY_TO_DATE(DATA_GERACAO,'YYYYMMDD'),'YYYYMM') = {{ get_month_ref() }}
AND ETAPA_COBRANCA = 'CORT'
AND (STATUS_EXECUCAO IS NULL OR STATUS_EXECUCAO = 'X')
AND COD_VIATURA IS NOT NULL AND COD_VIATURA <> ' '
)

UNION ALL 

