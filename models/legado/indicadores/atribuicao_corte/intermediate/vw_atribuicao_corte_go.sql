SELECT
	CURRENT_TIMESTAMP DH_ETL,
	6 COD_EMPRESA,
	moc.NR_OS NOTA,
	aoc.ATROS_DATA DH_ATRIBUICAO ,
	COALESCE(pt.PRX_NOME_INTERNO, pt.PRX_DESCRICAO) EQUIPE,
	DECODE(
aoc.ATROS_STATUS,
'E', 'ATRIBUIDO',
'A', 'ATRIBUIDO',
'R', 'REJEITADO',
'N', 'NAO_EXECUTADO',
'C', 'CANCELADO',
'S', 'ATRIBUIDO',
'F', 'EXECUTADO') STATUS
FROM
	EQTLINFO_RAW.OPER_GO.ATRIBUI_OS_COMERCIAL aoc
INNER JOIN EQTLINFO_RAW.OPER_GO.PREFIXO_TURMA pt ON
	pt.PREFIXO_TURMA_ID = aoc.PREFIXO_TURMA_ID
INNER JOIN EQTLINFO_RAW.OPER_GO.MOVTO_OS_COMERCIAL moc ON
	moc.CD_MOVTO_OS_COMERCIAL = aoc.CD_MOVTO_OS_COMERCIAL
WHERE
	to_varchar(trunc(ATROS_DATA, 'MM'),'YYYYMM') = {{ get_month_ref() }}
	AND moc.OSTIPO_ID = 'CT'