WITH base_cte AS (
SELECT *
FROM SB_PERFORMANCE.EQTL_CORP.DISPONIBILIDADE_OPERACIONAL vae
LEFT JOIN (
SELECT 
sec.ano,
sec.mes,
ge.SIGLA_EMPRESA,
sec.CONTRATO,
con.FORNECEDOR_SAP,
    CASE
        WHEN SUBSTR(REGEXP_REPLACE(UPPER(TRIM(sec.PREFIXO)), '[^A-Z0-9]', ''), 1, 2) IN ('MA','PA','PI','AL','AP','GO','RS')
            AND LENGTH(REGEXP_REPLACE(UPPER(TRIM(sec.PREFIXO)), '[^A-Z0-9]', '')) = 10
            THEN SUBSTR(REGEXP_REPLACE(UPPER(TRIM(sec.PREFIXO)), '[^A-Z0-9]', ''), LENGTH(REGEXP_REPLACE(UPPER(TRIM(sec.PREFIXO)), '[^A-Z0-9]', '')) - 7, 8)
        ELSE REGEXP_REPLACE(UPPER(TRIM(sec.PREFIXO)), '[^A-Z0-9]', '')
    END AS PREFIXO_UNICO_CTR
FROM
{{ ref('slv_equipe_contrato') }} sec 
LEFT JOIN SB_PERFORMANCE.EQTL_CORP.SLV_CONTRATOS con ON con.CONTRATO_SAP = sec.CONTRATO AND con.COD_EMPRESA = sec.COD_EMPRESA
LEFT JOIN SB_PERFORMANCE.SILVER.GLOSSARIO_EMPRESAS ge ON ge.COD_EMPRESA = sec.COD_EMPRESA
) A
	ON a.PREFIXO_UNICO_CTR = vae.PREFIXO 
	AND a.ANO = EXTRACT(YEAR FROM vae.DATA)
	AND a.MES = EXTRACT(MONTH FROM vae.DATA)
	AND a.SIGLA_EMPRESA = vae.ORIGEM
WHERE vae.DATA >= date'2024-08-01'
AND JORNADA_PREVISTA IS NOT NULL
),

cte_gpby as(
SELECT 
ORIGEM EMPRESA,
NVL(CONTRATO,'AUSENTE') CONTRATO,
FORNECEDOR_SAP FORNECEDOR,
EXTRACT(YEAR FROM DATA) ANO,
EXTRACT(MONTH FROM DATA) MES,
ROUND(SUM(NVL(JORNADA_PREVISTA,0)),2) VAR_1_TEMPO_PREVISTO,
ROUND(SUM(NVL(TEMPO_DISPONIVEL,0)),2) VAR_2_TEMPO_DISPONIVEL,
ROUND(SUM(NVL(TEMPO_DISPONIVEL,0)) / SUM(NVL(JORNADA_PREVISTA,0)),4) VALOR,
'%' UNIDADE
FROM base_cte
GROUP BY
ORIGEM,
NVL(CONTRATO,'AUSENTE'),
FORNECEDOR_SAP,
EXTRACT(YEAR FROM DATA),
EXTRACT(MONTH FROM DATA)
)

SELECT 
CURRENT_TIMESTAMP AS DATA_ETL,
a.EMPRESA,
a.CONTRATO,
a.FORNECEDOR,
a.ANO,
a.MES,
'VAR_2_TEMPO_DISPONIVEL / VAR_1_TEMPO_PREVISTO' FORMULA,
a.VAR_1_TEMPO_PREVISTO,
a.VAR_2_TEMPO_DISPONIVEL,
a.VALOR,
a.UNIDADE
FROM cte_gpby a
WHERE ano IS NOT NULL 
AND mes IS NOT NULL 
AND contrato IS NOT null