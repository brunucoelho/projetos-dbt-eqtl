SELECT DISTINCT
    CURRENT_TIMESTAMP AS DATA_ETL,
    'EQTL_'||LEFT(TX_RESOURCEEXTERNALID,2) AS ORIGEM,
    INT.NB_ID,
    INT.DT_ACTIVITY AS DATA_INTERVALO,
    INT.TX_STATUS AS STATUS,
    CASE
        WHEN SUBSTR(REPLACE(SUBSTR(RES.TX_EXTERNALID, 4), '-', ''), 1, 2) IN ('AP','AL','PI','MA','PA','GO','RS')
            THEN SUBSTR(REPLACE(SUBSTR(RES.TX_EXTERNALID, 4), '-', ''), 3)
        ELSE REPLACE(SUBSTR(RES.TX_EXTERNALID, 4), '-', '')
    END AS PREFIXO,
    DATEADD(HOUR, -3, INT.DT_SERVICEWINSTART) AS INI_PREV,
    DATEADD(HOUR, -3, INT.DT_SERVICEWINEND) AS FIM_PREV,
    DATEDIFF(SECOND, INT.DT_SERVICEWINSTART, INT.DT_SERVICEWINEND) / 3600.0 AS DURACAO_PREV_HORAS,
    DATEADD(HOUR, -3, INT.DT_ESTIMATEDTIMEARRIVAL) AS INI_REAL,
    DATEADD(MINUTE, INT.NB_DURATION,DATEADD(HOUR, -3, INT.DT_ESTIMATEDTIMEARRIVAL)) AS FIM_REAL,
    CASE
        WHEN INT.TX_STATUS IN ('complete', 'completed') THEN
            DATEDIFF(
                SECOND,
                DATEADD(HOUR, -3, INT.DT_ESTIMATEDTIMEARRIVAL),
                DATEADD(
                    MINUTE,
                    INT.NB_DURATION,
                    DATEADD(HOUR, -3, INT.DT_ESTIMATEDTIMEARRIVAL)
                )
            ) / 3600.0
        ELSE NULL
    END AS DURACAO_REAL_HORAS
FROM EQTLINFO_RAW.SIGA.TB_OFSC_ACTIVITIES_RT INT
LEFT JOIN EQTLINFO_RAW.SIGA.TB_OFSC_RESOURCES_RT RES
       ON RES.TX_EXTERNALID = INT.TX_RESOURCEEXTERNALID
WHERE TO_CHAR(DATE_TRUNC('MONTH', INT.DT_ACTIVITY), 'YYYYMM') = {{ get_month_ref() }}
  AND DATE(INT.DT_ACTIVITY) <= DATEADD(DAY, -1, CURRENT_DATE)
  AND INT.TX_WORKTYPE = 'INTERVALO'
  AND INT.TX_STATUS NOT IN ('cancelled','deleted','suspended')
  AND LEFT(TX_RESOURCEEXTERNALID,2) IN ('AL','AP','PI','PA','GO','RS','MA')