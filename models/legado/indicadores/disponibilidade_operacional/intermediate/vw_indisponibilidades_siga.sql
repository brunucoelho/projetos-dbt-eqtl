SELECT DISTINCT
    CURRENT_TIMESTAMP AS DATA_ETL,
    'EQTL_'||LEFT(TX_RESOURCEEXTERNALID,2) AS ORIGEM,
    INT.NB_ID,
    INT.DT_ACTIVITY AS DATA_INDISPONIBILIDADE,
    INT.TX_STATUS AS STATUS,
    INT.TX_EQ_OBSERVACAO AS OBSERVACAO,

    CASE
        WHEN SUBSTR(REPLACE(SUBSTR(RES.TX_EXTERNALID, 4), '-', ''), 1, 2) IN ('AP','AL','PI','MA','PA','GO','RS')
        THEN SUBSTR(REPLACE(SUBSTR(RES.TX_EXTERNALID, 4), '-', ''), 3)
        ELSE REPLACE(SUBSTR(RES.TX_EXTERNALID, 4), '-', '')
    END AS PREFIXO,

    INT.TX_WORKTYPE AS TIPO,

    DATEADD(hour, -3, INT.DT_ESTIMATEDTIMEARRIVAL) AS INI_REAL,

    DATEADD(
        minute, 
        INT.NB_DURATION, 
        DATEADD(hour, -3, INT.DT_ESTIMATEDTIMEARRIVAL)
    ) AS FIM_REAL,

    DATEDIFF(
        second, 
        DATEADD(hour, -3, INT.DT_ESTIMATEDTIMEARRIVAL), 
        DATEADD(minute, INT.NB_DURATION, DATEADD(hour, -3, INT.DT_ESTIMATEDTIMEARRIVAL))
    ) / 3600.0 AS DURACAO_REAL_HORAS,

    INT.TIPO_DE_INDISPONIBILIDADE
    
FROM EQTLINFO_RAW.SIGA.TB_OFSC_ACTIVITIES_RT INT
LEFT JOIN EQTLINFO_RAW.SIGA.TB_OFSC_RESOURCES_RT RES 
       ON RES.TX_EXTERNALID = INT.TX_RESOURCEEXTERNALID

WHERE TO_VARCHAR(DATE_TRUNC('DAY', INT.DT_ACTIVITY), 'YYYYMM') = {{ get_month_ref() }}
  AND DATE_TRUNC('DAY', INT.DT_ACTIVITY) <= DATE_TRUNC('DAY', CURRENT_DATE) - 1
  AND INT.TX_WORKTYPE IN ('Indisponibilidade','TrocaTurno')
  AND INT.TX_STATUS NOT IN ('suspended','cancelled','notdone','deleted')
  AND LEFT(TX_RESOURCEEXTERNALID,2) IN ('AL','AP','PI','PA','GO','RS','MA')
