WITH CTE_ESCALAS AS (
    SELECT 
        ADE.ORIGEM,
        ADE.PREFIXO,
        ADE.REGIONAL,
        ADE.POLO,
        ADE.MUNICIPIO,
        ADE.FORNECEDOR,
        ADE.SERV_CAMPO,
        ADE.PROCESSO,
        ADE.SERV_CONTRATO,
        ADE.TIPO_EQP,
        ADE.SUBTIPO_EQP,
        ADE.TIPO_EQP_CONTRATO,
        ADE.DATA,
        CASE
            WHEN SUM(ESCALA_DEMANDADA) < 0 THEN 0
            ELSE SUM(ESCALA_DEMANDADA)
        END AS ESCALA_PREVISTA
    FROM (
        SELECT DISTINCT
            ORIGEM,
            PREFIXO_UNICO PREFIXO,
            REGIONAL,
            POLO,
            MUNICIPIO,
            FORNECEDOR,
            SERV_CAMPO,
            PROCESSO,
            SERV_CONTRATO,
            TIPO_EQP,
            SUBTIPO_EQP,
            TIPO_EQP_CONTRATO,
            DATA,
            DATEDIFF('second', HORARIO_INICIO_ESCALA, HORARIO_FIM_ESCALA) / 3600.0 AS ESCALA_DEMANDADA
        FROM {{ ref('aderencia_escala_gstc') }} AA
        WHERE TO_VARCHAR(DATE_TRUNC('DAY', AA.DATA), 'YYYYMM') = {{ get_month_ref() }}
          AND DATE_TRUNC('DAY', AA.DATA) <= DATE_TRUNC('DAY', CURRENT_DATE) - 1
    ) ADE
    GROUP BY
        ADE.ORIGEM, ADE.PREFIXO, ADE.REGIONAL, ADE.POLO, ADE.MUNICIPIO,
        ADE.FORNECEDOR, ADE.SERV_CAMPO, ADE.PROCESSO, ADE.SERV_CONTRATO,
        ADE.TIPO_EQP, ADE.SUBTIPO_EQP, ADE.TIPO_EQP_CONTRATO, ADE.DATA
),

CTE_INTERVALOS AS (
    SELECT 
        ORIGEM, PREFIXO, DATA_INTERVALO,
        SUM(DURACAO_PREV_HORAS) AS INTERVALO_PREVISTO,
        SUM(DURACAO_REAL_HORAS) AS INTERVALO_REAL
    FROM {{ ref('intervalos') }} AA
    WHERE TO_VARCHAR(TRUNC(AA.DATA_INTERVALO,'MM'), 'YYYYMM') = {{ get_month_ref() }}
    GROUP BY ORIGEM, PREFIXO, DATA_INTERVALO

    UNION ALL

    SELECT 
        ORIGEM, PREFIXO, DATA_INTERVALO,
        SUM(DURACAO_PREV_HORAS), SUM(DURACAO_REAL_HORAS)
    FROM {{ ref('intervalos_go') }} aa
    WHERE TO_VARCHAR(TRUNC(AA.DATA_INTERVALO,'MM'), 'YYYYMM') = {{ get_month_ref() }}
    GROUP BY ORIGEM, PREFIXO, DATA_INTERVALO
),

CTE_INDISPONIBILIDADES AS (
    SELECT
        ORIGEM, PREFIXO, DATA_INDISPONIBILIDADE,
        SUM(DURACAO_REAL_HORAS) AS INDISPONIBILIDADE
    FROM {{ ref('indisponibilidades') }} AA
    WHERE TO_VARCHAR(TRUNC(AA.DATA_INDISPONIBILIDADE,'MM'), 'YYYYMM') = {{ get_month_ref() }}
    GROUP BY ORIGEM, PREFIXO, DATA_INDISPONIBILIDADE

    UNION ALL
    SELECT
        ORIGEM, PREFIXO, DATA_INDISPONIBILIDADE,
        SUM(DURACAO_REAL_HORAS)
    FROM {{ ref('indisponibilidades_go') }} AA
    WHERE TO_VARCHAR(TRUNC(AA.DATA_INDISPONIBILIDADE,'MM'), 'YYYYMM') = {{ get_month_ref() }}
    GROUP BY ORIGEM, PREFIXO, DATA_INDISPONIBILIDADE
),

-- Example for one TURNOS CTE (same pattern for others)
CTE_TURNOS_SIGA AS (
    SELECT 
        ORIGEM, PREFIXO, DATA_TURNO,
        ROUND(SUM(TEMPO_TOTAL),2) AS TEMPO_TOTAL
    FROM (
        SELECT 
            'EQTL_'||LEFT(TX_RESOURCEEXTERNALID,2) ORIGEM,
            CASE
                WHEN SUBSTR(REPLACE(SUBSTR(RES.TX_EXTERNALID, 4),'-',''), 1, 2) 
                    IN ('AP','AL','PI','MA','PA','GO','RS') 
                THEN SUBSTR(REPLACE(SUBSTR(RES.TX_EXTERNALID, 4),'-',''),3)
                ELSE REPLACE(SUBSTR(RES.TX_EXTERNALID, 4),'-','')
            END PREFIXO,
            ROT.DT_ROUTE AS DATA_TURNO,
            CASE
                WHEN COALESCE(DATEDIFF('second', ROT.DT_ROUTEACTIVATIONTIME, COALESCE(ROT.DT_ROUTEDEACTIVATIONTIME, ROT.DT_ROUTEREACTIVATIONTIME))/3600.0, 0) < 0 THEN 0
                WHEN COALESCE(DATEDIFF('second', ROT.DT_ROUTEACTIVATIONTIME, COALESCE(ROT.DT_ROUTEDEACTIVATIONTIME, ROT.DT_ROUTEREACTIVATIONTIME))/3600.0, 0) > 24 THEN 24
                ELSE COALESCE(DATEDIFF('second', ROT.DT_ROUTEACTIVATIONTIME, COALESCE(ROT.DT_ROUTEDEACTIVATIONTIME, ROT.DT_ROUTEREACTIVATIONTIME))/3600.0, 0)
            END AS TEMPO_TOTAL
        FROM EQTLINFO_RAW.SIGA.TB_OFSC_ROTA_RT ROT
        INNER JOIN EQTLINFO_RAW.SIGA.TB_OFSC_RESOURCES_RT RES 
            ON RES.TX_EXTERNALID = ROT.TX_RESOURCEEXTERNALID
        WHERE TO_VARCHAR(DATE_TRUNC('DAY', DT_ROUTE), 'YYYYMM') = {{ get_month_ref() }}
        AND DATE_TRUNC('DAY', DT_ROUTE) <= DATE_TRUNC('DAY', CURRENT_DATE) - 1
        AND RES.TX_TYPE IN ('Prefixo','PrefixoManutencao')
        AND LEFT(TX_RESOURCEEXTERNALID,2) IN ('AL','AP','PI','PA','GO','RS','MA')
    )
    GROUP BY ORIGEM, PREFIXO, DATA_TURNO
),

-- Repeat same conversion for ALAGOAS, AMAPA, MARANHAO, PARA, PIAUI, GOIAS
-- (pattern: replace NVL→COALESCE, (date2 - date1)*24 → DATEDIFF/3600.0, TO_CHAR/TRUNC → TO_VARCHAR/DATE_TRUNC)
CTE_TURNOS_GOIAS AS (
    SELECT 
        ORIGEM,
        PREFIXO,
        DATA_TURNO,
        ROUND(SUM(TEMPO_TOTAL), 2) AS TEMPO_TOTAL
    FROM (
        SELECT DISTINCT 
            HIST_TURMA_PLANTAO_ID,
            'EQTL_GO' AS ORIGEM,
            REGEXP_REPLACE(UPPER(TRIM(MA_HTP.VIATURA_ID)), '[^A-Z0-9]', '') AS VIATURA,
            
            CASE 
                WHEN SUBSTR(REGEXP_REPLACE(UPPER(TRIM(MA_PRT.PRX_DESCRICAO)), '[^A-Z0-9]', ''), 1, 2) LIKE 'GO'
                     AND LENGTH(REGEXP_REPLACE(UPPER(TRIM(MA_PRT.PRX_DESCRICAO)), '[^A-Z0-9]', '')) = 10
                THEN SUBSTR(
                        REGEXP_REPLACE(UPPER(TRIM(MA_PRT.PRX_DESCRICAO)), '[^A-Z0-9]', ''),
                        LENGTH(REGEXP_REPLACE(UPPER(TRIM(MA_PRT.PRX_DESCRICAO)), '[^A-Z0-9]', '')) - 7,
                        8
                     )
                ELSE REGEXP_REPLACE(UPPER(TRIM(MA_PRT.PRX_DESCRICAO)), '[^A-Z0-9]', '')
            END AS PREFIXO,

            DATE_TRUNC('DAY', MA_HTP.HTP_INICIO_TURNO) AS DATA_TURNO,

            CASE 
                WHEN COALESCE(DATEDIFF('second', HTP_INICIO_TURNO, HTP_FIM_TURNO) / 3600.0, 0) < 0 
                    THEN 0
                WHEN COALESCE(DATEDIFF('second', HTP_INICIO_TURNO, HTP_FIM_TURNO) / 3600.0, 0) > 24 
                    THEN 24
                ELSE COALESCE(DATEDIFF('second', HTP_INICIO_TURNO, HTP_FIM_TURNO) / 3600.0, 0)
            END AS TEMPO_TOTAL

        FROM EQTLINFO_RAW.OPER_GO.HISTORICO_TURMA_PLANTAO MA_HTP
        LEFT JOIN EQTLINFO_RAW.OPER_GO.PREFIXO_TURMA MA_PRT 
               ON MA_PRT.PREFIXO_TURMA_ID = MA_HTP.PREFIXO_TURMA_ID

        WHERE TO_CHAR(DATE_TRUNC('DAY', MA_HTP.HTP_INICIO_TURNO), 'YYYYMM') = {{ get_month_ref() }}
          AND DATE_TRUNC('DAY', MA_HTP.HTP_INICIO_TURNO) <= DATEADD(DAY, -1, CURRENT_DATE)
    ) t
    GROUP BY ORIGEM, PREFIXO, DATA_TURNO
),

CTE_TURNOS AS (
    SELECT * FROM CTE_TURNOS_SIGA
    UNION ALL
    SELECT * FROM CTE_TURNOS_GOIAS
)

SELECT 
    CURRENT_DATE AS DATA_ETL,
    ESC.*,
    INT.INTERVALO_PREVISTO,
    CASE 
        WHEN ESC.ESCALA_PREVISTA - COALESCE(INTERVALO_PREVISTO,0) < 0 THEN 0
        ELSE ESC.ESCALA_PREVISTA - COALESCE(INTERVALO_PREVISTO,0)
    END AS JORNADA_PREVISTA,

    TUR.TEMPO_TOTAL AS TURNO_REAL,
    INT.INTERVALO_REAL,
    IDS.INDISPONIBILIDADE AS TEMPO_INDISPONIBILIDADE,

    CASE
        WHEN ESC.ESCALA_PREVISTA IS NULL THEN NULL
        WHEN (TUR.TEMPO_TOTAL - COALESCE(INT.INTERVALO_REAL,0) - COALESCE(IDS.INDISPONIBILIDADE,0)) < 0 THEN 0
        WHEN ESC.ESCALA_PREVISTA - COALESCE(INT.INTERVALO_PREVISTO,0) < 0 THEN 0
        WHEN (TUR.TEMPO_TOTAL - COALESCE(INT.INTERVALO_REAL,0) - COALESCE(IDS.INDISPONIBILIDADE,0)) 
             > (ESC.ESCALA_PREVISTA - COALESCE(INTERVALO_PREVISTO,0))
        THEN ESC.ESCALA_PREVISTA - COALESCE(INTERVALO_PREVISTO,0)
        ELSE TUR.TEMPO_TOTAL - COALESCE(INT.INTERVALO_REAL,0) - COALESCE(IDS.INDISPONIBILIDADE,0)
    END AS TEMPO_DISPONIVEL,

    CASE
        WHEN ESC.ESCALA_PREVISTA IS NULL THEN (TUR.TEMPO_TOTAL - COALESCE(INT.INTERVALO_REAL,0) - COALESCE(IDS.INDISPONIBILIDADE,0))
        WHEN (TUR.TEMPO_TOTAL - COALESCE(INT.INTERVALO_REAL,0) - COALESCE(IDS.INDISPONIBILIDADE,0)) 
             >= (ESC.ESCALA_PREVISTA - COALESCE(INTERVALO_PREVISTO,0))
        THEN (TUR.TEMPO_TOTAL - COALESCE(INT.INTERVALO_REAL,0) - COALESCE(IDS.INDISPONIBILIDADE,0)) 
             - (ESC.ESCALA_PREVISTA - COALESCE(INTERVALO_PREVISTO,0))
        WHEN ESC.ESCALA_PREVISTA - COALESCE(INTERVALO_PREVISTO,0) < 0
        THEN TUR.TEMPO_TOTAL - COALESCE(INT.INTERVALO_REAL,0) - COALESCE(IDS.INDISPONIBILIDADE,0)
        ELSE (TUR.TEMPO_TOTAL - COALESCE(INT.INTERVALO_REAL,0) - COALESCE(IDS.INDISPONIBILIDADE,0)) 
             - (ESC.ESCALA_PREVISTA - COALESCE(INTERVALO_PREVISTO,0))
    END AS TEMPO_DISPONIVEL_EXCEDENTE,

    CASE
    WHEN ESC.PREFIXO IS NULL THEN NULL
    WHEN LENGTH(ESC.PREFIXO) = 6 THEN ESC.PREFIXO
    WHEN LENGTH(ESC.PREFIXO) = 7 AND ESC.ORIGEM != 'EQTL_GO' THEN SUBSTR(ESC.PREFIXO, 1, 3) || '-' || SUBSTR(ESC.PREFIXO, 4)
    WHEN LENGTH(ESC.PREFIXO) = 7 AND ESC.ORIGEM = 'EQTL_GO' THEN ESC.PREFIXO
    WHEN LENGTH(ESC.PREFIXO) = 8 THEN SUBSTR(ESC.ORIGEM, -2) || '-' || SUBSTR(ESC.PREFIXO, 1, 3) || '-' || SUBSTR(ESC.PREFIXO, 4)
    ELSE ESC.PREFIXO
    END AS PREFIXO_PADRAO,

    EXTRACT (YEAR FROM ESC.DATA) ANO,
    EXTRACT (MONTH FROM ESC.DATA) MES,
    EXTRACT (DAY FROM ESC.DATA) DIA,
    TO_CHAR(TRUNC(DATA,'MM'),'YYYYMM') COMPETENCIA,
    EC.CONTRATO CONTRATO_SAP

FROM CTE_ESCALAS ESC
LEFT JOIN CTE_INTERVALOS INT 
    ON ESC.ORIGEM = INT.ORIGEM 
   AND ESC.PREFIXO = INT.PREFIXO 
   AND ESC.DATA = INT.DATA_INTERVALO
LEFT JOIN CTE_INDISPONIBILIDADES IDS 
    ON ESC.ORIGEM = IDS.ORIGEM
   AND ESC.PREFIXO = IDS.PREFIXO
   AND ESC.DATA = IDS.DATA_INDISPONIBILIDADE
LEFT JOIN CTE_TURNOS TUR 
    ON ESC.ORIGEM = TUR.ORIGEM
   AND ESC.PREFIXO = TUR.PREFIXO
   AND ESC.DATA = TUR.DATA_TURNO
LEFT JOIN (
    SELECT sec.*,
    DECODE(COD_EMPRESA,1,'EQTL_AL',2,'EQTL_AP',3,'EQTL_MA',4,'EQTL_PA',5,'EQTL_PI',6,'EQTL_GO',7,'EQTL_RS') EMPRESA,
            CASE
                WHEN SUBSTR(REGEXP_REPLACE(UPPER(TRIM(sec.prefixo)), '[^A-Z0-9]', ''), 1, 2) IN ('MA','PA','PI','AL','AP','GO','RS')
                    AND LENGTH(REGEXP_REPLACE(UPPER(TRIM(sec.prefixo)), '[^A-Z0-9]', '')) = 10
                THEN SUBSTR(
                        REGEXP_REPLACE(UPPER(TRIM(sec.prefixo)), '[^A-Z0-9]', ''), 
                        LENGTH(REGEXP_REPLACE(UPPER(TRIM(sec.prefixo)), '[^A-Z0-9]', '')) - 7, 
                        8
                    )
                ELSE REGEXP_REPLACE(UPPER(TRIM(sec.prefixo)), '[^A-Z0-9]', '')
            END AS PREFIXO_UNICO
    FROM {{ ref('slv_equipe_contrato') }} sec 
) EC ON EC.PREFIXO_UNICO = ESC.PREFIXO
    AND EC.EMPRESA = ESC.ORIGEM 
    AND EC.ANO = EXTRACT (YEAR FROM ESC.DATA)
    AND EC.MES = EXTRACT (MONTH FROM ESC.DATA)