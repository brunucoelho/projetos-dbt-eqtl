WITH CTE_INTERVALO_PREV AS (

    SELECT 
        CASE
            WHEN SUBSTR(REGEXP_REPLACE(UPPER(TRIM(PRT.PRX_DESCRICAO)), '[^A-Z0-9]', ''), 1, 2) IN ('MA','PA','PI','AL','AP','GO','RS')
                 AND LENGTH(REGEXP_REPLACE(UPPER(TRIM(PRT.PRX_DESCRICAO)), '[^A-Z0-9]', '')) = 10
            THEN SUBSTR(
                    REGEXP_REPLACE(UPPER(TRIM(PRT.PRX_DESCRICAO)), '[^A-Z0-9]', ''), 
                    LENGTH(REGEXP_REPLACE(UPPER(TRIM(PRT.PRX_DESCRICAO)), '[^A-Z0-9]', '')) - 7, 
                    8
                 )
            ELSE REGEXP_REPLACE(UPPER(TRIM(PRT.PRX_DESCRICAO)), '[^A-Z0-9]', '')
        END AS PREFIXO,
        
        DATE_TRUNC('DAY', ECL.ESC_DATA) AS DATA_INTERVALO,
        
        POS.POS_INTERVALO AS DURACAO_PREV_HORAS
               
    FROM EQTLINFO_RAW.EQTL_GO.ESCALA ECL
    LEFT JOIN EQTLINFO_RAW.OPER_GO.POSICAO POS 
           ON POS.POS_CODIGO = ECL.POS_CODIGO
    LEFT JOIN EQTLINFO_RAW.OPER_GO.PREFIXO_TURMA PRT 
           ON PRT.PREFIXO_TURMA_ID = ECL.PREFIXO_TURMA_ID

    WHERE TO_CHAR(TRUNC(ECL.ESC_DATA,'MM'), 'YYYYMM') = {{ get_month_ref() }}
      AND DATE(ECL.ESC_DATA) <= DATEADD(DAY, -1, CURRENT_DATE)

      AND POS.POS_CODIGO NOT IN ('X', 'SB', 'FE', 'IND', 'RES') 
),
CTE_INTERVALO_REAL AS (

    SELECT 
        INT.*,
        CASE
            WHEN SUBSTR(REGEXP_REPLACE(UPPER(TRIM(PT.PRX_DESCRICAO)), '[^A-Z0-9]', ''), 1, 2) IN ('MA','PA','PI','AL','AP','GO','RS')
                 AND LENGTH(REGEXP_REPLACE(UPPER(TRIM(PT.PRX_DESCRICAO)), '[^A-Z0-9]', '')) = 10
            THEN SUBSTR(
                    REGEXP_REPLACE(UPPER(TRIM(PT.PRX_DESCRICAO)), '[^A-Z0-9]', ''), 
                    LENGTH(REGEXP_REPLACE(UPPER(TRIM(PT.PRX_DESCRICAO)), '[^A-Z0-9]', '')) - 7, 
                    8
                 )
            ELSE REGEXP_REPLACE(UPPER(TRIM(PT.PRX_DESCRICAO)), '[^A-Z0-9]', '')
        END AS PREFIXO

    FROM EQTLINFO_RAW.OPER_GO.INTERVALO INT
    LEFT JOIN EQTLINFO_PRD.EQTL_GO.MOTIVO_INTERVALO MI 
           ON INT.MOT_INTERVALO_ID = MI.MOT_INTERVALO_ID
    LEFT JOIN EQTLINFO_RAW.OPER_GO.HISTORICO_TURMA_PLANTAO HTP 
           ON INT.HIST_TURMA_PLANTAO_ID = HTP.HIST_TURMA_PLANTAO_ID
    LEFT JOIN EQTLINFO_RAW.OPER_GO.PREFIXO_TURMA PT 
           ON HTP.PREFIXO_TURMA_ID = PT.PREFIXO_TURMA_ID 
    
    WHERE INT.MOT_INTERVALO_ID = 1
      AND TO_CHAR(TRUNC(INT.INT_DATA_INICIO,'MM'), 'YYYYMM') = {{ get_month_ref() }}
      AND DATE(INT.INT_DATA_INICIO) <= DATEADD(DAY, -1, CURRENT_DATE)
),
CTE_INTERVALOS AS (

    SELECT 
        CURRENT_TIMESTAMP AS DATA_ETL,
        'EQTL_GO' AS ORIGEM,
        INTR.INTERVALO_ID,
        INTP.DATA_INTERVALO,
        INTP.PREFIXO,
        INTP.DURACAO_PREV_HORAS,
        INTR.INT_DATA_INICIO AS INI_REAL,
        INTR.INT_DATA_FINAL AS FIM_REAL,
        DATEDIFF(SECOND, INTR.INT_DATA_INICIO, INTR.INT_DATA_FINAL) / 3600.0 AS DURACAO_REAL_HORAS,

        ROW_NUMBER() OVER (
            PARTITION BY INTP.PREFIXO, INTP.DATA_INTERVALO 
            ORDER BY INTR.INT_DATA_INICIO
        ) AS RN

    FROM CTE_INTERVALO_PREV INTP
    LEFT JOIN CTE_INTERVALO_REAL INTR 
           ON INTP.PREFIXO = INTR.PREFIXO
          AND INTP.DATA_INTERVALO = DATE_TRUNC('DAY', INTR.INT_DATA_INICIO)
)

SELECT 
    DATA_ETL,
    ORIGEM,
    INTERVALO_ID,
    DATA_INTERVALO,
    PREFIXO,

    CASE
        WHEN RN > 1 THEN NULL
        ELSE DURACAO_PREV_HORAS
    END AS DURACAO_PREV_HORAS,

    INI_REAL,
    FIM_REAL,
    DURACAO_REAL_HORAS,
    RN AS ORDEM_NO_DIA

FROM CTE_INTERVALOS