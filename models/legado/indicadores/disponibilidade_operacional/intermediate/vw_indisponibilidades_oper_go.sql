SELECT 
    CURRENT_TIMESTAMP AS DATA_ETL,
    'EQTL_GO' AS ORIGEM,
    INT.INTERVALO_ID,
    DATE_TRUNC('DAY', INT_DATA_INICIO) AS DATA_INDISPONIBILIDADE,

    CASE
        WHEN SUBSTR(REGEXP_REPLACE(UPPER(TRIM(PT.PRX_DESCRICAO)), '[^A-Z0-9]', ''), 1, 2) IN ('MA','PA','PI','AL','AP','GO','RS')
             AND LEN(REGEXP_REPLACE(UPPER(TRIM(PT.PRX_DESCRICAO)), '[^A-Z0-9]', '')) = 10
        THEN SUBSTR(
                REGEXP_REPLACE(UPPER(TRIM(PT.PRX_DESCRICAO)), '[^A-Z0-9]', ''), 
                LEN(REGEXP_REPLACE(UPPER(TRIM(PT.PRX_DESCRICAO)), '[^A-Z0-9]', '')) - 7, 
                8
             )
        ELSE REGEXP_REPLACE(UPPER(TRIM(PT.PRX_DESCRICAO)), '[^A-Z0-9]', '')
    END AS PREFIXO,

    TRIM(MI.MOT_DESCRICAO) AS TIPO,
    INT_DATA_INICIO AS INI_REAL,
    INT_DATA_FINAL AS FIM_REAL,

    DATEDIFF('second', INT_DATA_INICIO, INT_DATA_FINAL) / 3600.0 AS DURACAO_REAL_HORAS,

    NULL AS TIPO_DE_INDISPONIBILIDADE
    
FROM EQTLINFO_RAW.OPER_GO.INTERVALO INT
LEFT JOIN EQTLINFO_PRD.EQTL_GO.MOTIVO_INTERVALO MI 
       ON INT.MOT_INTERVALO_ID = MI.MOT_INTERVALO_ID
LEFT JOIN EQTLINFO_RAW.OPER_GO.HISTORICO_TURMA_PLANTAO HTP 
       ON INT.HIST_TURMA_PLANTAO_ID = HTP.HIST_TURMA_PLANTAO_ID
LEFT JOIN EQTLINFO_RAW.OPER_GO.PREFIXO_TURMA PT 
       ON HTP.PREFIXO_TURMA_ID = PT.PREFIXO_TURMA_ID 

WHERE INT.MOT_INTERVALO_ID = 2
  AND TO_VARCHAR(DATE_TRUNC('DAY', INT.INT_DATA_INICIO), 'YYYYMM') = {{ get_month_ref() }}
  AND DATE_TRUNC('DAY', INT.INT_DATA_INICIO) <= DATE_TRUNC('DAY', CURRENT_DATE) - 1