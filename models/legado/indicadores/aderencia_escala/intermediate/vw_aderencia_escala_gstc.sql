SELECT ae.*, eqp.REGIONAL, eqp.POLO, eqp.MUNICIPIO, eqp.FONECEDOR FORNECEDOR, eqp.SERV_CAMPO, eqp.PROCESSO, eqp.SERV_CONTRATO, eqp.TIPO_EQP, eqp.SUBTIPO_EQP, eqp.TIPO_EQP_CONTRATO
FROM {{ ref('aderencia_escala') }} ae
LEFT JOIN (
	SELECT eq.*,
	ROW_NUMBER() OVER (PARTITION BY DISTRIBUIDORA, eq.PREFIXO ORDER BY VIGENCIA_INICIAL_EQP DESC ) AS RN,
	CASE
	    WHEN POSITION('-' IN eq.PREFIXO) > 0 
	         AND SUBSTRING(eq.PREFIXO, 1, POSITION('-' IN eq.PREFIXO) - 1) 
	             IN ('AP','AL','PI','MA','PA','GO','RS') 
	    THEN REPLACE(SUBSTRING(eq.PREFIXO, POSITION('-' IN eq.PREFIXO) + 1), '-', '')
	    ELSE REPLACE(eq.PREFIXO, '-', '')
	END AS PREFIXO_UNICO
	FROM {{ ref('gp_gstc_equipes2021') }} eq
	WHERE eq.STATUS = 'ATIVA'
) eqp ON eqp.PREFIXO_UNICO = ae.PREFIXO_UNICO AND eqp.DISTRIBUIDORA = ae.ORIGEM AND eqp.RN = 1
WHERE TO_CHAR(TRUNC(DATA,'MM'),'YYYYMM') = {{ get_month_ref() }}