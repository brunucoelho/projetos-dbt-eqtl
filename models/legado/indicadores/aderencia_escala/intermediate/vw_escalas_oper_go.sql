WITH SIGA AS(
SELECT
	ROW_NUMBER() OVER (ORDER BY NULL) AS UID_ESCALA,
	PRT.PRX_DESCRICAO AS PREFIXO,
	ECL.ESC_DATA::DATE AS DT_ROUTE,
    TO_TIMESTAMP_NTZ(TO_CHAR(ECL.ESC_DATA, 'YYYY-MM-DD') || ' ' || TO_CHAR(POS.POS_TURNO_INICIO, 'HH24:MI:SS'),'YYYY-MM-DD HH24:MI:SS') AS HORARIO_INICIO_ESCALA,
    TO_TIMESTAMP_NTZ(
    TO_CHAR(
        DATEADD(
            day,
            DATEDIFF(day, POS.POS_TURNO_INICIO, POS.POS_TURNO_FIM),
            ECL.ESC_DATA
        ),
        'YYYY-MM-DD'
    ) || ' ' || TO_CHAR(POS.POS_TURNO_FIM, 'HH24:MI:SS'),
    'YYYY-MM-DD HH24:MI:SS'
) AS HORARIO_FIM_ESCALA,
COALESCE(PRT.PRX_NOME_INTERNO, PRT.PRX_DESCRICAO) PREFIXO_PADRAO
       
FROM EQTLINFO_RAW.EQTL_GO.ESCALA ECL
LEFT JOIN EQTLINFO_RAW.OPER_GO.POSICAO POS ON POS.POS_CODIGO = ECL.POS_CODIGO
LEFT JOIN EQTLINFO_RAW.OPER_GO.PREFIXO_TURMA PRT ON PRT.PREFIXO_TURMA_ID = ECL.PREFIXO_TURMA_ID

WHERE TO_CHAR(ECL.ESC_DATA::DATE,'YYYYMM') = {{ get_month_ref() }}
  AND ECL.ESC_DATA::DATE <= CURRENT_TIMESTAMP::DATE -1
  AND POS.POS_CODIGO NOT IN ('X', 'SB', 'FE', 'IND', 'RES') 
--WHEN 'X' THEN 'FOLGA'
 --WHEN 'P' THEN 'PTP'
 --WHEN 'RES' THEN 'RESERVA'
 --WHEN 'SB' THEN 'SOBREAVISO'
 --WHEN 'FE' THEN 'FORA ESCALA'
 --WHEN 'IND' THEN 'INDISPONIVEL'	
),

OPER AS(

SELECT DISTINCT
	HTP.HIST_TURMA_PLANTAO_ID,
	HTP.HTP_INICIO_TURNO,
	HTP.HTP_FIM_TURNO,
    P.PRX_DESCRICAO,
    CASE
        WHEN SUBSTR(REGEXP_REPLACE(UPPER(TRIM(P.PRX_DESCRICAO)), '[^A-Z0-9]', ''), 1, 2) IN ('MA','PA','PI','AL','AP','GO','RS')
            AND LENGTH(REGEXP_REPLACE(UPPER(TRIM(P.PRX_DESCRICAO)), '[^A-Z0-9]', '')) = 10
            THEN SUBSTR(REGEXP_REPLACE(UPPER(TRIM(P.PRX_DESCRICAO)), '[^A-Z0-9]', ''), LENGTH(REGEXP_REPLACE(UPPER(TRIM(P.PRX_DESCRICAO)), '[^A-Z0-9]', '')) - 7, 8)
        ELSE REGEXP_REPLACE(UPPER(TRIM(P.PRX_DESCRICAO)), '[^A-Z0-9]', '')
    END AS PREFIXO,
    COALESCE(P.PRX_NOME_INTERNO, P.PRX_DESCRICAO) PREFIXO_PADRAO
FROM EQTLINFO_RAW.OPER_GO.HISTORICO_TURMA_PLANTAO HTP
LEFT JOIN EQTLINFO_RAW.OPER_GO.PREFIXO_TURMA p ON p.PREFIXO_TURMA_ID = htp.PREFIXO_TURMA_ID
WHERE TO_CHAR(HTP_INICIO_TURNO::DATE,'YYYYMM') = {{ get_month_ref() }}
	AND HTP_INICIO_TURNO <= CURRENT_TIMESTAMP::DATE -1

), 

ADERENCIA AS(
SELECT 
SI.UID_ESCALA,
SI.PREFIXO AS PREFIXO_SIGA,
SI.PREFIXO_PADRAO PRX_PADRAO_SIGA,
OP.PREFIXO_PADRAO PRX_PADRAO_OPER,

CASE
	WHEN SI.DT_ROUTE IS NULL THEN OP.HTP_INICIO_TURNO::DATE
	WHEN SI.DT_ROUTE IS NOT NULL THEN SI.DT_ROUTE
	ELSE NULL
END AS DATA,

--SI.DT_ROUTE AS DATA,
SI.HORARIO_INICIO_ESCALA,
SI.HORARIO_FIM_ESCALA,

OP.HIST_TURMA_PLANTAO_ID,
OP.PREFIXO AS PREFIXO_OPER,
OP.HTP_INICIO_TURNO,
OP.HTP_FIM_TURNO,
--OP.REGIONAL,
--OP.POLO,
--OP.BASE,
--OP.ELETRICISTAS,

CASE
	WHEN (SI.DT_ROUTE IS NULL
	OR SI.HORARIO_INICIO_ESCALA IS NULL)
	AND SI.UID_ESCALA IS NOT NULL 
        THEN 'ESCALA INCORRETA'
	WHEN SI.UID_ESCALA IS NULL
	AND OP.HIST_TURMA_PLANTAO_ID IS NOT NULL 
        THEN 'TURNO SEM ESCALA'
	WHEN ROW_NUMBER() OVER (
            PARTITION BY SI.UID_ESCALA
ORDER BY
	ABS(DATEDIFF(SECOND, OP.HTP_INICIO_TURNO, SI.HORARIO_INICIO_ESCALA))
         ) > 1 
        THEN 'EXTRA'
	WHEN HTP_INICIO_TURNO IS NULL 
        THEN 'AUSENTE'
	WHEN OP.HTP_INICIO_TURNO > (SI.HORARIO_INICIO_ESCALA + INTERVAL '30 MINUTE') 
        THEN 'ATRASADO'
	WHEN OP.HTP_INICIO_TURNO < (SI.HORARIO_INICIO_ESCALA - INTERVAL '30 MINUTE') 
        THEN 'ADIANTADO'
	WHEN OP.HTP_INICIO_TURNO BETWEEN (SI.HORARIO_INICIO_ESCALA - INTERVAL '30 MINUTE') 
                                  AND (SI.HORARIO_INICIO_ESCALA + INTERVAL '30 MINUTE')
        THEN 'ADERENTE'
	ELSE NULL
END AS ESTADO,

DATEDIFF(minute, SI.HORARIO_INICIO_ESCALA, OP.HTP_INICIO_TURNO) AS ATRASO_MINUTOS,

ROW_NUMBER() OVER (
    PARTITION BY SI.UID_ESCALA 
    ORDER BY ABS(DATEDIFF(minute, SI.HORARIO_INICIO_ESCALA, OP.HTP_INICIO_TURNO))
) AS RN

FROM SIGA SI
FULL OUTER JOIN OPER OP 
	ON OP.PREFIXO = SI.PREFIXO
	AND OP.HTP_INICIO_TURNO >= DATEADD(hour, -8, SI.HORARIO_INICIO_ESCALA)
	AND OP.HTP_INICIO_TURNO <= DATEADD(hour,  8, SI.HORARIO_INICIO_ESCALA)

)

SELECT A.*,
EXTRACT (YEAR FROM DATA) ANO,
EXTRACT (MONTH FROM DATA) MES,
EXTRACT (DAY FROM DATA) DIA,
TO_VARCHAR(DATA,'YYYYMM') COMPETENCIA,
sec.CONTRATO CONTRATO_SAP
FROM (
SELECT 
	CURRENT_TIMESTAMP AS DATA_ETL,
	'EQTL_GO' AS ORIGEM,
	CASE
		WHEN PREFIXO_SIGA IS NULL THEN PREFIXO_OPER
		ELSE PREFIXO_SIGA
	END AS PREFIXO_UNICO,
	ADE.DATA::TIMESTAMP DATA,
	ADE.HORARIO_INICIO_ESCALA,
	ADE.HORARIO_FIM_ESCALA,
	TO_CHAR(ADE.HORARIO_INICIO_ESCALA, 'HH24:MI')||' - '||TO_CHAR(ADE.HORARIO_FIM_ESCALA, 'HH24:MI') AS TIPO_ESCALA,	
	ADE.HIST_TURMA_PLANTAO_ID,
	--ADE.ELETRICISTAS AS INTEGRANTES,
	ADE.HTP_INICIO_TURNO,
	ADE.HTP_FIM_TURNO,
	--ADE.UID_ESCALA,
	ADE.ESTADO,
	ADE.ATRASO_MINUTOS,
	CASE
		WHEN PRX_PADRAO_SIGA IS NULL THEN PRX_PADRAO_OPER
		ELSE PRX_PADRAO_SIGA
	END AS PREFIXO_PADRAO
FROM ADERENCIA ADE
) A
JOIN SB_PERFORMANCE.SILVER.GLOSSARIO_EMPRESAS ge ON ge.SIGLA_EMPRESA = a.ORIGEM
LEFT JOIN {{ ref('slv_equipe_contrato') }} sec ON
	sec.COD_EMPRESA = ge.COD_EMPRESA AND
	sec.ANO = EXTRACT(YEAR FROM a."DATA") AND
	sec.MES = EXTRACT(MONTH FROM a."DATA") AND
	sec.PREFIXO = a.PREFIXO_PADRAO