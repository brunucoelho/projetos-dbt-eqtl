WITH SIGA AS (
  SELECT 
    UUID_STRING() AS UID_ESCALA,  -- SYS_GUID() equivalent in Snowflake
    ROT.*,
    DATEADD(hour, -3, ROT.DT_WORKINGDAYSTART) AS HORARIO_INICIO_ESCALA,
    DATEADD(hour, -3, ROT.DT_WORKINGDAYEND) AS HORARIO_FIM_ESCALA,
    DATEADD(hour, -3, ROT.DT_ROUTEACTIVATIONTIME) AS HTP_INICIO_TURNO,
    DATEADD(hour, -3, COALESCE(ROT.DT_ROUTEDEACTIVATIONTIME, ROT.DT_ROUTEREACTIVATIONTIME)) AS HTP_FIM_TURNO,

    CASE
      WHEN SUBSTR(REPLACE(SUBSTR(RES.TX_EXTERNALID, 4), '-', ''), 1, 2) IN ('AP','AL','PI','MA','PA','GO','RS')
        THEN SUBSTR(REPLACE(SUBSTR(RES.TX_EXTERNALID, 4), '-', ''), 3)
      ELSE REPLACE(SUBSTR(RES.TX_EXTERNALID, 4), '-', '')
    END AS PREFIXO

  FROM EQTLINFO_RAW.SIGA.TB_OFSC_ROTA ROT
  INNER JOIN EQTLINFO_RAW.SIGA.TB_OFSC_RESOURCES_RT RES
    ON RES.TX_EXTERNALID = ROT.TX_RESOURCEEXTERNALID

  WHERE TO_VARCHAR(ROT.DT_ROUTE, 'YYYYMM') = {{ get_month_ref() }}
    AND RES.TX_TYPE IN ('Prefixo','PrefixoManutencao')
),

ADERENCIA AS (
  SELECT 
    SI.UID_ESCALA,
    SI.PREFIXO AS PREFIXO_SIGA,
    SI.TX_RESOURCEEXTERNALID,

    CASE
      WHEN SI.DT_ROUTE IS NULL THEN DATE_TRUNC('day', SI.HTP_INICIO_TURNO)
      WHEN SI.DT_ROUTE IS NOT NULL THEN SI.DT_ROUTE
      ELSE NULL
    END AS DATA,

    SI.HORARIO_INICIO_ESCALA,
    SI.HORARIO_FIM_ESCALA,

    NULL::VARCHAR AS HIST_TURMA_PLANTAO_ID,
    SI.HTP_INICIO_TURNO,
    SI.HTP_FIM_TURNO,

    CASE
      WHEN (SI.DT_ROUTE IS NULL OR SI.HORARIO_INICIO_ESCALA IS NULL) AND SI.UID_ESCALA IS NOT NULL THEN 'ESCALA INCORRETA'
      WHEN ROW_NUMBER() OVER (PARTITION BY SI.UID_ESCALA ORDER BY ABS(DATEDIFF(second, SI.HTP_INICIO_TURNO, SI.HORARIO_INICIO_ESCALA))) > 1 THEN 'EXTRA'
      WHEN SI.HTP_INICIO_TURNO IS NULL THEN 'AUSENTE'
      WHEN SI.HTP_INICIO_TURNO > DATEADD(minute, 30, SI.HORARIO_INICIO_ESCALA) THEN 'ATRASADO'
      WHEN SI.HTP_INICIO_TURNO < DATEADD(minute, -30, SI.HORARIO_INICIO_ESCALA) THEN 'ADIANTADO'
      WHEN SI.HTP_INICIO_TURNO <= DATEADD(minute, 30, SI.HORARIO_INICIO_ESCALA)
           AND SI.HTP_INICIO_TURNO >= DATEADD(minute, -30, SI.HORARIO_INICIO_ESCALA) THEN 'ADERENTE'
      ELSE NULL
    END AS ESTADO,

    DATEDIFF(second, SI.HORARIO_INICIO_ESCALA, SI.HTP_INICIO_TURNO) / 60.0 AS ATRASO_MINUTOS,

    ROW_NUMBER() OVER (PARTITION BY SI.UID_ESCALA ORDER BY ABS(DATEDIFF(second, SI.HTP_INICIO_TURNO, SI.HORARIO_INICIO_ESCALA))) AS RN

  FROM SIGA SI
)

SELECT A.*,
EXTRACT (YEAR FROM DATA) ANO,
EXTRACT (MONTH FROM DATA) MES,
EXTRACT (DAY FROM DATA) DIA,
TO_VARCHAR(DATA,'YYYYMM') COMPETENCIA,
sec.CONTRATO CONTRATO_SAP
FROM (
SELECT A.*,
CASE
  WHEN PREFIXO_UNICO IS NULL THEN NULL
  WHEN LENGTH(PREFIXO_UNICO) = 6 THEN PREFIXO_UNICO
  WHEN LENGTH(PREFIXO_UNICO) = 7 AND ORIGEM != 'EQTL_GO' THEN SUBSTR(PREFIXO_UNICO, 1, 3) || '-' || SUBSTR(PREFIXO_UNICO, 4)
  WHEN LENGTH(PREFIXO_UNICO) = 7 AND ORIGEM = 'EQTL_GO' THEN PREFIXO_UNICO
  WHEN LENGTH(PREFIXO_UNICO) = 8 THEN SUBSTR(ORIGEM, -2) || '-' || SUBSTR(PREFIXO_UNICO, 1, 3) || '-' || SUBSTR(PREFIXO_UNICO, 4)
  ELSE PREFIXO_UNICO
END AS PREFIXO_PADRAO
FROM (
SELECT 
  CURRENT_TIMESTAMP() AS DATA_ETL,
  
  CASE
	WHEN LEFT(ADE.TX_RESOURCEEXTERNALID,3) = 'AP_' THEN 'EQTL_AP'
  	WHEN LEFT(ADE.TX_RESOURCEEXTERNALID,3) = 'AL_' THEN 'EQTL_AL'
  	WHEN LEFT(ADE.TX_RESOURCEEXTERNALID,3) = 'MA_' THEN 'EQTL_MA'
  	WHEN LEFT(ADE.TX_RESOURCEEXTERNALID,3) = 'PA_' THEN 'EQTL_PA'
  	WHEN LEFT(ADE.TX_RESOURCEEXTERNALID,3) = 'PI_' THEN 'EQTL_PI'
  	WHEN LEFT(ADE.TX_RESOURCEEXTERNALID,3) = 'RS_' THEN 'EQTL_RS'
  END AS ORIGEM,
  PREFIXO_SIGA AS PREFIXO_UNICO,
  ADE.DATA,
  ADE.HORARIO_INICIO_ESCALA,
  ADE.HORARIO_FIM_ESCALA,
  TO_VARCHAR(ADE.HORARIO_INICIO_ESCALA, 'HH24:MI') || ' - ' || TO_VARCHAR(ADE.HORARIO_FIM_ESCALA, 'HH24:MI') AS TIPO_ESCALA,
  ADE.HIST_TURMA_PLANTAO_ID,
  ADE.HTP_INICIO_TURNO,
  ADE.HTP_FIM_TURNO,
  ADE.ESTADO,
  ADE.ATRASO_MINUTOS
FROM ADERENCIA ADE
) A
) A
JOIN SB_PERFORMANCE.SILVER.GLOSSARIO_EMPRESAS ge ON ge.SIGLA_EMPRESA = a.ORIGEM
LEFT JOIN {{ ref('slv_equipe_contrato') }} sec ON
	sec.COD_EMPRESA = ge.COD_EMPRESA AND
	sec.ANO = EXTRACT(YEAR FROM a."DATA") AND
	sec.MES = EXTRACT(MONTH FROM a."DATA") AND
	sec.PREFIXO = a.PREFIXO_PADRAO