WITH ESCALA AS (
SELECT
	UUID_STRING() AS UID_ESCALA,
	rot.TX_RESOURCEEXTERNALID PREFIXO_ESCALA,
	DATEADD(HOUR, -3, ROT.DT_WORKINGDAYSTART) AS HORARIO_INICIO_ESCALA,
	DATEADD(HOUR, -3, ROT.DT_WORKINGDAYEND) AS HORARIO_FIM_ESCALA,
	DATEADD(HOUR, -3, ROT.DT_ROUTEACTIVATIONTIME) AS HTP_INICIO_TURNO,
	DATEADD(HOUR, -3, ROT.DT_ROUTEREACTIVATIONTIME) AS REATIVACAO,
	DATEADD(HOUR, -3, COALESCE(ROT.DT_ROUTEDEACTIVATIONTIME, ROT.DT_ROUTEREACTIVATIONTIME)) AS HTP_FIM_TURNO
FROM
	EQTLINFO_RAW.SIGA.TB_OFSC_ROTA_RT rot
WHERE
	TRUNC(DT_ROUTE, 'MM') = TO_DATE({{ get_month_ref() }},'YYYYMM')

),

TURNO AS (
SELECT
	htp.ID_INICIO_DE_TURNO,
	TRY_TO_TIMESTAMP(htp.DATA_DE_INICIO_DO_TURNO, 'YYYY-MM-DD HH24:MI:SS') DATA_DE_INICIO_DO_TURNO,
	htp.ID_EXTERNO_DO_RECURSO PREFIXO_TURNO,
	CASE WHEN htp.RECURSO_COM_TURNO_INICIADO = 2 THEN htp.TIMESTAMP END FIM_TURNO
FROM
	EQTLINFO_RAW.SIGA.TB_OFSC_HISTORICO_TURNO htp
WHERE
	TRUNC(TO_DATE(DATA_DE_INICIO_DO_TURNO, 'YYYY-MM-DD HH24:MI:SS'), 'MM') = TO_DATE({{ get_month_ref() }},'YYYYMM')
),

ESCALA_TURNO AS (
SELECT
	esc.UID_ESCALA,
	esc.HTP_FIM_TURNO HTP_FIM_TURNO_ESCALA,
	CURRENT_TIMESTAMP DATA_ETL,
	DECODE(LEFT(NVL(esc.PREFIXO_ESCALA, t.PREFIXO_TURNO), 2), 'AL', 'EQTL_AL', 'AP', 'EQTL_AP', 'MA', 'EQTL_MA', 'PA', 'EQTL_PA', 'PI', 'EQTL_PI', 'GO', 'EQTL_GO', 'RS', 'EQTL_RS') ORIGEM,
	SPLIT_PART(NVL(esc.PREFIXO_ESCALA, t.PREFIXO_TURNO), '_', 2) PREFIXO,
	NVL(t.DATA_DE_INICIO_DO_TURNO, esc.HORARIO_INICIO_ESCALA)::DATE::TIMESTAMP DATA,
	esc.HORARIO_INICIO_ESCALA,
	esc.HORARIO_FIM_ESCALA,
	TO_VARCHAR(esc.HORARIO_INICIO_ESCALA, 'HH24:MI') || ' - ' || TO_VARCHAR(esc.HORARIO_FIM_ESCALA, 'HH24:MI') AS TIPO_ESCALA,
	t.ID_INICIO_DE_TURNO HIST_TURMA_PLANTAO_ID,
	t.DATA_DE_INICIO_DO_TURNO HTP_INICIO_TURNO,
	t.FIM_TURNO HTP_FIM_TURNO
FROM
	TURNO t
FULL OUTER JOIN ESCALA esc ON
	esc.PREFIXO_ESCALA = t.PREFIXO_TURNO
	AND t.DATA_DE_INICIO_DO_TURNO >= DATEADD(HOUR, -8, esc.HORARIO_INICIO_ESCALA)
		AND t.DATA_DE_INICIO_DO_TURNO <= DATEADD(HOUR, 8, esc.HORARIO_INICIO_ESCALA)
),

ADERENCIA_ESCALA AS (
SELECT
	A.*,
	CASE
		WHEN (A.DATA IS NULL
			OR A.HORARIO_INICIO_ESCALA IS NULL)
		AND A.UID_ESCALA IS NOT NULL THEN 'ESCALA INCORRETA'
		WHEN A.UID_ESCALA IS NULL
		AND HIST_TURMA_PLANTAO_ID IS NOT NULL THEN 'TURNO SEM ESCALA'
		WHEN ROW_NUMBER() OVER (PARTITION BY A.UID_ESCALA
	ORDER BY
		ABS(DATEDIFF(SECOND, A.HTP_INICIO_TURNO, A.HORARIO_INICIO_ESCALA))) > 1 THEN 'EXTRA'
		WHEN A.HTP_INICIO_TURNO IS NULL THEN 'AUSENTE'
		WHEN A.HTP_INICIO_TURNO > DATEADD(minute, 30, A.HORARIO_INICIO_ESCALA) THEN 'ATRASADO'
		WHEN A.HTP_INICIO_TURNO < DATEADD(minute, -30, A.HORARIO_INICIO_ESCALA) THEN 'ADIANTADO'
		WHEN A.HTP_INICIO_TURNO <=  DATEADD(minute, 30, A.HORARIO_INICIO_ESCALA)
		AND A.HTP_INICIO_TURNO >=  DATEADD(minute, -30, A.HORARIO_INICIO_ESCALA) THEN 'ADERENTE'
		ELSE NULL
	END AS ESTADO,
FROM
	ESCALA_TURNO A
ORDER BY
	DATA
)

SELECT 
A.DATA_ETL,
A.ORIGEM,
REGEXP_REPLACE(
        REGEXP_REPLACE(UPPER(A.PREFIXO), '^(MA-|AL-|AP-|RS-|PI-|PA-|GO-)', ''), 
        '[^A-Z0-9]', 
        ''
    ) PREFIXO_UNICO,
A.DATA,
A.HORARIO_INICIO_ESCALA,
A.HORARIO_FIM_ESCALA,
A.TIPO_ESCALA,
A.HIST_TURMA_PLANTAO_ID,
A.HTP_INICIO_TURNO,
A.HTP_FIM_TURNO,
A.ESTADO,
DATEDIFF(second, A.HORARIO_INICIO_ESCALA, A.HTP_INICIO_TURNO) / 60.0 ATRASO_MINUTOS,
A.PREFIXO PREFIXO_PADRAO,
EXTRACT (YEAR FROM A.DATA) ANO,
EXTRACT (MONTH FROM A.DATA) MES,
EXTRACT (DAY FROM A.DATA) DIA,
TO_CHAR(A.DATA,'YYYYMM') COMPETENCIA,
sec.CONTRATO CONTRATO_SAP
FROM ADERENCIA_ESCALA A
JOIN SB_PERFORMANCE.SILVER.GLOSSARIO_EMPRESAS ge ON ge.SIGLA_EMPRESA = a.ORIGEM
LEFT JOIN {{ ref('slv_equipe_contrato') }} sec ON
	sec.COD_EMPRESA = ge.COD_EMPRESA AND
	sec.ANO = EXTRACT(YEAR FROM a."DATA") AND
	sec.MES = EXTRACT(MONTH FROM a."DATA") AND
	sec.PREFIXO = a.PREFIXO
WHERE a.DATA IS NOT NULL