SELECT *
FROM
  (SELECT 
  		CURRENT_TIMESTAMP DATA_ETL,
  		'EQTL_GO' EMPRESA,
                    a.*
   FROM
     (SELECT c.*,
             CASE
                 WHEN OS_ANTERIOR IS NOT NULL
                      AND IMPROCEDENTE <> 1
                      AND IMPROCEDENTE_R <> 1 THEN 1
                 ELSE 0
             END REINCIDENTE,
        CASE WHEN OS_ANTERIOR IS NOT NULL THEN 1
			       ELSE 0
        END REIN_COM_IMP
      FROM
        (SELECT a.*,
                ROW_NUMBER() OVER (PARTITION BY INSTALACAO, ID_INTERNO
                                   ORDER BY DT_CONCLUSAO_R DESC) RN
         FROM
           (SELECT *
FROM (
		SELECT 
		o.OCORRENCIA_ID ID_INTERNO,
		(TO_VARCHAR(O.OCO_NUMERO::INT) || '-' || TO_VARCHAR(O.OCO_MES::INT) || '-' || TO_VARCHAR(O.OCO_ANO::INT)) OS,
		o.TB_TP_ABRANGENCIA ABRANGENCIA,
		TO_CHAR(o.CR_NUMERO) INSTALACAO,
		NVL(PT.PRX_NOME_INTERNO,pt.PRX_DESCRICAO) PREFIXO,
		hot.HOT_VI_DATA_CONCLUSAO DT_CONCLUSAO,
		tdc.TCA_DESCRICAO CAUSA,
		CASE 
			WHEN tdc.TB_CAUSA_ID IN (83, 14, 22, 23, 39, 56, 84, 85, 11, 34, 82) THEN 1
			ELSE 0
		END IMPROCEDENTE
		FROM EQTLINFO_RAW.OPER_GO.HISTORICO_OCORRENCIA_TURMA hot
		LEFT JOIN EQTLINFO_RAW.OPER_GO.OCORRENCIA o ON o.OCORRENCIA_ID = hot.OCORRENCIA_ID 
		LEFT JOIN EQTLINFO_RAW.OPER_GO.HISTORICO_TURMA_PLANTAO htp ON htp.HIST_TURMA_PLANTAO_ID = hot.HIST_TURMA_PLANTAO_ID 
		LEFT JOIN EQTLINFO_RAW.OPER_GO.PREFIXO_TURMA pt ON pt.PREFIXO_TURMA_ID = htp.PREFIXO_TURMA_ID 
		LEFT JOIN EQTLINFO_RAW.OPER_GO.TIPO_DE_CAUSA tdc ON tdc.TB_CAUSA_ID = o.TB_CAUSA_ID 
		WHERE hot.HOT_VI_STATUS = 4
		AND o.TB_TP_ABRANGENCIA = 'CR'
		AND o.TB_NATUREZA_ID IN (3,1)
		AND TRUNC(HOT_VI_DATA_CONCLUSAO,'MM') = TO_DATE({{ get_month_ref() }},'YYYYMM')
	) a
	LEFT JOIN (
		SELECT 
		o.OCORRENCIA_ID ID_INTERNO_R,
		(TO_VARCHAR(O.OCO_NUMERO::INT) || '-' || TO_VARCHAR(O.OCO_MES::INT) || '-' || TO_VARCHAR(O.OCO_ANO::INT)) OS_ANTERIOR,
		NVL(PT.PRX_NOME_INTERNO,pt.PRX_DESCRICAO) PREFIXO_ANTERIOR,
		TO_CHAR(o.CR_NUMERO) INSTALACAO_R,
		hot.HOT_VI_DATA_CONCLUSAO DT_CONCLUSAO_R,
		tdc.TCA_DESCRICAO CAUSA_R,
		CASE 
			WHEN tdc.TB_CAUSA_ID IN (83, 14, 22, 23, 39, 56, 84, 85, 11, 34, 82) THEN 1
			ELSE 0
		END IMPROCEDENTE_R
		FROM EQTLINFO_RAW.OPER_GO.HISTORICO_OCORRENCIA_TURMA hot
		LEFT JOIN EQTLINFO_RAW.OPER_GO.OCORRENCIA o ON o.OCORRENCIA_ID = hot.OCORRENCIA_ID 
		LEFT JOIN EQTLINFO_RAW.OPER_GO.HISTORICO_TURMA_PLANTAO htp ON htp.HIST_TURMA_PLANTAO_ID = hot.HIST_TURMA_PLANTAO_ID 
		LEFT JOIN EQTLINFO_RAW.OPER_GO.PREFIXO_TURMA pt ON pt.PREFIXO_TURMA_ID = htp.PREFIXO_TURMA_ID 
		LEFT JOIN EQTLINFO_RAW.OPER_GO.TIPO_DE_CAUSA tdc ON tdc.TB_CAUSA_ID = o.TB_CAUSA_ID 
		WHERE hot.HOT_VI_STATUS = 4
		AND o.TB_TP_ABRANGENCIA = 'CR'
		AND o.TB_NATUREZA_ID IN (3,1)
		AND HOT_VI_DATA_CONCLUSAO::DATE >= DATEADD(DAY, -91, TO_DATE({{ get_month_ref() }},'YYYYMM')) AND TRUNC(HOT_VI_DATA_CONCLUSAO,'MM') <= TO_DATE({{ get_month_ref() }},'YYYYMM')
	) b ON a.INSTALACAO = b.INSTALACAO_R
            AND b.DT_CONCLUSAO_R >= DATEADD(DAY, -90, a.DT_CONCLUSAO)
            AND b.DT_CONCLUSAO_R < (a.DT_CONCLUSAO)
            AND a.ID_INTERNO != b.ID_INTERNO_R) a) c
      WHERE RN = 1) A)