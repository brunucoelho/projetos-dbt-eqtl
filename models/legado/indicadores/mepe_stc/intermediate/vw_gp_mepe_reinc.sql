SELECT
	TUDO.*,
	(TUDO.PERCENTUAL / TUDO.META)* 100 ATINGIMENTO,
	CASE
		WHEN (TUDO.PERCENTUAL / TUDO.META)* 100 >= 150 THEN 0
		WHEN (TUDO.PERCENTUAL / TUDO.META)* 100 >= 130 THEN 30
		WHEN (TUDO.PERCENTUAL / TUDO.META)* 100 > 110 THEN 70
		WHEN (TUDO.PERCENTUAL / TUDO.META)* 100 <= 110 THEN 100
	END MEPE_REINCIDENCIA,
	CASE
		WHEN (TUDO.PERCENTUAL / TUDO.META)* 100 >= 150 THEN 'D'
		WHEN (TUDO.PERCENTUAL / TUDO.META)* 100 >= 130 THEN 'C'
		WHEN (TUDO.PERCENTUAL / TUDO.META)* 100 >= 110 THEN 'B'
		WHEN (TUDO.PERCENTUAL / TUDO.META)* 100 < 110 THEN 'A'
	END CLASSE_REINCIDENCIA
FROM
	(
	SELECT
		R.*,
		T.QTD_TOTAL,
		CASE
			WHEN R.QTD_REINCIDENTES IS NULL
			OR R.QTD_REINCIDENTES = 0 THEN 0
			WHEN T.QTD_TOTAL IS NULL
			OR T.QTD_TOTAL = 0 THEN 0
			ELSE (R.QTD_REINCIDENTES / T.QTD_TOTAL)* 100
		END PERCENTUAL
	FROM
		(
		SELECT
			AA.EMPRESA,
			AA.DISTRIBUIDORA,
			AA.REGIONAL,
			AA.POLO,
			AA.PROCESSO,
			AA.SERV_CAMPO,
			MT.META * 100 META,
			X.EQUIPE,
			X.COMPETENCIA,
			SUM(X.CLIE) QTD_REINCIDENTES
		FROM
			(
			SELECT
				sub1.*,
				CASE
					WHEN EMPRESA = 'EQTL MA' THEN 'MARANHAO'
					WHEN EMPRESA = 'EQTL PA' THEN 'PARA'
					WHEN EMPRESA = 'EQTL AL' THEN 'ALAGOAS'
					WHEN EMPRESA = 'EQTL PI' THEN 'PIAUI'
					WHEN EMPRESA = 'EQTL RS' THEN 'RIO GRANDE DO SUL'
					WHEN EMPRESA = 'EQTL AP' THEN 'AMAPA'
					WHEN EMPRESA = 'EQTL GO' THEN 'GOIAS'
				END EMPRESA2,
				to_char(data_conclusao, 'YYYYMM') COMPETENCIA,
				to_char(data_conclusao, 'YYYY') ANO
			FROM
				(
				SELECT
					A.EMPRESA,
					A.REGIONAL,
					A.SECCIONAL,
					
				REGEXP_REPLACE(A.EQUIPE, '^(AL_|MA_|AP_|PA_|PI_|RS_|GO_)', '') EQUIPE,
				A.DATA,
				A.DATA_CONCLUSAO,
				A.NATUREZA,
				A.OCO_NUMERO,
				A.OCO_ID,
				A.ABRANGENCIA,
				A.PDF,
				A.TIPO_EQP,
				A.CHI,
				A.CLIE,
				A.REINCIDENTE_90_DIAS,
				A.N_REINC,
				A.DATA_DADOS,
				CASE
					WHEN reincidente_90_dias = 'S'
					AND n_reinc >= 2 THEN 'S'
					ELSE 'N'
				END reincidente
			FROM
				{{ ref('det_reinc_trafo') }} A
				WHERE TRUNC(A.DATA_CONCLUSAO,'MM') = TO_DATE({{ get_month_ref() }},'YYYYMM')
				AND A.EMPRESA <> 'EQTL_GO'
                                ) sub1 
                          ) X
		INNER JOIN {{ ref('vw_gp_gstc_equipes_sem_duplicatas') }} AA ON
			X.EMPRESA = AA.DISTRIBUIDORA
			AND X.EQUIPE = AA.PREFIXO
		LEFT JOIN (
			SELECT
				*
			FROM
				SB_PERFORMANCE.EQTL_CORP.METAS_MEPE_COM
			WHERE
				servico = 'REINCIDENCIA') MT ON
			mt.empresa = aa.empresa
			AND mt.regional = aa.regional
			AND mt.polo = aa.polo
			AND X.ANO = MT.ANO
		WHERE
			X.REINCIDENTE = 'S'
		GROUP BY
			AA.EMPRESA,
			AA.DISTRIBUIDORA,
			AA.REGIONAL,
			AA.POLO,
			AA.PROCESSO,
			AA.SERV_CAMPO,
			MT.META,
			X.EQUIPE,
			X.COMPETENCIA,
			X.REINCIDENTE_90_DIAS ) R
	LEFT JOIN (
		SELECT
			AA.EMPRESA,
			AA.DISTRIBUIDORA,
			AA.REGIONAL,
			AA.POLO,
			AA.PROCESSO,
			AA.SERV_CAMPO,
			X.EQUIPE,
			X.COMPETENCIA,
			SUM(X.CLIE) QTD_TOTAL
		FROM
			(
			SELECT
				sub1.*,
				CASE
					WHEN EMPRESA = 'EQTL MA' THEN 'MARANHAO'
					WHEN EMPRESA = 'EQTL PA' THEN 'PARA'
					WHEN EMPRESA = 'EQTL AL' THEN 'ALAGOAS'
					WHEN EMPRESA = 'EQTL PI' THEN 'PIAUI'
					WHEN EMPRESA = 'EQTL RS' THEN 'RIO GRANDE DO SUL'
					WHEN EMPRESA = 'EQTL AP' THEN 'AMAPA'
					WHEN EMPRESA = 'EQTL GO' THEN 'GOIAS'
				END EMPRESA2,
				to_char(data_conclusao, 'YYYYMM') COMPETENCIA
			FROM
				(
				SELECT
					A.EMPRESA,
					A.REGIONAL,
					A.SECCIONAL,
					REGEXP_REPLACE(A.EQUIPE, '^(AL_|MA_|AP_|PA_|PI_|RS_|GO_)', '') EQUIPE,
				A.DATA,
				A.DATA_CONCLUSAO,
				A.NATUREZA,
				A.OCO_NUMERO,
				A.OCO_ID,
				A.ABRANGENCIA,
				A.PDF,
				A.TIPO_EQP,
				A.CHI,
				A.CLIE,
				A.REINCIDENTE_90_DIAS,
				A.N_REINC,
				A.DATA_DADOS,
				CASE
					WHEN reincidente_90_dias = 'S'
						AND n_reinc >= 2 THEN 'S'
						ELSE 'N'
					END reincidente
				FROM
					{{ ref('det_reinc_trafo') }} A
					WHERE TRUNC(A.DATA_CONCLUSAO,'MM') = TO_DATE({{ get_month_ref() }},'YYYYMM')
					AND A.EMPRESA <> 'EQTL_GO'
                                          ) sub1
                                     
                                     ) X
		INNER JOIN {{ ref('vw_gp_gstc_equipes_sem_duplicatas') }} AA ON
			X.EMPRESA = AA.DISTRIBUIDORA
			AND X.EQUIPE = AA.PREFIXO
		GROUP BY
			AA.EMPRESA,
			AA.DISTRIBUIDORA,
			AA.REGIONAL,
			AA.POLO,
			AA.PROCESSO,
			AA.SERV_CAMPO,
			X.EQUIPE,
			X.COMPETENCIA ) T ON
		T.COMPETENCIA = R.COMPETENCIA
		AND T.EQUIPE = R.EQUIPE
		AND T.EMPRESA = R.EMPRESA

     ) TUDO
