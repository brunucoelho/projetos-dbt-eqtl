SELECT
	X.*,
	CASE
		WHEN X.ADERENCIA >= 100 THEN 100
		WHEN X.ADERENCIA >= 95 THEN 70
		WHEN X.ADERENCIA > 90 THEN 30
		WHEN X.ADERENCIA <= 90 THEN 0
		ELSE 0
	END MEPE_PRIM_VIS,
	CASE
		WHEN X.ADERENCIA >= 100 THEN 'A'
		WHEN X.ADERENCIA >= 95 THEN 'B'
		WHEN X.ADERENCIA > 90 THEN 'C'
		WHEN X.ADERENCIA <= 90 THEN 'D'
		ELSE 'ERRO'
	END CLASSE_MEPE_P_VISITA
FROM
	(
	SELECT
		T.EMPRESA,
		E.REGIONAL,
		E.POLO,
		E.TIPO_EQP,
		E.PROCESSO,
		E.SERV_CAMPO,
		T.PREFIXO,
		T.MES_COMPETENCIA,
		T.QTD_TOTAL,
		NVL(P.QTD_PRIM, 0) QTD_PRIM,
		CASE
			WHEN P.QTD_PRIM IS NULL THEN 100
			ELSE (P.QTD_PRIM / T.QTD_TOTAL)* 100
		END PERC,
		'90' META_PRIMEIRA,
		CASE
			WHEN P.QTD_PRIM IS NULL THEN 100
			ELSE (((P.QTD_PRIM / T.QTD_TOTAL)* 100)/ 90)* 100
		END ADERENCIA
	FROM
		(
		SELECT
			TT.EMPRESA,
			TT.PREFIXO,
			SUM(TT.QTD_TOTAL)QTD_TOTAL,
			TT.MES_COMPETENCIA
		FROM
			(
        
        SELECT  
        A.EMPRESA,
        REGEXP_REPLACE(A.EQUIPE_EXECUCAO, '^(AL_|MA_|AP_|PA_|PI_|RS_|GO_)', '') PREFIXO,
        COUNT(A.NOTA) QTD_TOTAL,
        A.MES_COMPETENCIA,
        '100' META
        FROM {{ ref('primeira_visita_global') }} A
        --LEFT JOIN OWENGCMR.EQUIPES_STC_DE_PARA C ON C.EMPRESA = A.EMPRESA AND C.NOVO = A.EQUIPE_EXECUCAO
        WHERE TO_DATE(A.MES_COMPETENCIA, 'YYYYMM') = TO_DATE({{ get_month_ref() }},'YYYYMM')
        GROUP BY 
        A.EMPRESA,
        REGEXP_REPLACE(A.EQUIPE_EXECUCAO, '^(AL_|MA_|AP_|PA_|PI_|RS_|GO_)', ''),
        A.MES_COMPETENCIA)TT
		GROUP BY
			TT.EMPRESA,
			TT.PREFIXO,
			TT.MES_COMPETENCIA) T
	LEFT JOIN (
		SELECT
			PP.EMPRESA,
			PP.PREFIXO,
			SUM(PP.QTD_PRIM)QTD_PRIM,
			PP.MES_COMPETENCIA
		FROM
			(SELECT 
                   A.EMPRESA, 
                   REGEXP_REPLACE(A.EQUIPE_EXECUCAO, '^(AL_|MA_|AP_|PA_|PI_|RS_|GO_)', '') PREFIXO,
                   COUNT(A.NOTA) QTD_PRIM,
                   A.MES_COMPETENCIA
                   FROM {{ ref('primeira_visita_global') }} A
                   --LEFT JOIN OWENGCMR.EQUIPES_STC_DE_PARA C ON C.EMPRESA = A.EMPRESA AND C.NOVO=A.EQUIPE_EXECUCAO 
                   WHERE A.PRIM_VISITA = 'S'
                   AND TO_DATE(A.MES_COMPETENCIA, 'YYYYMM') = TO_DATE({{ get_month_ref() }},'YYYYMM')
                   GROUP BY 
                   A.EMPRESA,
                   REGEXP_REPLACE(A.EQUIPE_EXECUCAO, '^(AL_|MA_|AP_|PA_|PI_|RS_|GO_)', ''), 
                   A.MES_COMPETENCIA
                   ) PP
		GROUP BY
			PP.EMPRESA,
			PP.PREFIXO,
			PP.MES_COMPETENCIA) P ON
		P.EMPRESA = T.EMPRESA
		AND P.MES_COMPETENCIA = T.MES_COMPETENCIA
		AND P.PREFIXO = T.PREFIXO
	LEFT JOIN {{ ref('vw_gp_gstc_equipes_sem_duplicatas') }} E ON
		E.PREFIXO = T.PREFIXO
		AND e.distribuidora = t.empresa
    ) X