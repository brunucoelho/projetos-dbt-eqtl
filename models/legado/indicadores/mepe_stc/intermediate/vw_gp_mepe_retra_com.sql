SELECT 

T.EMPRESA,
E.REGIONAL,
E.POLO,
E.TIPO_EQP,
E.PROCESSO,
E.SERV_CAMPO,
E.FONECEDOR FORNECEDOR,
T.COMPETENCIA,
T.EQUIPE PREFIXO,
T.QTD_TOTAL,
NVL(R.QTD_RETRA,0) QTD_RETRA,
CASE WHEN R.QTD_RETRA IS NULL THEN 0 
ELSE (NVL(R.QTD_RETRA,0)/NVL(T.QTD_TOTAL,0))*100 END PERC_RETRA,
CAST(T.META AS NUMBER) META,
CASE WHEN R.QTD_RETRA IS NULL THEN 0 ELSE (((NVL(R.QTD_RETRA,0)/NVL(T.QTD_TOTAL,0))*100/T.META)*100) END ATINGIMENTO,
CASE 
WHEN (CASE WHEN R.QTD_RETRA IS NULL THEN 0 ELSE (((R.QTD_RETRA/T.QTD_TOTAL)*100/T.META)*100)END) > 100 THEN 'D'
WHEN (CASE WHEN R.QTD_RETRA IS NULL THEN 0 ELSE (((R.QTD_RETRA/T.QTD_TOTAL)*100/T.META)*100)END) > 80 THEN 'C'
WHEN (CASE WHEN R.QTD_RETRA IS NULL THEN 0 ELSE (((R.QTD_RETRA/T.QTD_TOTAL)*100/T.META)*100)END) > 60 THEN 'B'
WHEN (CASE WHEN R.QTD_RETRA IS NULL THEN 0 ELSE (((R.QTD_RETRA/T.QTD_TOTAL)*100/T.META)*100)END) <= 60 THEN 'A'
WHEN (CASE WHEN R.QTD_RETRA IS NULL THEN 0 ELSE (((R.QTD_RETRA/T.QTD_TOTAL)*100/T.META)*100)END) is null  THEN 'A'
END CLASSE

FROM (
SELECT TOTAL.EMPRESA,TOTAL.COMPETENCIA,TOTAL.EQUIPE,COUNT(OS_OPER)QTD_TOTAL, CAST(3 AS NUMBER(10,0)) META

FROM (
SELECT
	A.EMPRESA,
	A.EQUIPE,
	A.OSTIPO_ID,
	A.TIPO_CONCLUSAO,
	to_char(A.OS_OPER) OS_OPER,
	A.COMPETENCIA
FROM
	{{ ref('serv_exec_detalhado2') }} A
WHERE
	TO_DATE(A.COMPETENCIA,'YYYYMM') = TO_DATE({{ get_month_ref() }},'YYYYMM')
      AND A.EMPRESA <> 'EQTL_GO'
	AND A.OSTIPO_ID IN ('RI', 'LG', 'DR', 'TR')
	AND A.COMPETENCIA >= '202501'
	AND A.Ch_Ups NOT IN ('CORTE IMPRODUTIVO', 'CORTE NO MEDIDOR', 'SERVICO INVALIDO', 'SERVICO IMPRODUTIVO', 'MT IMPRODUTIVA', 'EMERGENCIA IMPRODUTIVA', 'RELIGACAO IMPRODUTIVA')
)TOTAL
GROUP BY 
TOTAL.EMPRESA,TOTAL.COMPETENCIA,TOTAL.EQUIPE) T 

    LEFT JOIN  (SELECT 
                RETRA.EMPRESA, 
                RETRA.COMPETENCIA, 
                RETRA.PREFIXO_ORIGEM,
                COUNT(RETRA.QTD_RETRA) QTD_RETRA 
                
                FROM (SELECT 
                      A.EMPRESA,
                      A.COMPETENCIA,
                      COUNT(A.OS_OPER_ORIGEM) QTD_RETRA,
                      A.PREFIXO_ORIGEM, 
                      A.OSTIPO_ORIGEM 
                      FROM {{ ref('vw_retrabalho_notas_rtb') }} a 
                      WHERE A.OSTIPO_ORIGEM IN ('RI','LG','DR','TR')
                      AND A.EMPRESA <> 'EQTL_GO'
                      AND TO_DATE(A.COMPETENCIA,'YYYYMM') = TO_DATE({{ get_month_ref() }},'YYYYMM')
                      GROUP BY A.EMPRESA,A.COMPETENCIA,A.PREFIXO_ORIGEM,A.OSTIPO_ORIGEM) RETRA
                GROUP BY
                RETRA.EMPRESA, RETRA.COMPETENCIA, RETRA.PREFIXO_ORIGEM ) R ON R.COMPETENCIA = T.COMPETENCIA AND R.EMPRESA=T.EMPRESA AND R.PREFIXO_ORIGEM = T.EQUIPE
    
    LEFT JOIN {{ ref('vw_gp_gstc_equipes_sem_duplicatas') }} E ON E.DISTRIBUIDORA = T.EMPRESA AND E.PREFIXO = T.EQUIPE 