SELECT
	T.EMPRESA,
	E.REGIONAL,
	E.POLO,
	E.TIPO_EQP,
	E.PROCESSO,
	E.SERV_CAMPO,
	T.PREFIXO,
	T.COMPETENCIA,
	T.QTD_TOTAL,
	NVL(R.QTD_RETRA, 0) QTD_RETRA,
	CASE
		WHEN R.QTD_RETRA IS NULL THEN 0
		ELSE (R.QTD_RETRA / T.QTD_TOTAL)* 100
	END PERC_RETRA,
	T.META,
	(((CASE
		WHEN R.QTD_RETRA IS NULL THEN 0
		ELSE (R.QTD_RETRA / T.QTD_TOTAL)* 100
	END)/ T.META)* 100) ATINGIMENTO,
	CASE
		WHEN R.QTD_RETRA IS NULL THEN 'A'
		WHEN R.QTD_RETRA = 0 THEN 'A'
		WHEN (((CASE
			WHEN R.QTD_RETRA IS NULL THEN 0
			ELSE (R.QTD_RETRA / T.QTD_TOTAL)* 100
		END)/ T.META)* 100) > 140 THEN 'D'
		WHEN (((CASE
			WHEN R.QTD_RETRA IS NULL THEN 0
			ELSE (R.QTD_RETRA / T.QTD_TOTAL)* 100
		END)/ T.META)* 100) > 100 THEN 'C'
		WHEN (((CASE
			WHEN R.QTD_RETRA IS NULL THEN 0
			ELSE (R.QTD_RETRA / T.QTD_TOTAL)* 100
		END)/ T.META)* 100) > 85 THEN 'B'
		WHEN (((CASE
			WHEN R.QTD_RETRA IS NULL THEN 0
			ELSE (R.QTD_RETRA / T.QTD_TOTAL)* 100
		END)/ T.META)* 100) <= 85 THEN 'A'
	END CLASSE
FROM
	(
	SELECT
		TOTAL.EMPRESA,
		TOTAL.COMPETENCIA,
		TOTAL.PREFIXO,
		SUM(TOTAL.QTD_TOTAL)QTD_TOTAL,
		CASE
			WHEN TOTAL.COMPETENCIA >= '202301'
			AND TOTAL.COMPETENCIA <= '202312' THEN DECODE(TOTAL.EMPRESA, 'MARANHAO', 8.29, 'PARA', 6.06, 'PIAUI', 7.02, 'ALAGOAS', 8.81, 'AMAPA', 10, 'RIO GRANDE DO SUL', 10)
			WHEN TOTAL.COMPETENCIA >= '202401'
			AND TOTAL.COMPETENCIA <= '202412' THEN DECODE(TOTAL.EMPRESA, 'MARANHAO', 4, 'PARA', 4, 'PIAUI', 4, 'ALAGOAS', 4, 'AMAPA', 5, 'RIO GRANDE DO SUL', 7, 'GOIAS', 4)
			WHEN TOTAL.COMPETENCIA >= '202501' THEN DECODE(TOTAL.EMPRESA, 'EQTL_MA', 4, 'EQTL_PA', 4, 'EQTL_PI', 4, 'EQTL_AL', 4, 'EQTL_AP', 4, 'EQTL_RS', 4, 'EQTL_GO', 4)
		END META
	FROM
		(
		SELECT
			A.EMPRESA,
			A.COMPETENCIA,
			COUNT(A.OS_OPER) QTD_TOTAL,
			/* A.OSTIPO,
			A.OSSUBTIPO,
			A.TIPO_CONCLUSAO,
			A.TIPO,
			*/
            A.PREFIXO
		FROM
			{{ ref('vw_retrabalho_notas') }} a
		WHERE
			TO_DATE(A.COMPETENCIA,'YYYYMM') = TO_DATE({{ get_month_ref() }},'YYYYMM')
			AND A.EMPRESA <> 'EQTL_GO'
			AND A.OSSUBTIPO = 'IND'
			AND a.ostipo = 'NR'
		GROUP BY
			A.EMPRESA,
			COMPETENCIA,
			A.PREFIXO) TOTAL
	GROUP BY
		TOTAL.EMPRESA,
		TOTAL.COMPETENCIA,
		TOTAL.PREFIXO) T
LEFT JOIN (
	SELECT
		RETRA.EMPRESA,
		RETRA.COMPETENCIA,
		PREFIXO_ORIGEM ,
		SUM(RETRA.QTD_RETRA) QTD_RETRA
	FROM
		(
		SELECT
			A.EMPRESA,
			A.COMPETENCIA,
			COUNT(A.OS_OPER_ORIGEM) QTD_RETRA,
			A.tipo_conclusao_origem,
			A.tipo,
			A.PREFIXO_ORIGEM
		FROM
			{{ ref('vw_retrabalho_notas_rtb') }} a
		WHERE
			TO_DATE(A.COMPETENCIA,'YYYYMM') = TO_DATE({{ get_month_ref() }},'YYYYMM')
			AND A.EMPRESA <> 'EQTL_GO'
			AND A.OSSUBTIPO_ORIGEM = 'IND'
			AND A.OSTIPO_ORIGEM = 'NR'
		GROUP BY
			A.EMPRESA,
			COMPETENCIA,
			a.tipo_conclusao_origem,
			a.tipo,
			A.PREFIXO_ORIGEM) RETRA
	GROUP BY
		RETRA.EMPRESA,
		RETRA.COMPETENCIA,
		RETRA.PREFIXO_ORIGEM ) R ON
	R.COMPETENCIA = T.COMPETENCIA
	AND R.EMPRESA = T.EMPRESA
	AND R.PREFIXO_ORIGEM = T.PREFIXO
LEFT JOIN {{ ref('vw_gp_gstc_equipes_sem_duplicatas') }} E ON
	E.DISTRIBUIDORA = T.EMPRESA
	AND E.PREFIXO = T.PREFIXO
