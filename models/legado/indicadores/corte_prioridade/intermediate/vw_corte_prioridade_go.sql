SELECT 
C.SIGLA_EMPRESA, 
C.EMPRESA, 
C.REGIONAL, 
C.POLO, 
AC.NOTA::VARCHAR NOTA, 
C.VALOR_DIVIDA,
CASE
	WHEN LEFT(C.DESCRICAO_STPOS,3) = 'P1 ' THEN 'P1'
	WHEN LEFT(C.DESCRICAO_STPOS,3) = 'P2 ' THEN 'P2'
	WHEN LEFT(C.DESCRICAO_STPOS,3) = 'P3 ' THEN 'P3'
	ELSE 'P3'
END PRIORIDADE,
'SUBTIPO' MODALIDADE_PRIORIDADE,
TRUNC(AC.DH_ATRIBUICAO, 'MM') DT_COMPETENCIA, 
SEC.CONTRATO CONTRATO_ATRIBUICAO, 
C.CONTRATO CONTRATO_CONCLUSAO, 
C.EQUIPE_CONCLUSAO, 
C.DH_EXECUCAO_CAMPO,
TO_JSON(ARRAY_AGG(
    OBJECT_CONSTRUCT(
        'EQUIPE', AC.EQUIPE,
        'CONTRATO', SEC.CONTRATO,
        'DH_ATRIBUICAO', TO_VARCHAR(AC.DH_ATRIBUICAO, 'YYYY-MM-DD HH24:MI:SS')
    )
) WITHIN GROUP (ORDER BY AC.DH_ATRIBUICAO)) AS HISTORICO_ATRIBUICOES,
COUNT(*) QTD_ATRIBUICOES
FROM {{ ref('atribuicao_corte') }} ac
LEFT JOIN {{ ref('slv_equipe_contrato') }} sec 
	ON sec.ANO = EXTRACT (YEAR FROM ac.DH_ATRIBUICAO) 
	AND sec.MES = EXTRACT (MONTH FROM ac.DH_ATRIBUICAO) 
	AND sec.COD_EMPRESA = ac.COD_EMPRESA 
	AND sec.PREFIXO = ac.EQUIPE
INNER JOIN (
	SELECT NC.COD_EMPRESA, NC.NOTA, nc.DH_EXECUCAO_CAMPO, nc.EQUIPE_CONCLUSAO, sec.CONTRATO, ge.SIGLA_EMPRESA, ge.EMPRESA, gr.REGIONAL, gp.POLO, NC.VALOR_DIVIDA, NC.DESCRICAO_STPOS
	FROM {{ ref('nota_corte') }} nc
	LEFT JOIN {{ ref('slv_equipe_contrato') }} sec 
		ON sec.ANO = EXTRACT (YEAR FROM nc.DH_EXECUCAO_CAMPO) 
		AND sec.MES = EXTRACT (MONTH FROM nc.DH_EXECUCAO_CAMPO) 
		AND sec.COD_EMPRESA = nc.COD_EMPRESA 
		AND sec.PREFIXO = nc.EQUIPE_CONCLUSAO
	LEFT JOIN SB_PERFORMANCE.SILVER.GLOSSARIO_EMPRESAS ge ON ge.COD_EMPRESA = nc.COD_EMPRESA 
	LEFT JOIN SB_PERFORMANCE.SILVER.GLOSSARIO_POLOS gp ON gp.COD_POLO = nc.COD_POLO 
	LEFT JOIN SB_PERFORMANCE.SILVER.GLOSSARIO_REGIONAIS gr ON gr.COD_REGIONAL = gp.COD_REGIONAL
	WHERE STATUS <> 'CANCELADO'
) C ON C.COD_EMPRESA = AC.COD_EMPRESA AND C.NOTA::VARCHAR = AC.NOTA::VARCHAR
WHERE AC.STATUS <> 'CANCELADO'
AND TO_VARCHAR(AC.DH_ATRIBUICAO, 'YYYYMM') = {{ get_month_ref() }}
GROUP BY ALL