SELECT 
C.SIGLA_EMPRESA, 
C.EMPRESA, 
C.REGIONAL, 
C.POLO, 
AC.NOTA::VARCHAR NOTA, 
C.VALOR_DIVIDA,
CASE
	WHEN C.VALOR_DIVIDA >= 5000 THEN 'P1'
	WHEN C.VALOR_DIVIDA >= 1000 THEN 'P2'
	ELSE 'P3'
END PRIORIDADE,
'DIVIDA' MODALIDADE_PRIORIDADE,
TRUNC(AC.DH_GERACAO, 'MM') DT_COMPETENCIA, 
SEC.CONTRATO CONTRATO_ATRIBUICAO, 
CASE WHEN AC.STATUS = 'FINALIZADO' THEN C.CONTRATO ELSE NULL END CONTRATO_CONCLUSAO, 
CASE WHEN AC.STATUS = 'FINALIZADO' THEN C.EQUIPE_CONCLUSAO ELSE NULL END EQUIPE_CONCLUSAO, 
CASE WHEN AC.STATUS = 'FINALIZADO' THEN C.DH_EXECUCAO_CAMPO ELSE NULL END DH_EXECUCAO_CAMPO,
TO_JSON(ARRAY_AGG(
    OBJECT_CONSTRUCT(
        'EQUIPE', AC.EQUIPE,
        'CONTRATO', SEC.CONTRATO,
        'DH_GERACAO', TO_VARCHAR(AC.DH_GERACAO, 'YYYY-MM-DD HH24:MI:SS')
    )
) WITHIN GROUP (ORDER BY AC.DH_GERACAO)) AS HISTORICO_ATRIBUICOES,
COUNT(*) QTD_ATRIBUICOES
FROM (SELECT DISTINCT * FROM {{ ref('corte_gerado') }}) AC
LEFT JOIN {{ ref('slv_equipe_contrato') }} sec 
	ON sec.ANO = EXTRACT (YEAR FROM ac.DH_GERACAO) 
	AND sec.MES = EXTRACT (MONTH FROM ac.DH_GERACAO) 
	AND sec.COD_EMPRESA = ac.COD_EMPRESA 
	AND sec.PREFIXO = REGEXP_REPLACE(ac.EQUIPE, '(MA_|AL_|AP_|PI_|PA_|RS_)', '')
INNER JOIN (
	SELECT NC.COD_EMPRESA, NC.NOTA, nc.DH_EXECUCAO_CAMPO, nc.EQUIPE_CONCLUSAO, sec.CONTRATO, ge.SIGLA_EMPRESA, ge.EMPRESA, gr.REGIONAL, gp.POLO, NC.VALOR_DIVIDA, NC.DESCRICAO_STPOS, STATUS
	FROM {{ ref('nota_corte') }} nc
	LEFT JOIN {{ ref('slv_equipe_contrato') }} sec 
		ON sec.ANO = EXTRACT (YEAR FROM nc.DH_EXECUCAO_CAMPO) 
		AND sec.MES = EXTRACT (MONTH FROM nc.DH_EXECUCAO_CAMPO) 
		AND sec.COD_EMPRESA = nc.COD_EMPRESA 
		AND sec.PREFIXO = nc.EQUIPE_CONCLUSAO
	LEFT JOIN SB_PERFORMANCE.SILVER.GLOSSARIO_EMPRESAS ge ON ge.COD_EMPRESA = nc.COD_EMPRESA 
	LEFT JOIN SB_PERFORMANCE.SILVER.GLOSSARIO_POLOS gp ON gp.COD_POLO = nc.COD_POLO 
	LEFT JOIN SB_PERFORMANCE.SILVER.GLOSSARIO_REGIONAIS gr ON gr.COD_REGIONAL = gp.COD_REGIONAL
	WHERE STATUS <> 'CANCELADO'
) C ON C.COD_EMPRESA = AC.COD_EMPRESA AND C.NOTA::VARCHAR = AC.NOTA::VARCHAR
WHERE AC.STATUS IN ('FINALIZADO','GERADO')
AND TO_VARCHAR(AC.DH_GERACAO, 'YYYYMM') = {{ get_month_ref() }}
GROUP BY ALL