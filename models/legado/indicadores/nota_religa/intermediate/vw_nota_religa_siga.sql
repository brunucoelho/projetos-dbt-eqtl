SELECT 
CURRENT_TIMESTAMP DH_ETL,
DECODE(TX_STATE,'AL',1,'AP',2,'MA',3,'PA',4,'PI',5,'GO',6,'RS',7) COD_EMPRESA,
NULL COD_POLO,
LTRIM(TX_EQ_CONTACONTRATO,'0') CONTA_CONTRATO,
LTRIM(NB_EQ_NUMNOTA,'0') NOTA,
TRY_TO_TIMESTAMP(TX_EQ_DATAABERTURA, 'DD/MM/YYYY HH24:MI:SS') DH_ABERTURA,
TRY_TO_TIMESTAMP(DATA_HORA_ACAO_CONCLUSAO, 'YYYY-MM-DD HH24:MI') DH_FINALIZADO,
TRY_TO_TIMESTAMP(TX_EQ_DATACONCLUSAOLIMITE, 'DD/MM/YYYY HH24:MI:SS') DH_LIMITE,
TRY_TO_TIMESTAMP(DATA_HORA_ACAO_CONCLUSAO, 'YYYY-MM-DD HH24:MI') DH_EXECUCAO_CAMPO,
CASE WHEN TX_STATUS IN ('completed','notdone','deleted','started','pending','enroute')  THEN SUBSTR(TX_RESOURCEEXTERNALID,4) ELSE NULL  END EQUIPE_CONCLUSAO,
DECODE(TX_STATUS, 
'started', 'ANDAMENTO', 
'deleted', 'FINALIZADA', 
'cancelled', 'CANCELADO', 
'completed', 'FINALIZADO', 
'notdone', 'FINALIZADO', 
'pending', 'ANDAMENTO', 
'suspended', 'CANCELADO', 
'enroute', 'ANDAMENTO',
'CANCELADO') STATUS,
DECODE(TX_EQ_TIPOCONCLUSAO, 'EX', 'S', 'NE', 'N') PRODUTIVA
FROM EQTLINFO_RAW.SIGA.TB_OFSC_ACTIVITIES_RT toar 
WHERE TX_EQ_TIPONOTA IN ('RL','RI')
AND TX_STATE IN ('AL','AP','MA','PA','PI','RS','GO')
AND TO_VARCHAR(TRY_TO_TIMESTAMP(TX_EQ_DATAABERTURA, 'DD/MM/YYYY HH24:MI:SS'),'YYYYMM') = {{ get_month_ref() }}