SELECT 
 CURRENT_TIMESTAMP AS DH_ETL,
 6 COD_EMPRESA,
 TO_NUMBER(d.GV3) COD_POLO,
 LTRIM(moc.CONTA_CONTRATO::VARCHAR,'0') CONTA_CONTRATO,
 LTRIM(MOC.NR_OS::VARCHAR,'0') NOTA,
 moc.DT_SOLICITACAO DH_ABERTURA,
 COC.DT_CONCLUSAO DH_FINALIZADO,
 moc.DT_LIMITE_EXECUCAO_OS DH_LIMITE,
 TOC.HOST_VI_DT_FIM_SERVICO DH_EXECUCAO_CAMPO,
 NVL(PT.PRX_NOME_INTERNO, PT.PRX_DESCRICAO) EQUIPE_CONCLUSAO,
 CASE
  WHEN moc.CD_STATUS_OS IN (1,2,3,4,5) THEN 'ANDAMENTO'
  WHEN cdm.TIPO_CONCLUSAO IN ('N','R') THEN 'FINALIZADO'
  ELSE 'CANCELADO'
 END STATUS,
 DECODE(CDM.TIPO_CONCLUSAO,'N','S','R','N','I','N') PRODUTIVA
 FROM EQTLINFO_RAW.OPER_GO.MOVTO_OS_COMERCIAL moc 
 LEFT JOIN EQTLINFO_RAW.OPER_GO.SUBTIPO_DE_OS sdo ON sdo.ossubtipo_id = moc.ossubtipo_id
 LEFT JOIN EQTLINFO_RAW.OPER_GO.CONCLUI_OS_COMERCIAL coc ON coc.CD_MOVTO_OS_COMERCIAL = moc.CD_MOVTO_OS_COMERCIAL
 LEFT JOIN EQTLINFO_RAW.OPER_GO.TURMA_OS_COMERCIAL toc ON toc.HIST_OS_TURMA_ID = coc.HIST_OS_TURMA_ID
 LEFT JOIN EQTLINFO_RAW.OPER_GO.HISTORICO_TURMA_PLANTAO htp ON htp.HIST_TURMA_PLANTAO_ID = toc.TURMA_PLANTAO_ID
 LEFT JOIN EQTLINFO_RAW.OPER_GO.PREFIXO_TURMA pt ON pt.PREFIXO_TURMA_ID = htp.PREFIXO_TURMA_ID
 LEFT JOIN EQTLINFO_RAW.OPER_GO.BASE b ON b.BASE_ID = moc.BASE_ID
 LEFT JOIN EQTLINFO_RAW.OPER_GO.PONTO_APOIO pa ON pa.CODIGO_PA = b.CODIGO_PA
 LEFT JOIN EQTLINFO_PRD.EQTL_GO.TAB_CADASTRO CAD ON TO_NUMBER(CAD.INSTALACAO) = moc.NR_UC
 LEFT JOIN SB_PERFORMANCE.EQTL_CORP.dicionario d ON d.DICIONARIO = 'OPER_PONTO_APOIO_CODIGO_PA' AND d.SV2 = pa.CODIGO_PA
 -- INNER JOIN EQTLINFO_RAW.CBILL_GO.RMA_SERVICE_REQUEST rsr ON rsr.id = moc.NR_OS
 -- INNER JOIN EQTLINFO_RAW.CBILL_GO.RMA_SREQUEST_STATUS rss ON rss.ID = rsr.FK_SRSTS_ID
 LEFT JOIN EQTLINFO_RAW.OPER_GO.CODE_MEDIDA CDM ON moc.GRUPO_CODE_MEDIDA_ID = CDM.GRUPO_CODE_MEDIDA_ID
  AND CDM.OSSUBTIPO_ID = moc.OSSUBTIPO_ID  
  AND CDM.CODE_MEDIDA_ID = COC.CD_TP_CONCLUSAO_OS  
 LEFT JOIN SB_PERFORMANCE.EQTL_CORP.CELG_GRUPO_CODE_MEDIDA GCM ON GCM.GRUPO_CODE_MEDIDA_ID = moc.GRUPO_CODE_MEDIDA_ID 
  AND GCM.OSSUBTIPO_ID = moc.OSSUBTIPO_ID
 WHERE to_varchar(trunc(moc.DT_SOLICITACAO,'MM'), 'YYYYMM') = {{ get_month_ref() }}
 AND moc.ostipo_id = 'RI' -- NOTA DE RELIGA