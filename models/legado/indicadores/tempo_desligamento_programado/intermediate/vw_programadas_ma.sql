SELECT 
CURRENT_TIMESTAMP DATA_ETL,
'EQTL_MA' EMPRESA,
REGIONAL,
EMPRESA FORNECEDOR,
NULL EQUIPE,
TO_CHAR(NUM_SI::INTEGER) SOLICITACAO_INTERVENCAO,
STATUS STATUS_SI,
NULL ID_NOTA_EMERGENCIAL,
CASE
	WHEN ((INI_PREV <= TO_TIMESTAMP(INI_REAL,'DD/MM/YYYY HH24:MI:SS'))
		AND (FIM_PREV >= TO_TIMESTAMP(FIM_REAL,'DD/MM/YYYY HH24:MI:SS'))) THEN 'NO_PRAZO'
	ELSE 'FORA_PRAZO'
END AS LIMITES_LN,
INI_PREV INICIO_PREVISTO,
FIM_PREV FIM_PREVISTO,
TO_TIMESTAMP(INI_REAL,'DD/MM/YYYY HH24:MI:SS') INICIO_REAL,
TO_TIMESTAMP(FIM_REAL,'DD/MM/YYYY HH24:MI:SS') FIM_REAL
FROM SB_OPERACAO.OPERACAO_CORP.programadas_MA P
WHERE STATUS = 'F'
AND TRUNC(p.INI_PREV,'MM') = TO_DATE ({{ get_month_ref() }},'YYYYMM')
