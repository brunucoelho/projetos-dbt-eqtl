SELECT
OCORRENCIA_ID, OCORRENCIA, PTP_ID, PTP_NUMERO, NATUREZA_PROGRAMACAO,
EMPREITEIRA, AREA_SOLICITANTE, MOTIVO, TIPO_INTERVENCAO, STATUS_PTP,
STATUS_OCORRENCIA, INICIO_OCORRENCIA, FIM_OCORRENCIA, INICIO_PREVISTO,
FIM_PREVISTO, PRIMEIRO_EVENTO, ULTIMO_EVENTO, INCONSISTENCIA_TEMPO, CSC,
VERIFICA_HORARIO, VERIFICA_INICIO, VERIFICA_FIM, DESVIO_INICIO, DESVIO_FIM,
CASE WHEN INCONSISTENCIA_TEMPO = 'NAO' AND STATUS_OCORRENCIA = 'FINALIZADA'
		AND (
			TIPO_INTERVENCAO = 'AT INTERRUPCAO' 
            OR TIPO_INTERVENCAO = 'BT INTERRUPCAO' 
            OR TIPO_INTERVENCAO = 'MT INTERRUPCAO'
        ) THEN
			CASE WHEN MOTIVO <> 'NCO - MIGRACAO AO MERCADO LIVRE'
					AND AREA_SOLICITANTE <> 'MERCADO - PARTICULAR SOLICITACAO DO CLIENTE'
                    AND AREA_SOLICITANTE <> 'OM  - OPERACAO DA REDE'
                    AND VERIFICA_HORARIO = 'FORA DO PRAZO' THEN 'SIM'
                ELSE 'NAO'   
            END
	ELSE NULL               
END AS DESLIGAMENTO_FORA_PRAZO,
CASE WHEN INCONSISTENCIA_TEMPO = 'NAO' 
		AND STATUS_OCORRENCIA = 'FINALIZADA'
		AND (
            TIPO_INTERVENCAO = 'AT INTERRUPCAO' 
            OR TIPO_INTERVENCAO = 'BT INTERRUPCAO' 
            OR TIPO_INTERVENCAO = 'MT INTERRUPCAO'
        )
        AND MOTIVO <> 'NCO - MIGRACAO AO MERCADO LIVRE'
        AND AREA_SOLICITANTE <> 'MERCADO - PARTICULAR SOLICITACAO DO CLIENTE'
        AND AREA_SOLICITANTE <> 'OM  - OPERACAO DA REDE' THEN 'SIM'
        ELSE 'NAO'
END AS DESLIGAMENTO,
CASE WHEN INCONSISTENCIA_TEMPO = 'NAO'
		AND (STATUS_OCORRENCIA <> 'FINALIZADA' OR STATUS_OCORRENCIA IS NULL)
        THEN 'NAO'   
	WHEN INCONSISTENCIA_TEMPO = 'NAO' THEN 'SIM' 
    ELSE NULL
END AS EXECUTADA,
CASE WHEN INCONSISTENCIA_TEMPO = 'NAO' THEN 'SIM'        
	ELSE NULL    
END AS TOTAL
FROM(
	SELECT    
	OCORRENCIA_ID, OCORRENCIA, PTP_ID, PTP_NUMERO,
	NATUREZA_PROGRAMACAO, EMPREITEIRA, AREA_SOLICITANTE,
	MOTIVO, TIPO_INTERVENCAO, STATUS_PTP, STATUS_OCORRENCIA,
	INICIO_OCORRENCIA, FIM_OCORRENCIA, INICIO_PREVISTO,
	FIM_PREVISTO, PRIMEIRO_EVENTO,ULTIMO_EVENTO,
	INCONSISTENCIA_TEMPO, CSC,
	CASE -- VERIFICA O PRAZO DO INICIO E FIM DA PTP
		WHEN STATUS_OCORRENCIA = 'FINALIZADA' AND INCONSISTENCIA_TEMPO = 'NAO' THEN
			CASE WHEN PRIMEIRO_EVENTO IS NULL AND ULTIMO_EVENTO IS NULL THEN
				CASE WHEN INICIO_OCORRENCIA >= INICIO_PREVISTO AND FIM_OCORRENCIA <= FIM_PREVISTO THEN 'NO PRAZO'
					ELSE 'FORA DO PRAZO'
                END
                WHEN PRIMEIRO_EVENTO IS NOT NULL AND ULTIMO_EVENTO IS NOT NULL THEN
					CASE WHEN PRIMEIRO_EVENTO >= INICIO_PREVISTO AND ULTIMO_EVENTO <= FIM_PREVISTO THEN 'NO PRAZO'
						ELSE 'FORA DO PRAZO'
                    END
                ELSE 'VERIFICAR'
            END
		ELSE 'PTP NAO FINALIZADA/INCONSISTENCIA'
    END VERIFICA_HORARIO,
	CASE -- VERIFICA O PRAZO DO INICIO DA PTP
		WHEN STATUS_OCORRENCIA = 'FINALIZADA' AND INCONSISTENCIA_TEMPO = 'NAO' THEN
			CASE WHEN PRIMEIRO_EVENTO IS NULL AND ULTIMO_EVENTO IS NULL THEN
				CASE WHEN INICIO_OCORRENCIA >= INICIO_PREVISTO THEN 'NO PRAZO'
					ELSE 'FORA DO PRAZO'
                END
                WHEN PRIMEIRO_EVENTO IS NOT NULL AND ULTIMO_EVENTO IS NOT NULL THEN
					CASE WHEN PRIMEIRO_EVENTO >= INICIO_PREVISTO THEN 'NO PRAZO'
						ELSE 'FORA DO PRAZO'
                    END        
                ELSE 'VERIFICAR'
            END
        ELSE 'PTP NAO FINALIZADA/INCONSISTENCIA'
    END AS VERIFICA_INICIO,
	CASE -- VERIFICA O PRAZO DO FIM DA PTP
		WHEN STATUS_OCORRENCIA = 'FINALIZADA' AND INCONSISTENCIA_TEMPO = 'NAO' THEN
			CASE WHEN PRIMEIRO_EVENTO IS NULL AND ULTIMO_EVENTO IS NULL THEN
				CASE WHEN FIM_OCORRENCIA <= FIM_PREVISTO THEN 'NO PRAZO'
					ELSE 'FORA DO PRAZO'
                END
                WHEN PRIMEIRO_EVENTO IS NOT NULL AND ULTIMO_EVENTO IS NOT NULL THEN
					CASE WHEN ULTIMO_EVENTO <= FIM_PREVISTO THEN 'NO PRAZO'
						ELSE 'FORA DO PRAZO'
                    END
                ELSE 'VERIFICAR'                      
            END
        ELSE 'PTP NAO FINALIZADA/INCONSISTENCIA'
    END AS VERIFICA_FIM,
	CASE -- CALCULA O DESVIO DO INICIO
	    WHEN STATUS_OCORRENCIA = 'FINALIZADA' AND INCONSISTENCIA_TEMPO = 'NAO' THEN
	        CASE 
	            WHEN PRIMEIRO_EVENTO IS NULL AND ULTIMO_EVENTO IS NULL THEN
	                CASE 
	                    WHEN INICIO_OCORRENCIA >= INICIO_PREVISTO THEN 0
	                    ELSE ROUND(DATEDIFF(MINUTE, INICIO_OCORRENCIA, INICIO_PREVISTO),1)
	                END
	            WHEN PRIMEIRO_EVENTO IS NOT NULL AND ULTIMO_EVENTO IS NOT NULL THEN
	                CASE 
	                    WHEN PRIMEIRO_EVENTO >= INICIO_PREVISTO THEN 0
	                    ELSE ROUND(DATEDIFF(MINUTE, PRIMEIRO_EVENTO, INICIO_PREVISTO),1)
	                END
	            ELSE 0
	        END
	    ELSE 0
	END AS DESVIO_INICIO,
	
	CASE -- CALCULA O DESVIO DO FIM
	    WHEN STATUS_OCORRENCIA = 'FINALIZADA' AND INCONSISTENCIA_TEMPO = 'NAO' THEN
	        CASE 
	            WHEN PRIMEIRO_EVENTO IS NULL AND ULTIMO_EVENTO IS NULL THEN
	                CASE 
	                    WHEN FIM_OCORRENCIA <= FIM_PREVISTO THEN 0
	                    ELSE ROUND(DATEDIFF(MINUTE, FIM_PREVISTO, FIM_OCORRENCIA),1)
	                END
	            WHEN PRIMEIRO_EVENTO IS NOT NULL AND ULTIMO_EVENTO IS NOT NULL THEN
	                CASE 
	                    WHEN ULTIMO_EVENTO <= FIM_PREVISTO THEN 0
	                    ELSE ROUND(DATEDIFF(MINUTE, FIM_PREVISTO, ULTIMO_EVENTO),1)
	                END
	            ELSE 0
	        END
	    ELSE 0
	END AS DESVIO_FIM
    FROM(
		SELECT
		OCO.OCORRENCIA_ID, (TO_VARCHAR(OCO.OCO_NUMERO::INT) || '-' || TO_VARCHAR(OCO.OCO_MES::INT) || '-' || TO_VARCHAR(OCO.OCO_ANO::INT)) AS OCORRENCIA,
		SOLICITACAO_INTERVENCAO_ID AS PTP_ID, REGEXP_REPLACE(REPLACE(SIN.SIN_PONTO_REFERENCIA,'PED-SGT:',''), '[^0-9]', '') PTP_NUMERO,
		TDN.TNA_DESCRICAO AS NATUREZA_PROGRAMACAO, EMP.EMP_DESCRICAO AS EMPREITEIRA, 
		ARS.ARS_DESCRICAO AS AREA_SOLICITANTE, MSI.MSI_DESCRICAO AS MOTIVO,
		TSI.TSI_DESCRICAO AS TIPO_INTERVENCAO,
		CASE SIN.SIN_STATUS
			WHEN 'A' THEN 'APROVADO'
			WHEN 'C' THEN 'CONFERIDO'
			WHEN 'D' THEN 'EXECUCAO'
			WHEN 'E' THEN 'PENDENTE'
			WHEN 'L' THEN 'CANCELADA'
			WHEN 'R' THEN 'REPROVADO'
			WHEN 'T' THEN 'ELABORACAO'
			WHEN 'X' THEN 'EXECUTADA'
			ELSE SIN.SIN_STATUS
        END AS STATUS_PTP,
		CASE OCO.OCO_STATUS
			WHEN 'V' THEN 'VINCULADA'
			WHEN 'T' THEN 'TRABALHANDO'
			WHEN 'A' THEN 'DESLOCAMENTO'
			WHEN 'C' THEN 'CANCELADA'
			WHEN 'E' THEN 'PREPARACAO'
			WHEN 'F' THEN 'FINALIZADA'
			ELSE OCO.OCO_STATUS
		END AS STATUS_OCORRENCIA,
		OCO.OCO_DATA_NR AS INICIO_OCORRENCIA,
		OCO.OCO_DATA_CONCLUSAO AS FIM_OCORRENCIA,
		SIN.SIN_INICIO_PREVISTO AS INICIO_PREVISTO,
		SIN.SIN_TERMINO_PREVISTO AS FIM_PREVISTO,
		MIN(EVE.EVE_DATA_INICIO) PRIMEIRO_EVENTO,
		MAX(EVE.EVE_DATA_INICIO) ULTIMO_EVENTO,
		CASE -- VERIFICA AS DATAS DE INÍCIO E TÉRMINO DA PROGRAMAÇÃO
			WHEN OCO.OCO_DATA_NR IS NOT NULL 
				AND OCO.OCO_DATA_CONCLUSAO IS NOT NULL 
                AND OCO.OCO_DATA_NR > OCO.OCO_DATA_CONCLUSAO THEN 'SIM'                   
            WHEN SIN.SIN_INICIO_PREVISTO IS NOT NULL 
				AND SIN.SIN_TERMINO_PREVISTO IS NOT NULL 
                AND SIN.SIN_INICIO_PREVISTO > SIN.SIN_TERMINO_PREVISTO THEN 'SIM'                      
            ELSE 'NAO'              
        END AS INCONSISTENCIA_TEMPO, PA.PAP_NOME AS CSC
        FROM EQTLINFO_RAW.OPER_GO.OCORRENCIA OCO
		RIGHT JOIN EQTLINFO_RAW.OPER_GO.SOLICITACAO_INTERVENCAO SIN ON OCO.OCORRENCIA_ID = SIN.OCORRENCIA_ID
		INNER JOIN EQTLINFO_RAW.OPER_GO.EMPREITEIRA EMP ON EMP.EMPREITEIRA_ID = SIN.EMPREITEIRA_ID
		INNER JOIN EQTLINFO_RAW.OPER_GO.TIPO_DE_NATUREZA TDN ON TDN.TB_NATUREZA_ID = SIN.TB_NATUREZA_ID
		INNER JOIN EQTLINFO_PRD.EQTL_GO.TIPO_SOLICITACAO_INTERVENCAO TSI ON TSI.TB_SOLICITACAO_INTERVENCAO_ID = SIN.TB_SOLICITACAO_INTERVENCAO_ID
		INNER JOIN EQTLINFO_PRD.CELG.MOTIVO_SOLICITACAO_INTERVENCAO MSI ON MSI.TB_MT_SOLICIT_INTERV_ID = SIN.TB_MT_SOLICIT_INTERV_ID
		INNER JOIN EQTLINFO_PRD.EQTL_GO.AREA_SOLICITANTE ARS ON ARS.AREA_SOLICITANTE_ID = SIN.AREA_SOLICITANTE_ID
		LEFT JOIN EQTLINFO_RAW.OPER_GO.BAIRRO BR ON BR.BAIRRO_ID = SIN.BAIRRO_ID
		LEFT JOIN EQTLINFO_RAW.OPER_GO.BASE BAS ON BAS.BASE_ID = BR.BASE_ID
		LEFT JOIN EQTLINFO_RAW.OPER_GO.PONTO_APOIO PA ON PA.CODIGO_PA = BAS.CODIGO_PA
		LEFT JOIN EQTLINFO_RAW.OPER_GO.EVENTOS EVE ON EVE.OCORRENCIA_ID = OCO.OCORRENCIA_ID         
        WHERE TO_VARCHAR(TRUNC(SIN.SIN_INICIO_PREVISTO,'MM'),'YYYYMM') = {{ get_month_ref() }} 
        GROUP BY OCO.OCORRENCIA_ID, (TO_VARCHAR(OCO.OCO_NUMERO::INT) || '-' || TO_VARCHAR(OCO.OCO_MES::INT) || '-' || TO_VARCHAR(OCO.OCO_ANO::INT)),
		SOLICITACAO_INTERVENCAO_ID, REPLACE(SIN.SIN_PONTO_REFERENCIA,'PED-SGT:',''),
		TDN.TNA_DESCRICAO, EMP.EMP_DESCRICAO, ARS.ARS_DESCRICAO,
		MSI.MSI_DESCRICAO, TSI.TSI_DESCRICAO,
		CASE SIN.SIN_STATUS
			WHEN 'A' THEN 'APROVADO'
			WHEN 'C' THEN 'CONFERIDO'
			WHEN 'D' THEN 'EXECUCAO'
			WHEN 'E' THEN 'PENDENTE'
			WHEN 'L' THEN 'CANCELADA'
			WHEN 'R' THEN 'REPROVADO'
			WHEN 'T' THEN 'ELABORACAO'
			WHEN 'X' THEN 'EXECUTADA'
			ELSE SIN.SIN_STATUS
		END,
		CASE OCO.OCO_STATUS
			WHEN 'V' THEN 'VINCULADA'
			WHEN 'T' THEN 'TRABALHANDO'
			WHEN 'A' THEN 'DESLOCAMENTO'
			WHEN 'C' THEN 'CANCELADA'
			WHEN 'E' THEN 'PREPARACAO'
			WHEN 'F' THEN 'FINALIZADA'
			ELSE OCO.OCO_STATUS
		END,
		OCO.OCO_DATA_NR, OCO.OCO_DATA_CONCLUSAO, SIN.SIN_INICIO_PREVISTO, SIN.SIN_TERMINO_PREVISTO,
		CASE WHEN OCO.OCO_DATA_NR IS NOT NULL 
				AND OCO.OCO_DATA_CONCLUSAO IS NOT NULL 
				AND OCO.OCO_DATA_NR > OCO.OCO_DATA_CONCLUSAO THEN 'SIM'   
			WHEN SIN.SIN_INICIO_PREVISTO IS NOT NULL 
				AND SIN.SIN_TERMINO_PREVISTO IS NOT NULL 
				AND SIN.SIN_INICIO_PREVISTO > SIN.SIN_TERMINO_PREVISTO THEN 'SIM'
			ELSE 'NAO'    
        END, PA.PAP_NOME
    )
)