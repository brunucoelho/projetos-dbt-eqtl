SELECT 
'GOIAS'||PT.PRX_DESCRICAO||NVL(SC.FORNECEDOR_SAP,'') CHAVE,
'GOIAS' EMPRESA,
'EQTL_GO' DISTRIBUIDORA,
RE.REL_DESCRICAO REGIONAL, 
DPO.POLO POLO,
B.BAS_NOME MUNICIPIO,
SC.FORNECEDOR_SAP FONECEDOR,
PT.PRX_DESCRICAO PREFIXO,
CASE
	WHEN PT.TB_EQUIPE_ID IN ('E') THEN 'ATENDIMENTO_EMERGENCIAL'
	WHEN PT.TB_EQUIPE_ID IN ('C','R') THEN 'CORTE_RELIGACAO'
	WHEN PT.TB_EQUIPE_ID IN ('L') THEN 'LIGACAO_NOVA'
	WHEN PT.TB_EQUIPE_ID IN ('O') THEN 'LINHA MORTA'
	WHEN PT.TB_EQUIPE_ID IN ('U','T','V') THEN 'MANUTENCAO'
	WHEN PT.TB_EQUIPE_ID IN ('M') THEN 'MULTIFUNCIONAL'
	WHEN PT.TB_EQUIPE_ID IN ('F','I','J','S') THEN 'RECUPERACAO_ENERGIA'
	WHEN PT.TB_EQUIPE_ID IN ('A') THEN 'GRUPO A'
	WHEN PT.TB_EQUIPE_ID IN ('D') THEN 'SEED_MONEY'
	WHEN PT.TB_EQUIPE_ID IN ('X','Y','G') THEN 'FISCAL'
	WHEN PT.TB_EQUIPE_ID IN ('K') THEN 'MICROGERACAO'
	WHEN PT.TB_EQUIPE_ID IN ('P','Z') THEN 'PODA'
	WHEN PT.TB_EQUIPE_ID IN ('H') THEN 'CORTE_MOTO'
	ELSE NULL
END TIPO_EQP,
NULL SUBTIPO_EQP,
CASE
	WHEN PT.TB_EQUIPE_ID IN ('E','M') THEN 'ATENDIMENTO EMERGENCIAL'
	WHEN PT.TB_EQUIPE_ID IN ('C','R') THEN 'CORTE LEVE'
	WHEN PT.TB_EQUIPE_ID IN ('L') AND V.VIA_TRACAO LIKE '%4x4%' THEN 'LN 4X4'
	WHEN PT.TB_EQUIPE_ID IN ('L','K') THEN 'LN LEVE'
	WHEN PT.TB_EQUIPE_ID IN ('O') THEN 'LINHA MORTA'
	WHEN PT.TB_EQUIPE_ID IN ('V') THEN 'LINHA VIVA'
	WHEN PT.TB_EQUIPE_ID IN ('T') THEN 'TATICA'
	WHEN PT.TB_EQUIPE_ID IN ('F','I','J','S') THEN 'PERDAS'
	WHEN PT.TB_EQUIPE_ID IN ('A') THEN 'GRUPO A'
	WHEN PT.TB_EQUIPE_ID IN ('D') THEN 'SEED MONEY'
	WHEN PT.TB_EQUIPE_ID IN ('X','Y','G') THEN 'FISCAL'
	WHEN PT.TB_EQUIPE_ID IN ('W') THEN 'EQUIPAMENTO'
	WHEN PT.TB_EQUIPE_ID IN ('P','Z') THEN 'PODA'
	WHEN PT.TB_EQUIPE_ID IN ('U','T') THEN 'MANUTENCAO'
	WHEN PT.TB_EQUIPE_ID IN ('H') THEN 'CORTE MOTO'
	ELSE NULL
END PROCESSO,
CASE
	WHEN PT.TB_EQUIPE_ID IN ('E','M') THEN '8h 3E'
	WHEN PT.TB_EQUIPE_ID IN ('O','V') THEN '8h 5E'
	ELSE '8h 2E'
END SERV_CAMPO,
NULL SERV_CONTRATO,
NULL TIPO_EQP_CONTRATO,
NULL TIPO,
NULL QTD_ELETRICISTAS,
EXTRACT(YEAR FROM TO_DATE({{ get_month_ref() }},'YYYYMM')::date) ANO_REFERENCIA,
'ATIVA' STATUS,
TO_TIMESTAMP('2000-01-01 '||TO_CHAR(HORARIO_INICIO_ESCALA), 'YYYY-MM-DD HH24:MI:SS') INICIO_TURNO1,
TO_TIMESTAMP('2000-01-01 '||TO_CHAR(HORARIO_FIM_ESCALA), 'YYYY-MM-DD HH24:MI:SS') FIM_TURNO1,
HOUR(HORARIO_INICIO_ESCALA) INICIO_TURNO2,
HOUR(HORARIO_FIM_ESCALA) FIM_TURNO2,
CURRENT_TIMESTAMP ATUALIZACAO,
NULL OBS,
TO_DATE({{ get_month_ref() }},'YYYYMM')::date VIGENCIA_INICIAL_EQP,
LAST_DAY(TO_DATE({{ get_month_ref() }},'YYYYMM')::date) VIGENCIA_FINAL_EQP,
NULL LATITUDE,
NULL LONGITUDE
FROM EQTLINFO_RAW.OPER_GO.PREFIXO_TURMA PT
JOIN EQTLINFO_RAW.OPER_GO.BASE B ON B.BASE_ID = PT.BASE_ID
JOIN EQTLINFO_RAW.OPER_GO.PONTO_APOIO PA ON PA.CODIGO_PA = B.CODIGO_PA
JOIN SB_PERFORMANCE.EQTL_CORP.DICIONARIO_POLOS_OPER_GO DPO ON DPO.CODIGO_PA = PA.CODIGO_PA
JOIN EQTLINFO_RAW.OPER_GO.REGIAO_ELETRICA RE ON RE.REG_ELETRICA_ID = PA.REG_ELETRICA_ID
INNER JOIN (
	SELECT PREFIXO_UNICO, HORARIO_INICIO_ESCALA, HORARIO_FIM_ESCALA
	FROM (
		SELECT A.*,
		ROW_NUMBER() OVER (PARTITION BY PREFIXO_UNICO ORDER BY QTD DESC) RN
		FROM (
			SELECT PREFIXO_UNICO, HORARIO_INICIO_ESCALA::TIME HORARIO_INICIO_ESCALA, HORARIO_FIM_ESCALA::TIME HORARIO_FIM_ESCALA, COUNT(*) QTD
			FROM {{ ref('aderencia_escala') }} ae
			WHERE ae.ORIGEM = 'EQTL_GO'
			AND TRUNC(ae.DATA,'MM') = TO_DATE({{ get_month_ref() }},'YYYYMM')
			AND ae.HORARIO_INICIO_ESCALA IS NOT NULL
			GROUP BY ALL
			ORDER BY ALL
		) A
	) WHERE RN = 1
) AE ON AE.PREFIXO_UNICO = PT.PRX_DESCRICAO
LEFT JOIN (
	SELECT *
	FROM (
		SELECT 
		A.*,
		ROW_NUMBER() OVER (PARTITION BY PRX_DESCRICAO ORDER BY QTD) RN
		FROM (
			SELECT pt.PRX_DESCRICAO, v.VIA_TRACAO, COUNT(*) QTD
			FROM EQTLINFO_RAW.OPER_GO.HISTORICO_TURMA_PLANTAO htp
			LEFT JOIN EQTLINFO_RAW.OPER_GO.VIATURA v ON v.VIATURA_ID = htp.VIATURA_ID
			LEFT JOIN EQTLINFO_RAW.OPER_GO.PREFIXO_TURMA pt ON pt.PREFIXO_TURMA_ID = htp.PREFIXO_TURMA_ID
			WHERE TRUNC(HTP_INICIO_TURNO,'MM') = TO_DATE({{ get_month_ref() }},'YYYYMM')
			GROUP BY ALL
		) A
	) WHERE RN = 1
) V ON V.PRX_DESCRICAO = PT.PRX_DESCRICAO
LEFT JOIN {{ ref('slv_equipe_contrato') }} sec ON 
	sec.ANO = 2025
	AND SEC.MES = 11
	AND NVL(PT.PRX_NOME_INTERNO, PT.PRX_DESCRICAO) = SEC.PREFIXO
	AND SEC.COD_EMPRESA = 6
LEFT JOIN SB_PERFORMANCE.EQTL_CORP.SLV_CONTRATOS sc ON sc.CONTRATO_SAP = sec.CONTRATO AND sc.cod_empresa = 6