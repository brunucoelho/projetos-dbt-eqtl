SELECT 
EMPRESA||PREFIXO||NVL(FONECEDOR,'') CHAVE,
A.*
FROM (
SELECT
CASE
    WHEN EQ.EMPRESA = 'Amapá' then 'AMAPA'
    WHEN EQ.EMPRESA = 'Alagoas' then 'ALAGOAS'
    WHEN EQ.EMPRESA = 'Pará' then 'PARA'
    WHEN EQ.EMPRESA = 'Piauí' then 'PIAUI'
    WHEN EQ.EMPRESA = 'Maranhão' then 'MARANHAO'
    WHEN EQ.EMPRESA = 'Goiás' then 'GOIAS'
    WHEN EQ.EMPRESA = 'Rio Grande do Sul' then 'RIO GRANDE DO SUL'
END AS EMPRESA,
CASE
    WHEN EQ.EMPRESA = 'Amapá' then 'EQTL_AP'
    WHEN EQ.EMPRESA = 'Alagoas' then 'EQTL_AL'
    WHEN EQ.EMPRESA = 'Pará' then 'EQTL_PA'
    WHEN EQ.EMPRESA = 'Piauí' then 'EQTL_PI'
    WHEN EQ.EMPRESA = 'Maranhão' then 'EQTL_MA'
    WHEN EQ.EMPRESA = 'Goiás' then 'EQTL_GO'
    WHEN EQ.EMPRESA = 'Rio Grande do Sul' then 'EQTL_RS'
END AS DISTRIBUIDORA,
D.REGIONAL REGIONAL,
D.POLO POLO,
EQ.BASE MUNICIPIO,
SC.FORNECEDOR_SAP FONECEDOR,
EQ.PREFIXO PREFIXO,
CASE
	WHEN EQ.SIGLA_PROCESSO IN ('E') THEN 'ATENDIMENTO_EMERGENCIAL'
	WHEN EQ.SIGLA_PROCESSO IN ('C') THEN 'CORTE'
	WHEN EQ.SIGLA_PROCESSO IN ('R') THEN 'RELIGACAO'
	WHEN EQ.SIGLA_PROCESSO IN ('L') THEN 'LIGACAO_NOVA'
	WHEN EQ.SIGLA_PROCESSO IN ('O') THEN 'LINHA MORTA'
	WHEN EQ.SIGLA_PROCESSO IN ('U','T','V') THEN 'MANUTENCAO'
	WHEN EQ.SIGLA_PROCESSO IN ('M') THEN 'MULTIFUNCIONAL'
	WHEN EQ.SIGLA_PROCESSO IN ('F','I','J','S') THEN 'RECUPERACAO_ENERGIA'
	WHEN EQ.SIGLA_PROCESSO IN ('A') THEN 'GRUPO A'
	WHEN EQ.SIGLA_PROCESSO IN ('D') THEN 'SEED_MONEY'
	WHEN EQ.SIGLA_PROCESSO IN ('X','Y','G') THEN 'FISCAL'
	WHEN EQ.SIGLA_PROCESSO IN ('K') THEN 'MICROGERACAO'
	WHEN EQ.SIGLA_PROCESSO IN ('P','Z') THEN 'PODA'
	WHEN EQ.SIGLA_PROCESSO IN ('H') THEN 'CORTE_MOTO'
	ELSE NULL
END TIPO_EQP,
NULL SUBTIPO_EQP,
CASE
	WHEN EQ.SIGLA_PROCESSO IN ('E','M') THEN 'ATENDIMENTO EMERGENCIAL'
	WHEN EQ.SIGLA_PROCESSO IN ('C','R') THEN 'CORTE LEVE'
	WHEN EQ.SIGLA_PROCESSO IN ('L') AND UPPER(EQ.VEICULO_TIPO) LIKE '%4X4%' THEN 'LN 4X4'
	WHEN EQ.SIGLA_PROCESSO IN ('L','K') THEN 'LN LEVE'
	WHEN EQ.SIGLA_PROCESSO IN ('O') THEN 'LINHA MORTA'
	WHEN EQ.SIGLA_PROCESSO IN ('V') THEN 'LINHA VIVA'
	WHEN EQ.SIGLA_PROCESSO IN ('T') THEN 'TATICA'
	WHEN EQ.SIGLA_PROCESSO IN ('F','I','J','S') THEN 'PERDAS'
	WHEN EQ.SIGLA_PROCESSO IN ('A') THEN 'GRUPO A'
	WHEN EQ.SIGLA_PROCESSO IN ('D') THEN 'SEED MONEY'
	WHEN EQ.SIGLA_PROCESSO IN ('X','Y','G') THEN 'FISCAL'
	WHEN EQ.SIGLA_PROCESSO IN ('W') THEN 'EQUIPAMENTO'
	WHEN EQ.SIGLA_PROCESSO IN ('P','Z') THEN 'PODA'
	WHEN EQ.SIGLA_PROCESSO IN ('U','T') THEN 'MANUTENCAO'
	WHEN EQ.SIGLA_PROCESSO IN ('H') THEN 'CORTE MOTO'
	ELSE NULL
END PROCESSO,
CASE
    WHEN EQ.HORARIOS IS NULL THEN NULL
    WHEN UPPER(EQ.HORARIOS) LIKE '%DOM-DOM%' THEN '8h 3E'
    WHEN UPPER(EQ.HORARIOS) LIKE '%SEG-SAB%' THEN '8h 2E'
    WHEN UPPER(EQ.HORARIOS) LIKE '%SEG-SEX%' THEN '8h 2E'
    ELSE '8h 2E'
END SERV_CAMPO,
NULL SERV_CONTRATO,
NULL TIPO_EQP_CONTRATO,
NULL TIPO,
NULL QTD_ELETRICISTAS,
EXTRACT(YEAR FROM TO_DATE({{ get_month_ref() }},'YYYYMM')::date) ANO_REFERENCIA,
CASE
	WHEN EQ.ATIVO = 0 THEN 'DESMOBILIZADA'
	WHEN EQ.INICIO_VIGENCIA IS NULL THEN 'DESMOBILIZADA'
	WHEN EQ.INICIO_VIGENCIA > TO_DATE({{ get_month_ref() }},'YYYYMM') THEN 'MOBILIZANDO'
	WHEN NVL(EQ.FIM_VIGENCIA,'2050-12-01') < LAST_DAY(TO_DATE({{ get_month_ref() }},'YYYYMM')) THEN 'DESMOBILIZADA'
	ELSE 'ATIVA'
END STATUS,
TRY_TO_TIMESTAMP('2000-01-01 '||CASE WHEN REGEXP_REPLACE(
REGEXP_SUBSTR(Upper(HORARIOS), '([0-2]?[0-9])([H:]?([0-5][0-9]))?' ), 'H', ':'
) NOT LIKE '%:%' THEN LPAD(REGEXP_SUBSTR(Upper(HORARIOS), '([0-2]?[0-9])'), 2,
'0') || ':00' ELSE REGEXP_REPLACE(REGEXP_SUBSTR(Upper(HORARIOS),
'([0-2]?[0-9])([H:]?([0-5][0-9]))?' ), 'H', ':') END) AS INICIO_TURNO1,
TRY_TO_TIMESTAMP('2000-01-01 '||CASE WHEN REGEXP_REPLACE(REGEXP_SUBSTR(Upper(HORARIOS),
'([0-2]?[0-9])([H:]?([0-5][0-9]))?' , 1, 2), 'H', ':') NOT LIKE '%:%' THEN
LPAD( REGEXP_SUBSTR(Upper(HORARIOS), '([0-2]?[0-9])', 1, 2), 2, '0') || ':00'
ELSE REGEXP_REPLACE(REGEXP_SUBSTR(Upper(HORARIOS),
'([0-2]?[0-9])([H:]?([0-5][0-9]))?' , 1, 2), 'H', ':') END) AS FIM_TURNO1,
HOUR(TRY_TO_TIME(CASE WHEN REGEXP_REPLACE(
REGEXP_SUBSTR(Upper(HORARIOS), '([0-2]?[0-9])([H:]?([0-5][0-9]))?' ), 'H', ':'
) NOT LIKE '%:%' THEN LPAD(REGEXP_SUBSTR(Upper(HORARIOS), '([0-2]?[0-9])'), 2,
'0') || ':00' ELSE REGEXP_REPLACE(REGEXP_SUBSTR(Upper(HORARIOS),
'([0-2]?[0-9])([H:]?([0-5][0-9]))?' ), 'H', ':') END)) INICIO_TURNO2,
HOUR(TRY_TO_TIME(CASE WHEN REGEXP_REPLACE(REGEXP_SUBSTR(Upper(HORARIOS),
'([0-2]?[0-9])([H:]?([0-5][0-9]))?' , 1, 2), 'H', ':') NOT LIKE '%:%' THEN
LPAD( REGEXP_SUBSTR(Upper(HORARIOS), '([0-2]?[0-9])', 1, 2), 2, '0') || ':00'
ELSE REGEXP_REPLACE(REGEXP_SUBSTR(Upper(HORARIOS),
'([0-2]?[0-9])([H:]?([0-5][0-9]))?' , 1, 2), 'H', ':') END)) FIM_TURNO2,
CURRENT_TIMESTAMP ATUALIZACAO,
'CADASTRO MENSAL HORUS' OBS,
TO_DATE({{ get_month_ref() }},'YYYYMM')::date VIGENCIA_INICIAL_EQP,
LAST_DAY(TO_DATE({{ get_month_ref() }},'YYYYMM')::date) VIGENCIA_FINAL_EQP,
NULL LATITUDE,
NULL LONGITUDE
FROM SB_PERFORMANCE.BRONZE.EQUIPES_HORUS_20260109 EQ
LEFT JOIN SB_PERFORMANCE.EQTL_CORP.DICIONARIO_HORUS_UPS d ON 
	D.EMPRESA_HORUS = EQ.EMPRESA
	AND D.GERENCIA_HORUS = EQ.GERENCIA
	AND NVL(D.REGIONAL_HORUS,'') = NVL(EQ.REGIONAL,'')
	AND NVL(D.COORDENACAO_HORUS,'') = NVL(EQ.POLO,'')
	AND NVL(D.BASE_HORUS,'') = NVL(EQ.BASE,'')
LEFT JOIN {{ ref('slv_equipe_contrato') }} sec ON 
	sec.ANO = YEAR(TO_DATE({{ get_month_ref() }},'YYYYMM'))
	AND SEC.MES = MONTH(TO_DATE({{ get_month_ref() }},'YYYYMM'))
	AND EQ.PREFIXO = SEC.PREFIXO
LEFT JOIN SB_PERFORMANCE.EQTL_CORP.SLV_CONTRATOS sc ON sc.CONTRATO_SAP = sec.CONTRATO
WHERE EQ.ATIVO = 1
ORDER BY EQ.ATIVO DESC
) A
WHERE EMPRESA IS NOT NULL