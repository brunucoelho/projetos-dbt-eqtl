select 
CURRENT_TIMESTAMP DATA_ETL,
6 COD_EMPRESA,
REGEXP_REPLACE(TUR.PRX_DESCRICAO,'^([A-Z]{3})([A-Z])([0-9]{3})([A-Z])$','GO-'||'\\1-\\2\\3\\4') PREFIXO,
YEAR(TO_DATE({{ get_month_ref() }},'YYYYMM')) ANO,
MONTH(TO_DATE({{ get_month_ref() }},'YYYYMM')) MES,
'SGT' ORIGEM,
'GO_'||REGEXP_REPLACE(TUR.PRX_DESCRICAO,'^([A-Z]{3})([A-Z])([0-9]{3})([A-Z])$','GO-'||'\\1-\\2\\3\\4') TX_EXTERNALID,
NULL VIATURA,
CON.CTR_NUMERO CONTRATO
from EQTLINFO_RAW.SGTSERVICOS.PREFIXO_TURMA AS TUR 
inner join EQTLINFO_RAW.SGTSERVICOS.CONTRATO AS CON on TUR.PRX_CTR_ID = CON.CTR_ID 