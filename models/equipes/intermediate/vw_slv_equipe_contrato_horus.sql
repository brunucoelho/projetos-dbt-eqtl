SELECT 
CURRENT_TIMESTAMP DATA_ETL,
CASE
    WHEN EMPRESA = 'Amapá' then 2
    WHEN EMPRESA = 'Alagoas' then 1
    WHEN EMPRESA = 'Pará' then 4
    WHEN EMPRESA = 'Piauí' then 5
    WHEN EMPRESA = 'Maranhão' then 3
    WHEN EMPRESA = 'Goiás' then 6
    WHEN EMPRESA = 'Rio Grande do Sul' then 7
END AS COD_EMPRESA,
PREFIXO,
YEAR(TO_DATE({{ get_month_ref() }},'YYYYMM')) ANO,
MONTH(TO_DATE({{ get_month_ref() }},'YYYYMM')) MES,
'HORUS' ORIGEM,
RESOURCE_ID TX_EXTERNALID,
NULL VIATURA,
CONTRATO_SAP CONTRATO
FROM SB_PERFORMANCE.BRONZE.EQUIPES_HORUS eh
WHERE EMPRESA <> 'Goiás'
AND EH.ATIVO = 1